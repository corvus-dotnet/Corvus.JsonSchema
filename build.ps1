<#
.SYNOPSIS
    Runs a .NET flavoured build process.
.DESCRIPTION
    This script was scaffolded using a template from the Endjin.RecommendedPractices.Build PowerShell module.
    It uses the InvokeBuild module to orchestrate an opinonated software build process for .NET solutions.
.EXAMPLE
    PS C:\> ./build.ps1
    Downloads any missing module dependencies (Endjin.RecommendedPractices.Build & InvokeBuild) and executes
    the build process.
.PARAMETER Tasks
    Optionally override the default task executed as the entry-point of the build.
.PARAMETER Configuration
    The build configuration, defaults to 'Release'.
.PARAMETER BuildRepositoryUri
    Optional URI that supports pulling MSBuild logic from a web endpoint (e.g. a GitHub blob).
.PARAMETER SourcesDir
    The path where the source code to be built is located, defaults to the current working directory.
.PARAMETER CoverageDir
    The output path for the test coverage data, if run.
.PARAMETER TestReportTypes
    The test report format that should be generated by the test report generator, if run.
.PARAMETER PackagesDir
    The output path for any packages produced as part of the build.
.PARAMETER LogLevel
    The logging verbosity.
.PARAMETER Clean
    When true, the .NET solution will be cleaned and all output/intermediate folders deleted.
.PARAMETER Website
    When true, builds the documentation website after the .NET build completes.
    The website build reuses the already-compiled binaries (skips .NET compilation).
    Also set when the BUILDVAR_BuildWebsite environment variable is 'true' (for CI).
.PARAMETER BasePathPrefix
    Base URL path prefix for the documentation website (e.g. '/Corvus.Text.Json' for
    GitHub Pages subpath hosting). Only used when -Website is set.
    Falls back to the BUILDVAR_BasePathPrefix environment variable if not specified.
.PARAMETER BuildModulePath
    The path to import the Endjin.RecommendedPractices.Build module from. This is useful when
    testing pre-release versions of the Endjin.RecommendedPractices.Build that are not yet
    available in the PowerShell Gallery.
.PARAMETER BuildModuleVersion
    The version of the Endjin.RecommendedPractices.Build module to import. This is useful when
    testing pre-release versions of the Endjin.RecommendedPractices.Build that are not yet
    available in the PowerShell Gallery.
.PARAMETER InvokeBuildModuleVersion
    The version of the InvokeBuild module to be used.
#>
[CmdletBinding()]
param (
    [Parameter(Position=0)]
    [string[]] $Tasks = @("."),

    [Parameter()]
    [string] $Configuration = "Release",

    [Parameter()]
    [string] $BuildRepositoryUri = "",

    [Parameter()]
    [string] $SourcesDir = $PWD,

    [Parameter()]
    [string] $CoverageDir = "_codeCoverage",

    [Parameter()]
    [string] $TestReportTypes = "Cobertura",

    [Parameter()]
    [string] $PackagesDir = "_packages",

    [Parameter()]
    [ValidateSet("quiet","minimal","normal","detailed")]
    [string] $LogLevel = "minimal",

    [Parameter()]
    [switch] $Clean,

    [Parameter()]
    [switch] $Website,

    [Parameter()]
    [string] $BasePathPrefix = "",

    [Parameter()]
    [string] $BuildModulePath,

    [Parameter()]
    [version] $BuildModuleVersion = "1.5.14",

    [Parameter()]
    [version] $InvokeBuildModuleVersion = "5.14.23"
)

$ErrorActionPreference = $ErrorActionPreference ? $ErrorActionPreference : 'Stop'
$InformationPreference = 'Continue'

$here = Split-Path -Parent $PSCommandPath

#region InvokeBuild setup
if (!(Get-Module -ListAvailable InvokeBuild | Where-Object { $_.Version -eq $InvokeBuildModuleVersion })) {
    Install-Module InvokeBuild -RequiredVersion $InvokeBuildModuleVersion -Scope CurrentUser -Force -Repository PSGallery
}
Import-Module InvokeBuild -RequiredVersion $InvokeBuildModuleVersion
# This handles calling the build engine when this file is run like a normal PowerShell script
# (i.e. avoids the need to have another script to setup the InvokeBuild environment and issue the 'Invoke-Build' command )
if ($MyInvocation.ScriptName -notlike '*Invoke-Build.ps1') {
    try {
        # PS 7.4+ adds ProgressAction to $PSBoundParameters which clashes with InvokeBuild
        $PSBoundParameters.Remove('ProgressAction') | Out-Null
        Invoke-Build $Tasks $MyInvocation.MyCommand.Path @PSBoundParameters
    }
    catch {
        $_.ScriptStackTrace
        throw
    }
    return
}
#endregion

#region Import shared tasks and initialise build framework
if (!($BuildModulePath)) {
    if (!(Get-Module -ListAvailable Endjin.RecommendedPractices.Build | ? { $_.Version -eq $BuildModuleVersion })) {
        Write-Information "Installing 'Endjin.RecommendedPractices.Build' module..."
        Install-Module Endjin.RecommendedPractices.Build -RequiredVersion $BuildModuleVersion -Scope CurrentUser -Force -Repository PSGallery
    }
    $BuildModulePath = "Endjin.RecommendedPractices.Build"
}
else {
    Write-Information "BuildModulePath: $BuildModulePath"
}
Import-Module $BuildModulePath -RequiredVersion $BuildModuleVersion -Force

# Load the build process & tasks
. Endjin.RecommendedPractices.Build.tasks
#endregion


#
# Build process control options
#
$SkipInit = $false
$SkipVersion = $false
$SkipBuild = $false
$CleanBuild = $Clean
$SkipTest = $false
$SkipTestReport = $false
$SkipPackage = $false
$SkipAnalysis = $true  # Covenant SBOM tool (v0.19.0) does not support .slnx format
$SkipPrAutoflowEnrollmentCheck = $true
$BuildWebsite = $Website.IsPresent


#
# Build process configuration
#
$SolutionToBuild = (Resolve-Path (Join-Path $here ".\Corvus.Text.Json.slnx")).Path
$ProjectsToPublish = @(
    # "Solutions/MySolution/MyWebSite/MyWebSite.csproj"
)
$NuSpecFilesToPackage = @(
    # "Solutions/MySolution/MyProject/MyProject.nuspec"
)

# Run test assemblies sequentially to avoid OOM on CI runners (7 GB).
# The solution has 7 test assemblies; running them all in parallel exhausts memory.
# Exclude 'outerloop' (memory stress tests) and 'failing' (known failures) categories
# which are too resource-intensive for CI runners.
# Disable SourceLink queries during test — the JSON-Schema-Test-Suite submodule isn't
# checked out in the test phase, and LocateRepository emits a warning per project.
$AdditionalTestArgs = @(
    "-m:1",
    "--filter", 'category!=failing&category!=outerloop',
    '-p:EnableSourceControlManagerQueries=false'
)

$CreateGitHubRelease = $true
$PublishNuGetPackagesAsGitHubReleaseArtefacts = $true


# Synopsis: Build, Test and Package
task . FullBuild


# build extensibility tasks
task RunFirst {}
task PreInit {}
task PostInit {}
task PreVersion {}
task PostVersion {}
task PreBuild {
    Write-Host "Initialising submodule"
    exec { & git submodule init }
    exec { & git submodule update }
}
task PostBuild {
    # Build the documentation website when -Website is set or BUILDVAR_BuildWebsite env var is 'true'
    if ($BuildWebsite) {
        $effectiveBasePathPrefix = if ($BasePathPrefix) { $BasePathPrefix } else { $env:BUILDVAR_BasePathPrefix }

        $websiteDir = Join-Path $here "docs\website"

        Write-Information "Building documentation website..."

        # Ensure Node dependencies are installed
        Push-Location $websiteDir
        try {
            if (!(Test-Path (Join-Path $websiteDir "node_modules"))) {
                Write-Information "Installing Node dependencies..."
                exec { & npm ci --prefix $websiteDir }
            }

            $websiteBuildArgs = @("-SkipDotNetBuild")
            if ($effectiveBasePathPrefix) {
                $websiteBuildArgs += "-BasePathPrefix", $effectiveBasePathPrefix
            }

            exec { & pwsh -File (Join-Path $websiteDir "build.ps1") @websiteBuildArgs }
        } finally {
            Pop-Location
        }
    }
}
task PreTest {
    # Turn down logging when running Specs to suppress ReqnRoll Given/When/Then output
    $script:LogLevelBackup = $LogLevel
    $script:LogLevel = "quiet"

    # Three test projects target net10.0 only (analyzer + codegen tests).
    # When dotnet test runs for net8.0/net481 it can't find their DLLs and returns
    # exit code 1, which aborts the InvokeBuild pipeline (skipping PostTest etc.).
    # Switch to a test-specific solution that excludes those projects.
    $script:SolutionToTestBackup = $SolutionToBuild
    if ($TargetFrameworkMoniker -and $TargetFrameworkMoniker -ne "net10.0") {
        $testSlnx = (Resolve-Path (Join-Path $here ".\Corvus.Text.Json.Test.slnx")).Path
        $script:SolutionToBuild = $testSlnx
        Write-Build Yellow "PreTest: Using $testSlnx for $TargetFrameworkMoniker (excludes net10.0-only test projects)"
    }
}
task PostTest {
    # Revert solution and logging level
    $script:SolutionToBuild = $SolutionToTestBackup
    $script:LogLevel = $LogLevelBackup

    # The V4 Specs TRX file (~105 MB, 19K tests) exceeds lxml's text-node size limit
    # in the publish-unit-test-result-action (xmlSAX2Characters error).
    # Strip per-test <Output> elements (captured Reqnroll step text) to shrink the
    # file while keeping pass/fail results visible in the CI test report.
    $v4Path = Join-Path $SourcesDir "src-v4"
    Get-ChildItem -Path $v4Path -Filter "test-results_*.trx" -Recurse -ErrorAction SilentlyContinue |
        ForEach-Object {
            $sizeMB = [math]::Round($_.Length / 1MB, 1)
            Write-Build Yellow "PostTest: stripping test output from $($_.FullName) ($sizeMB MB)"
            try {
                $content = [System.IO.File]::ReadAllText($_.FullName)
                $content = [regex]::Replace(
                    $content,
                    '<Output>\s*<StdOut>.*?</StdOut>\s*</Output>',
                    '',
                    [System.Text.RegularExpressions.RegexOptions]::Singleline)
                [System.IO.File]::WriteAllText($_.FullName, $content)
                $newSizeMB = [math]::Round((Get-Item $_.FullName).Length / 1MB, 1)
                Write-Build Yellow "  Reduced to $newSizeMB MB"
            }
            catch {
                Write-Build Yellow "  Strip failed, removing file: $_"
                Remove-Item $_.FullName -Force -ErrorAction SilentlyContinue
            }
        }
}
task PreTestReport {
    # Backup: if PostTest didn't run, strip or remove V4 TRX files before publishing.
    $v4Path = Join-Path $SourcesDir "src-v4"
    Get-ChildItem -Path $v4Path -Filter "test-results_*.trx" -Recurse -ErrorAction SilentlyContinue |
        Where-Object { $_.Length -gt 10MB } |
        ForEach-Object {
            $sizeMB = [math]::Round($_.Length / 1MB, 1)
            Write-Build Yellow "PreTestReport: stripping large TRX $($_.FullName) ($sizeMB MB)"
            try {
                $content = [System.IO.File]::ReadAllText($_.FullName)
                $content = [regex]::Replace(
                    $content,
                    '<Output>\s*<StdOut>.*?</StdOut>\s*</Output>',
                    '',
                    [System.Text.RegularExpressions.RegexOptions]::Singleline)
                [System.IO.File]::WriteAllText($_.FullName, $content)
                $newSizeMB = [math]::Round((Get-Item $_.FullName).Length / 1MB, 1)
                Write-Build Yellow "  Reduced to $newSizeMB MB"
            }
            catch {
                Write-Build Yellow "  Strip failed, removing file: $_"
                Remove-Item $_.FullName -Force -ErrorAction SilentlyContinue
            }
        }
}
task PostTestReport {
    # GitHub PR comments have a 65536 character limit. The coverage summary for this
    # solution often exceeds that. Truncate and append a note when too large.
    $summaryPath = Join-Path $CoverageDir "SummaryGithub.md"
    $maxChars = 60000  # leave headroom for sticky-comment wrapper
    if (Test-Path $summaryPath) {
        $content = Get-Content -Raw -Path $summaryPath
        if ($content.Length -gt $maxChars) {
            $originalLen = $content.Length
            $truncated = $content.Substring(0, $maxChars)
            # Cut at last newline to avoid splitting a table row
            $lastNl = $truncated.LastIndexOf("`n")
            if ($lastNl -gt 0) { $truncated = $truncated.Substring(0, $lastNl) }
            $truncated += "`n`n---`n> **Note:** Coverage summary truncated from $originalLen to $($truncated.Length) characters. Full report is in the build artifacts.`n"
            Set-Content -Path $summaryPath -Value $truncated -Encoding UTF8 -NoNewline
            Write-Build Yellow "PostTestReport: truncated $summaryPath from $originalLen to $($truncated.Length) chars"
        }
    }
}
