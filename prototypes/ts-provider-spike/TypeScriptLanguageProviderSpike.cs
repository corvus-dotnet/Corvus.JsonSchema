using System.Diagnostics.CodeAnalysis;
using System.Text;
using System.Text.Json;
using Corvus.Json.CodeGeneration;

namespace TsProviderSpike;

// Feasibility spike #2: emit real AOT VALIDATORS (not just interfaces) from the core pipeline.
// Walks the core type graph (StandaloneEvaluator-style: one evaluate{Type} per subschema, recursing
// via PropertyDeclaration.ReducedPropertyType), reading constraints from LocatedSchema.Schema.
// Emits ONE combined module so the spike can compile + run it without import wiring.
// Scope: type / required / properties (recurse) / minLength-maxLength / pattern / minimum-maximum /
// integer / enum / const. Enough to prove the constraint model drives a working validator.
public sealed class TypeScriptLanguageProviderSpike : ILanguageProvider
{
    private const string TsNameKey = "Ts_TypeName";
    private const string TsFinalKey = "Ts_FinalName";

    public ILanguageProvider RegisterCodeFileBuilders(params ICodeFileBuilder[] builders) => this;

    public ILanguageProvider RegisterValidationHandlers(params IKeywordValidationHandler[] handlers) => this;

    public ILanguageProvider RegisterNameHeuristics(params INameHeuristic[] heuristics) => this;

    public bool TryGetValidationHandlersFor(IKeyword keyword, [NotNullWhen(true)] out IReadOnlyCollection<IKeywordValidationHandler>? validationHandlers)
    {
        validationHandlers = null;
        return false;
    }

    public bool ShouldGenerate(TypeDeclaration type) => true;

    public void IdentifyNonGeneratedType(TypeDeclaration typeDeclaration, CancellationToken cancellationToken)
    {
    }

    public void SetNamesBeforeSubschema(TypeDeclaration typeDeclaration, string fallbackName, CancellationToken cancellationToken)
    {
        if (!typeDeclaration.TryGetMetadata<string>(TsNameKey, out string? existing) || string.IsNullOrEmpty(existing))
        {
            typeDeclaration.SetMetadata(TsNameKey, Sanitize(fallbackName));
        }
    }

    public void SetNamesAfterSubschema(TypeDeclaration typeDeclaration, IEnumerable<TypeDeclaration> existingTypeDeclarations, CancellationToken cancellationToken)
    {
        if (!typeDeclaration.TryGetMetadata<string>(TsNameKey, out string? existing) || string.IsNullOrEmpty(existing))
        {
            typeDeclaration.SetMetadata(TsNameKey, "GeneratedType");
        }
    }

    public IReadOnlyCollection<GeneratedCodeFile> GenerateCodeFor(IEnumerable<TypeDeclaration> typeDeclarations, CancellationToken cancellationToken)
    {
        List<TypeDeclaration> types = typeDeclarations.ToList();

        // Pass 1: assign final, unique TS names so cross-references resolve.
        var used = new HashSet<string>(StringComparer.Ordinal);
        foreach (TypeDeclaration td in types)
        {
            string baseName = td.TryGetMetadata<string>(TsNameKey, out string? n) && !string.IsNullOrEmpty(n) ? n! : "GeneratedType";
            string name = baseName;
            int suffix = 2;
            while (!used.Add(name))
            {
                name = baseName + suffix++;
            }

            td.SetMetadata(TsFinalKey, name);
        }

        // Pass 2: emit one combined module (interface + validator per type).
        var sb = new StringBuilder();
        sb.Append("// AUTO-GENERATED by TypeScriptLanguageProviderSpike: interfaces + AOT validators.\n\n");
        foreach (TypeDeclaration td in types)
        {
            EmitInterface(sb, td);
            EmitValidator(sb, td);
        }

        return new[] { new GeneratedCodeFile("generated.ts", sb.ToString()) };
    }

    private static string FinalName(TypeDeclaration td)
        => td.TryGetMetadata<string>(TsFinalKey, out string? n) && !string.IsNullOrEmpty(n) ? n! : "GeneratedType";

    private static void EmitInterface(StringBuilder sb, TypeDeclaration td)
    {
        sb.Append("export interface ").Append(FinalName(td)).Append(" {\n");
        foreach (PropertyDeclaration p in td.PropertyDeclarations)
        {
            string opt = p.RequiredOrOptional == RequiredOrOptional.Optional ? "?" : string.Empty;
            sb.Append("  readonly ").Append(JsonString(p.JsonPropertyName)).Append(opt).Append(": unknown;\n");
        }

        sb.Append("}\n\n");
    }

    private static void EmitValidator(StringBuilder sb, TypeDeclaration td)
    {
        JsonElement schema = td.LocatedSchema.Schema;
        sb.Append("export function evaluate").Append(FinalName(td)).Append("(value: unknown): boolean {\n");

        // type: value must match one of the allowed kinds.
        List<string> kinds = ReadTypes(schema);
        if (kinds.Count > 0)
        {
            sb.Append("  if (!(").Append(string.Join(" || ", kinds.Select(KindExpr))).Append(")) { return false; }\n");
        }

        // string constraints
        if (TryInt(schema, "minLength", out int minLen))
        {
            sb.Append("  if (typeof value === \"string\" && [...value].length < ").Append(minLen).Append(") { return false; }\n");
        }

        if (TryInt(schema, "maxLength", out int maxLen))
        {
            sb.Append("  if (typeof value === \"string\" && [...value].length > ").Append(maxLen).Append(") { return false; }\n");
        }

        if (schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty("pattern", out JsonElement pat) && pat.ValueKind == JsonValueKind.String)
        {
            // ECMA-262 regex is native in JS (no translation). Production hoists the RegExp.
            sb.Append("  if (typeof value === \"string\" && !new RegExp(").Append(pat.GetRawText()).Append(", \"u\").test(value)) { return false; }\n");
        }

        // numeric constraints (spike uses JS compare; production uses exact text/BigInt per design 4.1)
        if (TryNum(schema, "minimum", out string? min))
        {
            sb.Append("  if (typeof value === \"number\" && value < ").Append(min).Append(") { return false; }\n");
        }

        if (TryNum(schema, "maximum", out string? max))
        {
            sb.Append("  if (typeof value === \"number\" && value > ").Append(max).Append(") { return false; }\n");
        }

        // enum / const -> deep-equality membership (spike uses JSON.stringify; production: exact numeric + deep structural)
        if (schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty("enum", out JsonElement en) && en.ValueKind == JsonValueKind.Array)
        {
            sb.Append("  { const allowed = ").Append(en.GetRawText()).Append("; if (!allowed.some((a) => JSON.stringify(a) === JSON.stringify(value))) { return false; } }\n");
        }

        if (schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty("const", out JsonElement co))
        {
            sb.Append("  if (JSON.stringify(value) !== JSON.stringify(").Append(co.GetRawText()).Append(")) { return false; }\n");
        }

        // object: required + recurse into each property's reduced type validator
        if (kinds.Contains("object") && td.PropertyDeclarations.Count > 0)
        {
            sb.Append("  if (typeof value === \"object\" && value !== null && !Array.isArray(value)) {\n");
            sb.Append("    const o = value as Record<string, unknown>;\n");
            foreach (PropertyDeclaration p in td.PropertyDeclarations)
            {
                string key = JsonStringLiteral(p.JsonPropertyName);
                bool required = p.RequiredOrOptional == RequiredOrOptional.Required || p.RequiredOrOptional == RequiredOrOptional.ComposedRequired;
                if (required)
                {
                    sb.Append("    if (!(").Append(key).Append(" in o)) { return false; }\n");
                }

                if (p.ReducedPropertyType.TryGetMetadata<string>(TsFinalKey, out string? pn) && !string.IsNullOrEmpty(pn))
                {
                    sb.Append("    if (").Append(key).Append(" in o && !evaluate").Append(pn).Append("(o[").Append(key).Append("])) { return false; }\n");
                }
            }

            sb.Append("  }\n");
        }

        sb.Append("  return true;\n}\n\n");
    }

    private static List<string> ReadTypes(JsonElement schema)
    {
        var result = new List<string>();
        if (schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty("type", out JsonElement t))
        {
            if (t.ValueKind == JsonValueKind.String)
            {
                result.Add(t.GetString()!);
            }
            else if (t.ValueKind == JsonValueKind.Array)
            {
                foreach (JsonElement item in t.EnumerateArray())
                {
                    if (item.ValueKind == JsonValueKind.String)
                    {
                        result.Add(item.GetString()!);
                    }
                }
            }
        }

        return result;
    }

    private static string KindExpr(string t) => t switch
    {
        "object" => "(typeof value === \"object\" && value !== null && !Array.isArray(value))",
        "array" => "Array.isArray(value)",
        "string" => "typeof value === \"string\"",
        "number" => "typeof value === \"number\"",
        "integer" => "(typeof value === \"number\" && Number.isInteger(value))",
        "boolean" => "typeof value === \"boolean\"",
        "null" => "value === null",
        _ => "true",
    };

    private static bool TryInt(JsonElement schema, string name, out int value)
    {
        value = 0;
        return schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty(name, out JsonElement e) && e.ValueKind == JsonValueKind.Number && e.TryGetInt32(out value);
    }

    private static bool TryNum(JsonElement schema, string name, out string? raw)
    {
        raw = null;
        if (schema.ValueKind == JsonValueKind.Object && schema.TryGetProperty(name, out JsonElement e) && e.ValueKind == JsonValueKind.Number)
        {
            raw = e.GetRawText();
            return true;
        }

        return false;
    }

    // a TS property-name token (quoted string-literal form, always valid)
    private static string JsonString(string name) => "\"" + name.Replace("\\", "\\\\").Replace("\"", "\\\"") + "\"";

    // a JS string literal for indexing/`in` checks
    private static string JsonStringLiteral(string name) => JsonString(name);

    private static string Sanitize(string value)
    {
        if (string.IsNullOrEmpty(value))
        {
            return "GeneratedType";
        }

        var sb = new StringBuilder();
        bool upperNext = true;
        foreach (char c in value)
        {
            if (char.IsLetterOrDigit(c))
            {
                sb.Append(upperNext ? char.ToUpperInvariant(c) : c);
                upperNext = false;
            }
            else
            {
                upperNext = true;
            }
        }

        if (sb.Length == 0)
        {
            return "GeneratedType";
        }

        if (char.IsDigit(sb[0]))
        {
            sb.Insert(0, '_');
        }

        return sb.ToString();
    }
}
