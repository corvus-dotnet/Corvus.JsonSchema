using System.Text;
using Corvus.Text;
using Corvus.Text.Json;

namespace Corvus.Text.Json.AsyncApi.CodeGeneration;

/// <summary>
/// A code generator that walks AsyncAPI 2.6 documents and emits C# source files
/// for producers, consumers, and message types.
/// </summary>
public sealed class AsyncApi26CodeGenerator
{
    private readonly AsyncApi30CodeGenerator emitter;
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="AsyncApi26CodeGenerator"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator.
    /// </param>
    public AsyncApi26CodeGenerator(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap)
    {
        this.emitter = new AsyncApi30CodeGenerator(rootNamespace, schemaTypeMap);
        this.schemaTypeMap = schemaTypeMap;
    }

    /// <summary>
    /// Walks the AsyncAPI 2.6 specification and collects schema JSON Pointer strings.
    /// </summary>
    /// <param name="doc">The parsed AsyncAPI document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">Optional external reference resolver.</param>
    /// <returns>An array of schema pointer strings.</returns>
    public static string[] CollectSchemaPointers(
        JsonElement doc,
        OperationFilter? filter = null,
        IAsyncApiReferenceResolver? referenceResolver = null)
    {
        List<string> pointers = [];

        if (doc.TryGetProperty("channels"u8, out JsonElement channels) &&
            channels.ValueKind == JsonValueKind.Object)
        {
            foreach (var channelProp in channels.EnumerateObject())
            {
                string channelName = channelProp.Name;
                if (filter?.Matches(channelName) == false)
                {
                    continue;
                }

                JsonElement channel = ResolveRef(channelProp.Value, doc, referenceResolver);
                if (channel.ValueKind != JsonValueKind.Object)
                {
                    continue;
                }

                AddOperationMessageSchemaPointers(channel, "publish"u8, channelProp.Utf8NameSpan.Span, doc, referenceResolver, pointers);
                AddOperationMessageSchemaPointers(channel, "subscribe"u8, channelProp.Utf8NameSpan.Span, doc, referenceResolver, pointers);
            }
        }

        if (doc.TryGetProperty("components"u8, out JsonElement components) &&
            components.ValueKind == JsonValueKind.Object)
        {
            if (components.TryGetProperty("schemas"u8, out JsonElement schemas) &&
                schemas.ValueKind == JsonValueKind.Object)
            {
                foreach (var schemaProp in schemas.EnumerateObject())
                {
                    pointers.Add(AsyncApiSchemaPointerBuilder.ComponentSchema(schemaProp.Utf8NameSpan.Span));
                }
            }

            if (components.TryGetProperty("messages"u8, out JsonElement messages) &&
                messages.ValueKind == JsonValueKind.Object)
            {
                foreach (var messageProp in messages.EnumerateObject())
                {
                    JsonElement message = ResolveRef(messageProp.Value, doc, referenceResolver);
                    if (message.ValueKind != JsonValueKind.Object)
                    {
                        continue;
                    }

                    if (message.TryGetProperty("payload"u8, out JsonElement payload) &&
                        payload.ValueKind != JsonValueKind.Undefined)
                    {
                        pointers.Add(AsyncApiSchemaPointerBuilder.ComponentMessagePayload(messageProp.Utf8NameSpan.Span));
                    }

                    if (message.TryGetProperty("headers"u8, out JsonElement headers) &&
                        headers.ValueKind != JsonValueKind.Undefined)
                    {
                        pointers.Add(AsyncApiSchemaPointerBuilder.ComponentMessageHeaders(messageProp.Utf8NameSpan.Span));
                    }
                }
            }
        }

        return [.. pointers];
    }

    /// <summary>
    /// Lists all operations in the AsyncAPI 2.6 specification, optionally filtered.
    /// </summary>
    /// <param name="doc">The parsed AsyncAPI document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <returns>An array of <see cref="AsyncApiOperationSummary"/> records.</returns>
    public static AsyncApiOperationSummary[] ListOperations(
        JsonElement doc,
        OperationFilter? filter = null)
    {
        List<AsyncApiOperationSummary> result = [];

        foreach (ChannelOperation operation in EnumerateChannelOperations(doc, null))
        {
            if (filter?.Matches(operation.ChannelAddress) == false)
            {
                continue;
            }

            List<string> tags = CollectTags(operation.Operation, doc, null);
            if (filter?.MatchesTags(tags) == false)
            {
                continue;
            }

            result.Add(new AsyncApiOperationSummary(
                operation.ChannelAddress,
                operation.Action,
                GetString(operation.Operation, "operationId"u8) ?? DeriveOperationName(operation.ChannelName, operation.OperationPropertyName),
                [.. tags],
                GetString(operation.Operation, "summary"u8),
                CountMessages(operation.Operation),
                Protocol: null));
        }

        return [.. result];
    }

    /// <summary>
    /// Walks the AsyncAPI 2.6 specification and emits C# source files.
    /// </summary>
    /// <param name="doc">The parsed AsyncAPI document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">Optional external reference resolver.</param>
    /// <returns>The generated source files.</returns>
    public IReadOnlyList<GeneratedFile> Generate(
        JsonElement doc,
        OperationFilter? filter = null,
        IAsyncApiReferenceResolver? referenceResolver = null)
    {
        (List<AsyncApi30CodeGenerator.OperationInfo> sendOps, List<AsyncApi30CodeGenerator.OperationInfo> receiveOps) =
            this.CollectOperations(doc, filter, referenceResolver);

        return this.emitter.GenerateOperations(sendOps, receiveOps, []);
    }

    /// <summary>
    /// Describes the document's channel operations with the generated producer/consumer details an Arazzo
    /// channel step needs (channel address, action, producer class + publish method, message payload
    /// types) — the AsyncAPI 2.6 counterpart of
    /// <see cref="AsyncApi30CodeGenerator.DescribeChannelOperations"/>. Collects operations from the 2.6
    /// channel/publish/subscribe shape, then maps them through the shared descriptor builder.
    /// </summary>
    /// <param name="doc">The parsed AsyncAPI 2.6 document.</param>
    /// <param name="filter">An optional channel filter.</param>
    /// <param name="referenceResolver">An optional reference resolver for cross-document <c>$ref</c>s.</param>
    /// <returns>One descriptor per channel operation.</returns>
    public IReadOnlyList<AsyncApiChannelDescriptor> DescribeChannelOperations(
        JsonElement doc,
        OperationFilter? filter = null,
        IAsyncApiReferenceResolver? referenceResolver = null)
    {
        (List<AsyncApi30CodeGenerator.OperationInfo> sendOps, List<AsyncApi30CodeGenerator.OperationInfo> receiveOps) =
            this.CollectOperations(doc, filter, referenceResolver);

        return this.emitter.BuildChannelDescriptors(sendOps, receiveOps);
    }

    private (List<AsyncApi30CodeGenerator.OperationInfo> Send, List<AsyncApi30CodeGenerator.OperationInfo> Receive) CollectOperations(
        JsonElement doc,
        OperationFilter? filter,
        IAsyncApiReferenceResolver? referenceResolver)
    {
        List<AsyncApi30CodeGenerator.OperationInfo> sendOps = [];
        List<AsyncApi30CodeGenerator.OperationInfo> receiveOps = [];

        foreach (ChannelOperation operation in EnumerateChannelOperations(doc, referenceResolver))
        {
            if (filter?.Matches(operation.ChannelAddress) == false)
            {
                continue;
            }

            List<string> tags = CollectTags(operation.Operation, doc, referenceResolver);
            if (filter?.MatchesTags(tags) == false)
            {
                continue;
            }

            List<AsyncApi30CodeGenerator.MessageInfo> messages = this.CollectOperationMessages(operation, doc, referenceResolver);
            List<AsyncApi30CodeGenerator.ChannelParameter> parameters = CollectChannelParameters(operation.Channel);
            AsyncApi30CodeGenerator.ReplyInfo? reply = this.CollectReplyInfo(operation.Operation, doc, referenceResolver);

            string? operationBindingsJson = operation.Operation.TryGetProperty("bindings"u8, out JsonElement operationBindings) &&
                operationBindings.ValueKind == JsonValueKind.Object
                    ? operationBindings.ToString()
                    : null;
            string? channelBindingsJson = operation.Channel.TryGetProperty("bindings"u8, out JsonElement channelBindings) &&
                channelBindings.ValueKind == JsonValueKind.Object
                    ? channelBindings.ToString()
                    : null;

            var info = new AsyncApi30CodeGenerator.OperationInfo(
                GetString(operation.Operation, "operationId"u8) ?? DeriveOperationName(operation.ChannelName, operation.OperationPropertyName),
                operation.Action,
                operation.ChannelAddress,
                IsDynamicAddress(operation.ChannelAddress),
                messages,
                parameters,
                reply,
                operation.ChannelName,
                GetChannelAllowedServers(operation.Channel),
                channelBindingsJson,
                operationBindingsJson,
                []);

            if (operation.Action == OperationAction.Send)
            {
                sendOps.Add(info);
            }
            else
            {
                receiveOps.Add(info);
            }
        }

        return (sendOps, receiveOps);
    }

    private static IEnumerable<ChannelOperation> EnumerateChannelOperations(
        JsonElement doc,
        IAsyncApiReferenceResolver? referenceResolver)
    {
        if (!doc.TryGetProperty("channels"u8, out JsonElement channels) ||
            channels.ValueKind != JsonValueKind.Object)
        {
            yield break;
        }

        foreach (var channelProp in channels.EnumerateObject())
        {
            JsonElement channel = ResolveRef(channelProp.Value, doc, referenceResolver);
            if (channel.ValueKind != JsonValueKind.Object)
            {
                continue;
            }

            string channelName = channelProp.Name;
            string channelAddress = GetChannelAddress(channel, channelName);

            if (channel.TryGetProperty("publish"u8, out JsonElement publish) &&
                publish.ValueKind == JsonValueKind.Object)
            {
                yield return new(channelName, channelAddress, channel, publish, OperationAction.Receive, "publish");
            }

            if (channel.TryGetProperty("subscribe"u8, out JsonElement subscribe) &&
                subscribe.ValueKind == JsonValueKind.Object)
            {
                yield return new(channelName, channelAddress, channel, subscribe, OperationAction.Send, "subscribe");
            }
        }
    }

    private List<AsyncApi30CodeGenerator.MessageInfo> CollectOperationMessages(
        ChannelOperation operation,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver)
    {
        List<AsyncApi30CodeGenerator.MessageInfo> messages = [];

        if (!operation.Operation.TryGetProperty("message"u8, out JsonElement messageElement) ||
            messageElement.ValueKind is JsonValueKind.Undefined or JsonValueKind.Null)
        {
            return messages;
        }

        if (messageElement.ValueKind == JsonValueKind.Object &&
            messageElement.TryGetProperty("oneOf"u8, out JsonElement oneOf) &&
            oneOf.ValueKind == JsonValueKind.Array)
        {
            int index = 0;
            foreach (JsonElement item in oneOf.EnumerateArray())
            {
                messages.Add(this.CreateMessageInfo(item, doc, resolver, operation.MessagePointerPrefix, $"Message{index}"));
                index++;
            }

            return messages;
        }

        messages.Add(this.CreateMessageInfo(messageElement, doc, resolver, operation.MessagePointerPrefix, "Message0"));
        return messages;
    }

    private AsyncApi30CodeGenerator.MessageInfo CreateMessageInfo(
        JsonElement messageRef,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        string inlineMessagePointer,
        string fallbackName)
    {
        JsonElement resolved = ResolveRef(messageRef, doc, resolver, out string? lastRefPointer);

        string messageName = GetString(resolved, "name"u8) ?? ExtractLastPointerSegment(lastRefPointer) ?? fallbackName;
        string? contentType = GetString(resolved, "contentType"u8) ?? GetString(doc, "defaultContentType"u8) ?? "application/json";
        string? messageBindingsJson = resolved.TryGetProperty("bindings"u8, out JsonElement bindings) &&
            bindings.ValueKind == JsonValueKind.Object
                ? bindings.ToString()
                : null;

        string? payloadPointer = null;
        string? payloadTypeName = null;
        if (resolved.TryGetProperty("payload"u8, out JsonElement payload) &&
            payload.ValueKind != JsonValueKind.Undefined)
        {
            payloadPointer = ResolveSchemaPointer(payload, doc, resolver, lastRefPointer, inlineMessagePointer, "payload");
            if (payloadPointer is not null)
            {
                this.schemaTypeMap.TryGetValue(payloadPointer, out payloadTypeName);
            }
        }

        string? headersPointer = null;
        string? headersTypeName = null;
        if (resolved.TryGetProperty("headers"u8, out JsonElement headers) &&
            headers.ValueKind != JsonValueKind.Undefined)
        {
            headersPointer = ResolveSchemaPointer(headers, doc, resolver, lastRefPointer, inlineMessagePointer, "headers");
            if (headersPointer is not null)
            {
                this.schemaTypeMap.TryGetValue(headersPointer, out headersTypeName);
            }
        }

        // The message's correlationId is inline ({ location, ... }) or a $ref to a named definition under
        // components.correlationIds — the $ref key is the name an Arazzo receive step's `correlationId` matches.
        string? correlationIdName = null;
        string? correlationIdLocation = null;
        if (resolved.TryGetProperty("correlationId"u8, out JsonElement correlationId) &&
            correlationId.ValueKind != JsonValueKind.Undefined)
        {
            correlationIdName = ExtractLastPointerSegment(TryGetRef(correlationId, resolver));
            correlationIdLocation = GetString(ResolveRef(correlationId, doc, resolver), "location"u8);
        }

        return new(messageName, payloadPointer, payloadTypeName, headersPointer, headersTypeName, contentType, messageBindingsJson, correlationIdName, correlationIdLocation);
    }

    private AsyncApi30CodeGenerator.ReplyInfo? CollectReplyInfo(
        JsonElement operation,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver)
    {
        if (!operation.TryGetProperty("x-corvus-reply"u8, out JsonElement reply) ||
            reply.ValueKind != JsonValueKind.Object)
        {
            return null;
        }

        string replyChannelAddress = string.Empty;
        if (reply.TryGetProperty("channel"u8, out JsonElement channelRef))
        {
            string? refPointer = TryGetRef(channelRef, resolver);
            JsonElement channel = ResolveRef(channelRef, doc, resolver);
            replyChannelAddress = GetChannelAddress(channel, ExtractLastPointerSegment(refPointer) ?? string.Empty);
        }

        string? addressLocationExpression = null;
        if (reply.TryGetProperty("address"u8, out JsonElement address) &&
            address.ValueKind == JsonValueKind.Object)
        {
            addressLocationExpression = GetString(address, "location"u8);
        }

        List<AsyncApi30CodeGenerator.MessageInfo> replyMessages = [];
        if (reply.TryGetProperty("message"u8, out JsonElement message) &&
            message.ValueKind != JsonValueKind.Undefined)
        {
            replyMessages.Add(this.CreateMessageInfo(message, doc, resolver, "#/x-corvus-reply/message", "Reply0"));
        }

        string? correlationIdLocation = null;
        if (operation.TryGetProperty("message"u8, out JsonElement requestMessage))
        {
            JsonElement resolvedRequestMessage = ResolveRef(requestMessage, doc, resolver);
            if (resolvedRequestMessage.ValueKind == JsonValueKind.Object &&
                resolvedRequestMessage.TryGetProperty("correlationId"u8, out JsonElement correlationId) &&
                correlationId.ValueKind == JsonValueKind.Object)
            {
                correlationIdLocation = GetString(correlationId, "location"u8);
            }
        }

        return new(replyChannelAddress, addressLocationExpression, replyMessages, correlationIdLocation);
    }

    private static void AddOperationMessageSchemaPointers(
        JsonElement channel,
        ReadOnlySpan<byte> operationName,
        ReadOnlySpan<byte> channelNameUtf8,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        List<string> pointers)
    {
        if (!channel.TryGetProperty(operationName, out JsonElement operation) ||
            operation.ValueKind != JsonValueKind.Object ||
            !operation.TryGetProperty("message"u8, out JsonElement message))
        {
            return;
        }

        if (message.ValueKind == JsonValueKind.Object &&
            message.TryGetProperty("oneOf"u8, out JsonElement oneOf) &&
            oneOf.ValueKind == JsonValueKind.Array)
        {
            foreach (JsonElement item in oneOf.EnumerateArray())
            {
                AddInlineMessagePointers(item, null, doc, resolver, pointers);
            }

            return;
        }

        string inlinePayloadPointer = operationName.SequenceEqual("publish"u8)
            ? AsyncApiSchemaPointerBuilder.ChannelPublishPayload(channelNameUtf8)
            : AsyncApiSchemaPointerBuilder.ChannelSubscribePayload(channelNameUtf8);
        AddInlineMessagePointers(message, inlinePayloadPointer, doc, resolver, pointers);
    }

    private static void AddInlineMessagePointers(
        JsonElement message,
        string? inlinePayloadPointer,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        List<string> pointers)
    {
        JsonElement resolved = ResolveRef(message, doc, resolver);
        if (resolved.ValueKind != JsonValueKind.Object)
        {
            return;
        }

        if (resolved.TryGetProperty("payload"u8, out JsonElement payload) &&
            payload.ValueKind != JsonValueKind.Undefined &&
            !IsRef(payload) &&
            inlinePayloadPointer is not null)
        {
            pointers.Add(inlinePayloadPointer);
        }
    }

    private static List<AsyncApi30CodeGenerator.ChannelParameter> CollectChannelParameters(JsonElement channel)
    {
        List<AsyncApi30CodeGenerator.ChannelParameter> parameters = [];
        if (!channel.TryGetProperty("parameters"u8, out JsonElement parametersElement) ||
            parametersElement.ValueKind != JsonValueKind.Object)
        {
            return parameters;
        }

        foreach (var parameterProp in parametersElement.EnumerateObject())
        {
            JsonElement parameter = parameterProp.Value;
            string? description = GetString(parameter, "description"u8);
            string? defaultValue = null;
            string[]? enumValues = null;

            if (parameter.TryGetProperty("schema"u8, out JsonElement schema) &&
                schema.ValueKind == JsonValueKind.Object)
            {
                defaultValue = GetString(schema, "default"u8);
                if (schema.TryGetProperty("enum"u8, out JsonElement enumElement) &&
                    enumElement.ValueKind == JsonValueKind.Array)
                {
                    List<string> values = [];
                    foreach (JsonElement value in enumElement.EnumerateArray())
                    {
                        if (value.ValueKind == JsonValueKind.String)
                        {
                            values.Add(value.GetString()!);
                        }
                    }

                    enumValues = [.. values];
                }
            }

            parameters.Add(new(parameterProp.Name, description, enumValues, defaultValue));
        }

        return parameters;
    }

    private static List<string> CollectTags(JsonElement operation, JsonElement doc, IAsyncApiReferenceResolver? resolver)
    {
        List<string> tags = [];
        if (!TryGetPropertyWithTraits(operation, "tags"u8, doc, resolver, out JsonElement tagsElement) ||
            tagsElement.ValueKind != JsonValueKind.Array)
        {
            return tags;
        }

        foreach (JsonElement tag in tagsElement.EnumerateArray())
        {
            if (tag.TryGetProperty("name"u8, out JsonElement name) &&
                name.ValueKind == JsonValueKind.String)
            {
                tags.Add(name.GetString()!);
            }
        }

        return tags;
    }

    private static bool TryGetPropertyWithTraits(
        JsonElement element,
        ReadOnlySpan<byte> propertyName,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        out JsonElement result)
    {
        if (element.TryGetProperty(propertyName, out result) &&
            result.ValueKind != JsonValueKind.Undefined)
        {
            return true;
        }

        if (element.TryGetProperty("traits"u8, out JsonElement traits) &&
            traits.ValueKind == JsonValueKind.Array)
        {
            JsonElement[] traitList = [.. traits.EnumerateArray()];
            for (int i = traitList.Length - 1; i >= 0; i--)
            {
                JsonElement resolvedTrait = ResolveRef(traitList[i], doc, resolver);
                if (resolvedTrait.ValueKind == JsonValueKind.Object &&
                    resolvedTrait.TryGetProperty(propertyName, out result) &&
                    result.ValueKind != JsonValueKind.Undefined)
                {
                    return true;
                }
            }
        }

        result = default;
        return false;
    }

    private static string? ResolveSchemaPointer(
        JsonElement schemaElement,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        string? messagePointer,
        string inlineMessagePointer,
        string suffix)
    {
        string? schemaRef = TryGetRef(schemaElement, resolver);
        if (schemaRef is not null)
        {
            return schemaRef;
        }

        return messagePointer is not null
            ? $"{messagePointer}/{suffix}"
            : $"{inlineMessagePointer}/{suffix}";
    }

    private static JsonElement ResolveRef(
        JsonElement element,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver = null)
    {
        return ResolveRef(element, doc, resolver, out _);
    }

    private static JsonElement ResolveRef(
        JsonElement element,
        JsonElement doc,
        IAsyncApiReferenceResolver? resolver,
        out string? lastRefPointer)
    {
        lastRefPointer = null;
        for (int i = 0; i < 10; i++)
        {
            if (element.ValueKind != JsonValueKind.Object)
            {
                return element;
            }

            if (!element.TryGetProperty("$ref"u8, out JsonElement refElement) ||
                refElement.ValueKind != JsonValueKind.String)
            {
                return element;
            }

            using UnescapedUtf8JsonString refUtf8 = refElement.GetUtf8String();
            ReadOnlySpan<byte> refSpan = refUtf8.Span;
            if (refSpan.StartsWith("#"u8))
            {
                if (Utf8JsonPointer.TryCreateJsonPointer(refSpan[1..], out Utf8JsonPointer ptr) &&
                    ptr.TryResolve<JsonElement, JsonElement>(doc, out JsonElement resolved))
                {
                    lastRefPointer = refElement.GetString()!;
                    element = resolved;
                    continue;
                }
            }
            else if (resolver is not null)
            {
                lastRefPointer = resolver.ResolveToAbsolute(refSpan);
                if (resolver.TryResolve(refSpan, out JsonElement resolved))
                {
                    element = resolved;
                    continue;
                }
            }

            return element;
        }

        return element;
    }

    private static string? TryGetRef(JsonElement element, IAsyncApiReferenceResolver? resolver)
    {
        if (element.ValueKind != JsonValueKind.Object ||
            !element.TryGetProperty("$ref"u8, out JsonElement refElement) ||
            refElement.ValueKind != JsonValueKind.String)
        {
            return null;
        }

        if (resolver is not null)
        {
            using UnescapedUtf8JsonString refUtf8 = refElement.GetUtf8String();
            if (!refUtf8.Span.StartsWith("#"u8))
            {
                return resolver.ResolveToAbsolute(refUtf8.Span);
            }
        }

        return refElement.GetString();
    }

    private static bool IsRef(JsonElement element)
    {
        return element.ValueKind == JsonValueKind.Object &&
            element.TryGetProperty("$ref"u8, out JsonElement refElement) &&
            refElement.ValueKind == JsonValueKind.String;
    }

    private static string? GetString(JsonElement element, ReadOnlySpan<byte> propertyName)
    {
        if (element.ValueKind == JsonValueKind.Object &&
            element.TryGetProperty(propertyName, out JsonElement value) &&
            value.ValueKind == JsonValueKind.String)
        {
            return value.GetString();
        }

        return null;
    }

    private static int CountMessages(JsonElement operation)
    {
        if (!operation.TryGetProperty("message"u8, out JsonElement message))
        {
            return 0;
        }

        if (message.ValueKind == JsonValueKind.Object &&
            message.TryGetProperty("oneOf"u8, out JsonElement oneOf) &&
            oneOf.ValueKind == JsonValueKind.Array)
        {
            return oneOf.GetArrayLength();
        }

        return 1;
    }

    private static string GetChannelAddress(JsonElement channel, string channelName)
    {
        if (channel.ValueKind == JsonValueKind.Object &&
            channel.TryGetProperty("address"u8, out JsonElement address) &&
            address.ValueKind == JsonValueKind.String)
        {
            return address.GetString()!;
        }

        return channelName;
    }

    private static bool IsDynamicAddress(string channelAddress)
    {
        return channelAddress.Length == 0;
    }

    private static IReadOnlyList<string>? GetChannelAllowedServers(JsonElement channel)
    {
        if (!channel.TryGetProperty("servers"u8, out JsonElement servers) ||
            servers.ValueKind != JsonValueKind.Array)
        {
            return null;
        }

        List<string> result = [];
        foreach (JsonElement server in servers.EnumerateArray())
        {
            if (server.ValueKind == JsonValueKind.String)
            {
                result.Add(server.GetString()!);
            }
        }

        return result.Count > 0 ? result : null;
    }

    private static string DeriveOperationName(string channelName, string operationPropertyName)
    {
        StringBuilder builder = new(operationPropertyName);
        bool capitalizeNext = true;
        foreach (char ch in channelName)
        {
            if (char.IsLetterOrDigit(ch))
            {
                builder.Append(capitalizeNext ? char.ToUpperInvariant(ch) : ch);
                capitalizeNext = false;
            }
            else
            {
                capitalizeNext = true;
            }
        }

        return builder.ToString();
    }

    private static string? ExtractLastPointerSegment(string? pointer)
    {
        if (string.IsNullOrEmpty(pointer))
        {
            return null;
        }

        int slash = pointer.LastIndexOf('/');
        string segment = slash >= 0 ? pointer[(slash + 1)..] : pointer;
        return segment.Replace("~1", "/").Replace("~0", "~");
    }

    private readonly record struct ChannelOperation(
        string ChannelName,
        string ChannelAddress,
        JsonElement Channel,
        JsonElement Operation,
        OperationAction Action,
        string OperationPropertyName)
    {
        public string MessagePointerPrefix
        {
            get
            {
                byte[] channelName = Encoding.UTF8.GetBytes(this.ChannelName);
                return this.OperationPropertyName == "publish"
                    ? AsyncApiSchemaPointerBuilder.ChannelPublishPayload(channelName).Replace("/payload", string.Empty, StringComparison.Ordinal)
                    : AsyncApiSchemaPointerBuilder.ChannelSubscribePayload(channelName).Replace("/payload", string.Empty, StringComparison.Ordinal);
            }
        }
    }
}