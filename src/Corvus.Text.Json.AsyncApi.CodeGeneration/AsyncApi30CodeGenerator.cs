using Corvus.Text.Json;
using Corvus.Text.Json.AsyncApi30;
using Corvus.Text.Json.OpenApi.CodeGeneration;

namespace Corvus.Text.Json.AsyncApi.CodeGeneration;

/// <summary>
/// A code generator that walks the strongly-typed <see cref="AsyncApiDocument"/>
/// model (3.0) and emits C# source files for producers, consumers, and message types.
/// </summary>
/// <remarks>
/// <para>
/// In AsyncAPI 3.x, operations are top-level with an <c>action</c> of <c>send</c> or
/// <c>receive</c>, and reference channels via <c>$ref</c>. Channels contain messages
/// whose payloads and headers are JSON Schemas.
/// </para>
/// </remarks>
public sealed class AsyncApi30CodeGenerator
{
    private readonly string rootNamespace;
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="AsyncApi30CodeGenerator"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator. Every schema pointer discovered
    /// during the walk must have a corresponding entry.
    /// </param>
    public AsyncApi30CodeGenerator(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap)
    {
        this.rootNamespace = rootNamespace;
        this.schemaTypeMap = schemaTypeMap;
    }

    /// <summary>
    /// Walks the AsyncAPI 3.0 specification and collects all schema
    /// JSON Pointer strings reachable from the selected operations.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter (filters by channel address).</param>
    /// <returns>An array of schema pointer strings.</returns>
    public static string[] CollectSchemaPointers(
        JsonElement specRoot,
        OperationFilter? filter = null)
    {
        AsyncApiDocument doc = specRoot;
        List<string> pointers = [];

        if (doc.ChannelsValue.IsUndefined())
        {
            return [];
        }

        // Collect schemas from channels → messages → payload/headers
        foreach (var channelProp in doc.ChannelsValue.EnumerateObject())
        {
            string channelName = channelProp.Name;

            AsyncApiDocument.Channel channel = (AsyncApiDocument.Channel)channelProp.Value;

            if (filter?.Matches(GetChannelAddress(channel, channelName)) == false)
            {
                continue;
            }

            CollectChannelSchemaPointers(channelName, channel, pointers);
        }

        // Collect schemas from components/schemas
        if (doc.ComponentsValue.IsNotUndefined())
        {
            JsonElement components = (JsonElement)doc.ComponentsValue;
            if (components.TryGetProperty("schemas"u8, out JsonElement schemas) &&
                schemas.ValueKind == JsonValueKind.Object)
            {
                foreach (var schemaProp in schemas.EnumerateObject())
                {
                    pointers.Add(AsyncApiSchemaPointerBuilder.ComponentSchema(schemaProp.Name));
                }
            }
        }

        return [.. pointers];
    }

    /// <summary>
    /// Lists all operations in the AsyncAPI 3.0 specification, optionally filtered.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter (filters by channel address).</param>
    /// <returns>An array of <see cref="AsyncApiOperationSummary"/> records.</returns>
    public static AsyncApiOperationSummary[] ListOperations(
        JsonElement specRoot,
        OperationFilter? filter = null)
    {
        AsyncApiDocument doc = specRoot;
        List<AsyncApiOperationSummary> result = [];

        if (doc.OperationsValue.IsUndefined())
        {
            return [];
        }

        foreach (var operationProp in doc.OperationsValue.EnumerateObject())
        {
            JsonElement opElement = (JsonElement)operationProp.Value;

            // Get the action (send/receive)
            if (!opElement.TryGetProperty("action"u8, out JsonElement actionEl) ||
                actionEl.ValueKind != JsonValueKind.String)
            {
                continue;
            }

            string actionStr = actionEl.GetString()!;
            OperationAction action = actionStr == "send" ? OperationAction.Send : OperationAction.Receive;

            // Resolve channel reference to get address
            string channelAddress = ResolveChannelAddress(opElement, doc);

            if (filter?.Matches(channelAddress) == false)
            {
                continue;
            }

            // Get operationId (the map key is often used as the id)
            string? operationId = null;
            if (opElement.TryGetProperty("operationId"u8, out JsonElement opIdEl) &&
                opIdEl.ValueKind == JsonValueKind.String)
            {
                operationId = opIdEl.GetString();
            }

            operationId ??= operationProp.Name;

            // Get summary
            string? summary = null;
            if (opElement.TryGetProperty("summary"u8, out JsonElement summaryEl) &&
                summaryEl.ValueKind == JsonValueKind.String)
            {
                summary = summaryEl.GetString();
            }

            // Get tags
            List<string> tags = [];
            if (opElement.TryGetProperty("tags"u8, out JsonElement tagsEl) &&
                tagsEl.ValueKind == JsonValueKind.Array)
            {
                foreach (JsonElement tag in tagsEl.EnumerateArray())
                {
                    if (tag.TryGetProperty("name"u8, out JsonElement nameEl) &&
                        nameEl.ValueKind == JsonValueKind.String)
                    {
                        tags.Add(nameEl.GetString()!);
                    }
                }
            }

            // Count messages
            int messageCount = 0;
            if (opElement.TryGetProperty("messages"u8, out JsonElement msgsEl) &&
                msgsEl.ValueKind == JsonValueKind.Array)
            {
                foreach (var msg in msgsEl.EnumerateArray())
                {
                    _ = msg;
                    messageCount++;
                }
            }

            result.Add(new AsyncApiOperationSummary(
                channelAddress,
                action,
                operationId,
                [.. tags],
                summary,
                messageCount,
                Protocol: null));
        }

        return [.. result];
    }

    /// <summary>
    /// Walks the AsyncAPI 3.0 specification and emits C# source files for
    /// producers, consumers, and message wrapper types.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter (filters by channel address).</param>
    /// <returns>The generated source files.</returns>
    public IReadOnlyList<GeneratedFile> Generate(
        JsonElement specRoot,
        OperationFilter? filter = null)
    {
        AsyncApiDocument doc = specRoot;
        List<GeneratedFile> files = [];

        if (doc.OperationsValue.IsUndefined())
        {
            return files;
        }

        List<OperationInfo> sendOps = [];
        List<OperationInfo> receiveOps = [];

        foreach (var operationProp in doc.OperationsValue.EnumerateObject())
        {
            JsonElement opElement = (JsonElement)operationProp.Value;

            if (!opElement.TryGetProperty("action"u8, out JsonElement actionEl) ||
                actionEl.ValueKind != JsonValueKind.String)
            {
                continue;
            }

            string actionStr = actionEl.GetString()!;
            OperationAction action = actionStr == "send" ? OperationAction.Send : OperationAction.Receive;
            string channelAddress = ResolveChannelAddress(opElement, doc);

            if (filter?.Matches(channelAddress) == false)
            {
                continue;
            }

            string operationName = operationProp.Name;
            List<MessageInfo> messages = CollectOperationMessages(opElement, doc);

            OperationInfo info = new(
                operationName,
                action,
                channelAddress,
                messages);

            if (action == OperationAction.Send)
            {
                sendOps.Add(info);
            }
            else
            {
                receiveOps.Add(info);
            }
        }

        // Emit producer types for send operations
        foreach (OperationInfo op in sendOps)
        {
            files.Add(EmitProducer(op));
        }

        // Emit consumer handler interfaces for receive operations
        foreach (OperationInfo op in receiveOps)
        {
            files.Add(EmitConsumerHandler(op));
        }

        // Emit message wrapper types for all operations
        foreach (OperationInfo op in sendOps.Concat(receiveOps))
        {
            foreach (MessageInfo msg in op.Messages)
            {
                if (msg.PayloadTypeName is not null)
                {
                    files.Add(EmitMessageWrapper(op, msg));
                }
            }
        }

        return files;
    }

    // ═══════════════════════════════════════════════════════════════════
    // Internal record types
    // ═══════════════════════════════════════════════════════════════════
    private readonly record struct OperationInfo(
        string Name,
        OperationAction Action,
        string ChannelAddress,
        List<MessageInfo> Messages);

    private readonly record struct MessageInfo(
        string Name,
        string? PayloadPointer,
        string? PayloadTypeName,
        string? HeadersPointer,
        string? HeadersTypeName);

    // ═══════════════════════════════════════════════════════════════════
    // Walk helpers
    // ═══════════════════════════════════════════════════════════════════
    private static void CollectChannelSchemaPointers(
        string channelName,
        AsyncApiDocument.Channel channel,
        List<string> pointers)
    {
        if (channel.Messages.IsNotUndefined())
        {
            JsonElement messages = (JsonElement)channel.Messages;
            if (messages.ValueKind == JsonValueKind.Object)
            {
                foreach (var msgProp in messages.EnumerateObject())
                {
                    string msgName = msgProp.Name;
                    JsonElement msg = msgProp.Value;

                    // If message is a $ref, the pointer target has the payload.
                    // Collect the channel-level pointer for inline payloads only.
                    if (msg.TryGetProperty("$ref"u8, out JsonElement _))
                    {
                        // This is a reference — the payload pointer is at the ref target.
                        // Component schemas are collected separately.
                        continue;
                    }

                    if (HasProperty(msg, "payload"u8))
                    {
                        pointers.Add(AsyncApiSchemaPointerBuilder.ChannelMessagePayload(channelName, msgName));
                    }

                    if (HasProperty(msg, "headers"u8))
                    {
                        pointers.Add(AsyncApiSchemaPointerBuilder.ChannelMessageHeaders(channelName, msgName));
                    }
                }
            }
        }
    }

    private static string GetChannelAddress(AsyncApiDocument.Channel channel, string channelName)
    {
        if (channel.Address.IsNotUndefined())
        {
            JsonElement addr = (JsonElement)channel.Address;
            if (addr.ValueKind == JsonValueKind.String)
            {
                return addr.GetString()!;
            }
        }

        return channelName;
    }

    private static string ResolveChannelAddress(JsonElement operation, AsyncApiDocument doc)
    {
        if (!operation.TryGetProperty("channel"u8, out JsonElement channelRef))
        {
            return string.Empty;
        }

        // Channel is a $ref to #/channels/{name}
        if (channelRef.TryGetProperty("$ref"u8, out JsonElement refEl) &&
            refEl.ValueKind == JsonValueKind.String)
        {
            string refStr = refEl.GetString()!;

            // Extract channel name from #/channels/{name}
            const string prefix = "#/channels/";
            if (refStr.StartsWith(prefix, StringComparison.Ordinal))
            {
                string channelName = refStr[prefix.Length..];

                // Try to get the channel and its address
                if (doc.ChannelsValue.TryGetProperty(channelName, out var channelEntity))
                {
                    AsyncApiDocument.Channel channel = (AsyncApiDocument.Channel)channelEntity;
                    return GetChannelAddress(channel, channelName);
                }

                return channelName;
            }
        }

        return string.Empty;
    }

    private List<MessageInfo> CollectOperationMessages(JsonElement operation, AsyncApiDocument doc)
    {
        List<MessageInfo> messages = [];

        if (!operation.TryGetProperty("messages"u8, out JsonElement msgsEl) ||
            msgsEl.ValueKind != JsonValueKind.Array)
        {
            return messages;
        }

        int index = 0;
        foreach (JsonElement msgRef in msgsEl.EnumerateArray())
        {
            string? payloadPointer = null;
            string? payloadTypeName = null;
            string? headersPointer = null;
            string? headersTypeName = null;
            string messageName = $"Message{index}";

            // Resolve $ref if present
            JsonElement resolved = ResolveRef(msgRef, doc);

            // Try to extract the name from the resolved message
            if (resolved.TryGetProperty("name"u8, out JsonElement nameEl) &&
                nameEl.ValueKind == JsonValueKind.String)
            {
                messageName = nameEl.GetString()!;
            }
            else if (msgRef.TryGetProperty("$ref"u8, out JsonElement refEl) &&
                     refEl.ValueKind == JsonValueKind.String)
            {
                // Extract name from ref path
                string refStr = refEl.GetString()!;
                int lastSlash = refStr.LastIndexOf('/');
                if (lastSlash >= 0)
                {
                    messageName = refStr[(lastSlash + 1)..];
                }
            }

            if (resolved.TryGetProperty("payload"u8, out JsonElement _))
            {
                // Determine the pointer based on where this message is defined
                payloadPointer = TryGetPointerFromRef(msgRef, "payload");
                if (payloadPointer is not null)
                {
                    this.schemaTypeMap.TryGetValue(payloadPointer, out payloadTypeName);
                }
            }

            if (resolved.TryGetProperty("headers"u8, out JsonElement _))
            {
                headersPointer = TryGetPointerFromRef(msgRef, "headers");
                if (headersPointer is not null)
                {
                    this.schemaTypeMap.TryGetValue(headersPointer, out headersTypeName);
                }
            }

            messages.Add(new MessageInfo(messageName, payloadPointer, payloadTypeName, headersPointer, headersTypeName));
            index++;
        }

        return messages;
    }

    private static JsonElement ResolveRef(JsonElement element, AsyncApiDocument doc)
    {
        // Resolve up to 10 levels of $ref to handle chained references
        for (int i = 0; i < 10; i++)
        {
            if (element.TryGetProperty("$ref"u8, out JsonElement refEl) &&
                refEl.ValueKind == JsonValueKind.String)
            {
                string refStr = refEl.GetString()!;

                // Navigate the pointer within the document
                if (refStr.StartsWith("#/", StringComparison.Ordinal))
                {
                    JsonElement root = (JsonElement)doc;
                    element = NavigatePointer(root, refStr[2..]);
                    continue;
                }
            }

            break;
        }

        return element;
    }

    private static JsonElement NavigatePointer(JsonElement root, string pointer)
    {
        JsonElement current = root;
        foreach (string segment in pointer.Split('/'))
        {
            string unescaped = segment.Replace("~1", "/").Replace("~0", "~");
            if (current.TryGetProperty(unescaped, out JsonElement next))
            {
                current = next;
            }
            else
            {
                return default;
            }
        }

        return current;
    }

    private static string? TryGetPointerFromRef(JsonElement msgRef, string suffix)
    {
        if (msgRef.TryGetProperty("$ref"u8, out JsonElement refEl) &&
            refEl.ValueKind == JsonValueKind.String)
        {
            string refStr = refEl.GetString()!;
            return $"{refStr}/{suffix}";
        }

        return null;
    }

    private static bool HasProperty(JsonElement element, ReadOnlySpan<byte> propertyName)
    {
        return element.TryGetProperty(propertyName, out JsonElement prop) &&
               prop.ValueKind != JsonValueKind.Undefined;
    }

    // ═══════════════════════════════════════════════════════════════════
    // Code emission
    // ═══════════════════════════════════════════════════════════════════
    private GeneratedFile EmitProducer(OperationInfo op)
    {
        string className = $"{ToPascalCase(op.Name)}Producer";
        IndentedWriter w = new();

        w.WriteLine("// <auto-generated/>");
        w.WriteLine($"namespace {this.rootNamespace};");
        w.WriteLine();
        w.WriteLine($"/// <summary>");
        w.WriteLine($"/// Producer for the <c>{op.Name}</c> operation on channel <c>{op.ChannelAddress}</c>.");
        w.WriteLine($"/// </summary>");
        w.WriteLine($"public sealed class {className}");
        w.OpenBrace();

        w.WriteLine("private readonly IMessageTransport transport;");
        w.WriteLine($"private const string ChannelAddress = \"{EscapeString(op.ChannelAddress)}\";");
        w.WriteLine();

        w.WriteLine($"/// <summary>");
        w.WriteLine($"/// Initializes a new instance of the <see cref=\"{className}\"/> class.");
        w.WriteLine($"/// </summary>");
        w.WriteLine($"public {className}(IMessageTransport transport)");
        w.OpenBrace();
        w.WriteLine("this.transport = transport;");
        w.CloseBrace();

        foreach (MessageInfo msg in op.Messages)
        {
            w.WriteLine();
            string payloadType = msg.PayloadTypeName ?? "Corvus.Text.Json.JsonElement";
            string methodName = $"Publish{ToPascalCase(msg.Name)}Async";
            w.WriteLine($"/// <summary>");
            w.WriteLine($"/// Publishes a <c>{msg.Name}</c> message.");
            w.WriteLine($"/// </summary>");
            w.WriteLine($"public ValueTask {methodName}({payloadType} payload, CancellationToken cancellationToken = default)");
            w.OpenBrace();
            w.WriteLine("return this.transport.PublishAsync(ChannelAddress, payload.SerializeToUtf8Bytes(), cancellationToken);");
            w.CloseBrace();
        }

        w.CloseBrace();

        return new GeneratedFile($"{className}.cs", w.ToString());
    }

    private GeneratedFile EmitConsumerHandler(OperationInfo op)
    {
        string interfaceName = $"I{ToPascalCase(op.Name)}Handler";
        IndentedWriter w = new();

        w.WriteLine("// <auto-generated/>");
        w.WriteLine($"namespace {this.rootNamespace};");
        w.WriteLine();
        w.WriteLine($"/// <summary>");
        w.WriteLine($"/// Handler interface for the <c>{op.Name}</c> receive operation on channel <c>{op.ChannelAddress}</c>.");
        w.WriteLine($"/// </summary>");
        w.WriteLine($"public interface {interfaceName}");
        w.OpenBrace();

        foreach (MessageInfo msg in op.Messages)
        {
            string payloadType = msg.PayloadTypeName ?? "Corvus.Text.Json.JsonElement";
            string methodName = $"Handle{ToPascalCase(msg.Name)}Async";
            w.WriteLine($"/// <summary>");
            w.WriteLine($"/// Handles a <c>{msg.Name}</c> message.");
            w.WriteLine($"/// </summary>");
            w.WriteLine($"ValueTask {methodName}({payloadType} payload, CancellationToken cancellationToken = default);");
            w.WriteLine();
        }

        w.CloseBrace();

        return new GeneratedFile($"{interfaceName}.cs", w.ToString());
    }

    private GeneratedFile EmitMessageWrapper(OperationInfo op, MessageInfo msg)
    {
        string className = $"{ToPascalCase(op.Name)}{ToPascalCase(msg.Name)}Message";
        string payloadType = msg.PayloadTypeName ?? "Corvus.Text.Json.JsonElement";
        IndentedWriter w = new();

        w.WriteLine("// <auto-generated/>");
        w.WriteLine($"namespace {this.rootNamespace};");
        w.WriteLine();
        w.WriteLine($"/// <summary>");
        w.WriteLine($"/// Typed message wrapper for <c>{msg.Name}</c> in operation <c>{op.Name}</c>.");
        w.WriteLine($"/// </summary>");
        w.WriteLine($"public readonly record struct {className}(");
        w.WriteLine($"    {payloadType} Payload,");
        if (msg.HeadersTypeName is not null)
        {
            w.WriteLine($"    {msg.HeadersTypeName} Headers);");
        }
        else
        {
            w.WriteLine($"    Corvus.Text.Json.JsonElement? Headers = default);");
        }

        return new GeneratedFile($"{className}.cs", w.ToString());
    }

    private static string ToPascalCase(string name)
    {
        if (string.IsNullOrEmpty(name))
        {
            return name;
        }

        // Simple PascalCase: capitalize first letter, preserve rest
        return char.ToUpperInvariant(name[0]) + name[1..];
    }

    private static string EscapeString(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }
}