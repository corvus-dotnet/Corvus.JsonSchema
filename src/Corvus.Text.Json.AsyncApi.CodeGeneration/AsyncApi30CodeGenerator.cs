    // Traits merge + content type / schema format
    // ═══════════════════════════════════════════════════════════════════

    /// <summary>
    /// Gets a property from a message, checking its traits array for fallbacks.
    /// In JSON Merge Patch semantics: traits provide defaults, the message itself wins.
    /// Traits are checked in reverse array order (later traits have higher priority).
    /// </summary>
    private static bool TryGetPropertyWithTraits(
        JsonElement element,
        ReadOnlySpan<byte> propertyName,
        AsyncApiDocument doc,
        IAsyncApiReferenceResolver? resolver,
        out JsonElement result)
    {
        // Target property takes precedence
        if (element.TryGetProperty(propertyName, out result) &&
            result.ValueKind != JsonValueKind.Undefined)
        {
            return true;
        }

        // Check traits in reverse order (later traits override earlier ones)
        if (element.TryGetProperty("traits"u8, out JsonElement traitsEl) &&
            traitsEl.ValueKind == JsonValueKind.Array)
        {
            // Walk traits in reverse so the last one wins
            JsonElement[] traitList = [.. traitsEl.EnumerateArray()];

            for (int i = traitList.Length - 1; i >= 0; i--)
            {
                JsonElement resolvedTrait = ResolveRef(traitList[i], doc, resolver);
                if (resolvedTrait.ValueKind == JsonValueKind.Object &&
                    resolvedTrait.TryGetProperty(propertyName, out result) &&
                    result.ValueKind != JsonValueKind.Undefined)
                {
                    return true;
                }
            }
        }

        result = default;
        return false;
    }

    private static string? GetMessageContentType(JsonElement message, AsyncApiDocument doc, IAsyncApiReferenceResolver? resolver = null)
    {
        // Message-level contentType takes precedence (including from traits)
        if (TryGetPropertyWithTraits(message, "contentType"u8, doc, resolver, out JsonElement ctEl) &&
            ctEl.ValueKind == JsonValueKind.String)
        {
            return ctEl.GetString();
        }

        // Fall back to root defaultContentType
        if (doc.DefaultContentType.IsNotUndefined())
        {
            return doc.DefaultContentType.GetString();
        }

        // AsyncAPI default is application/json when not specified
        return "application/json";
    }

    private static void ValidateContentType(string? contentType, string messageName)
    {
        if (contentType is null)
        {
            return;
        }

        // Accept JSON-compatible content types
        if (contentType.Contains("json", StringComparison.OrdinalIgnoreCase) ||
            contentType.Contains("octet-stream", StringComparison.OrdinalIgnoreCase))
        {
            return;
        }

        ThrowHelper.ThrowUnsupportedContentType(messageName, contentType);
    }

    private static void ValidateSchemaFormat(JsonElement message, string messageName)
    {
        // Check payload for multiFormatSchema with unsupported schemaFormat
        if (message.TryGetProperty("payload"u8, out JsonElement payloadEl) &&
            payloadEl.ValueKind == JsonValueKind.Object &&
            payloadEl.TryGetProperty("schemaFormat"u8, out JsonElement formatEl) &&
            formatEl.ValueKind == JsonValueKind.String)
        {
            string format = formatEl.GetString()!;
            if (!IsJsonSchemaFormat(format))
            {
                ThrowHelper.ThrowUnsupportedSchemaFormat(messageName, format);
            }
        }
    }

    private static bool IsJsonSchemaFormat(string format)
    {
        // Recognized JSON Schema format strings per AsyncAPI spec
        return format.Contains("schema+json", StringComparison.OrdinalIgnoreCase) ||
               format.Contains("asyncapi", StringComparison.OrdinalIgnoreCase) ||
               format.Contains("json-schema", StringComparison.OrdinalIgnoreCase) ||
               format.StartsWith("application/vnd.aai", StringComparison.OrdinalIgnoreCase);
    }

    /// <summary>
    /// Unwraps a multiFormatSchema object to its inner <c>schema</c> property.
    /// If the element has a <c>schemaFormat</c> and <c>schema</c>, returns the <c>schema</c>.
    /// Otherwise returns the element unchanged.
    /// </summary>
    private static JsonElement UnwrapMultiFormatSchema(JsonElement element)
    {
        if (element.ValueKind == JsonValueKind.Object &&
            element.TryGetProperty("schemaFormat"u8, out _) &&
            element.TryGetProperty("schema"u8, out JsonElement innerSchema))
        {
            return innerSchema;
        }

        return element;
    }

    // ═══════════════════════════════════════════════════════════════════
    // Reference resolution
    // ═══════════════════════════════════════════════════════════════════
    private static JsonElement ResolveRef(JsonElement element, AsyncApiDocument doc, IAsyncApiReferenceResolver? resolver = null)
    {
        return ResolveRef(element, doc, resolver, out _);
    }

    private static JsonElement ResolveRef(
        JsonElement element,
        AsyncApiDocument doc,
        IAsyncApiReferenceResolver? resolver,
        out string? lastRefPointer)
    {
        lastRefPointer = null;

        // Resolve up to 10 levels of $ref to handle chained references
        for (int i = 0; i < 10; i++)
        {
            AsyncApiDocument.Reference asRef = element;
            if (!asRef.Ref.IsNotUndefined())
            {
                break;
            }

            using UnescapedUtf8JsonString refUtf8 = asRef.Ref.GetUtf8String();
            ReadOnlySpan<byte> refSpan = refUtf8.Span;

            if (refSpan.StartsWith("#"u8))
            {
                // Local fragment reference — resolve via built-in JSON Pointer
                if (Utf8JsonPointer.TryCreateJsonPointer(refSpan[1..], out Utf8JsonPointer ptr) &&
                    ptr.TryResolve<AsyncApiDocument, JsonElement>(doc, out JsonElement resolved))
                {
                    lastRefPointer = asRef.Ref.GetString()!;
                    element = resolved;
                    continue;
                }
            }
            else if (resolver is not null)
            {
                // External reference — resolve to absolute for the pointer
                lastRefPointer = resolver.ResolveToAbsolute(refSpan);
                if (resolver.TryResolve(refSpan, out JsonElement resolved))
                {
                    element = resolved;
                    continue;
                }
            }

            break;
        }

        return element;
    }

    /// <summary>
    /// Resolves the actual schema pointer for a payload or headers element.
    /// If the schema element itself is a $ref, returns that ref target.
    /// Otherwise follows the message ref chain via ResolveRef to find
    /// the canonical message location, then appends the suffix (payload/headers).
    /// </summary>
    private static string? ResolveSchemaPointer(
        JsonElement schemaElement,
        JsonElement msgRef,
        ReadOnlySpan<byte> suffix,
        AsyncApiDocument doc,
        IAsyncApiReferenceResolver? resolver = null)
    {
        // If the schema element is itself a $ref (e.g. payload: { $ref: "#/components/schemas/X" }),
        // the canonical pointer is the $ref target — resolve to absolute if external.
        AsyncApiDocument.Reference schemaAsRef = schemaElement;
        if (schemaAsRef.Ref.IsNotUndefined())
        {
            if (resolver is not null)
            {
                using UnescapedUtf8JsonString schemaRefUtf8 = schemaAsRef.Ref.GetUtf8String();
                return resolver.ResolveToAbsolute(schemaRefUtf8.Span);
            }

            return schemaAsRef.Ref.GetString()!;
        }

        // Otherwise the schema is inline — follow the message ref chain to find where
        // the message actually lives, then append /suffix to get the schema location.
        AsyncApiDocument.Reference msgAsRef = msgRef;
        if (!msgAsRef.Ref.IsNotUndefined())
        {
            return null;
        }

        // ResolveRef follows the chain and returns the last ref pointer it resolved through.
        ResolveRef(msgRef, doc, resolver, out string? lastRefPointer);

        if (lastRefPointer is null)
        {
            return null;
        }

        return $"{lastRefPointer}/{System.Text.Encoding.UTF8.GetString(suffix)}";
    }

