    private List<MessageInfo> CollectOperationMessages(JsonElement operation, AsyncApiDocument doc, IAsyncApiReferenceResolver? resolver = null)
    {
        List<MessageInfo> messages = [];

        if (!TryGetPropertyWithTraits(operation, "messages"u8, doc, resolver, out JsonElement msgsEl) ||
            msgsEl.ValueKind != JsonValueKind.Array)
        {
            return messages;
        }

        int index = 0;
        foreach (JsonElement msgRef in msgsEl.EnumerateArray())
        {
            string? payloadPointer = null;
            string? payloadTypeName = null;
            string? headersPointer = null;
            string? headersTypeName = null;
            string messageName = $"Message{index}";
            string? contentType = null;

            // Resolve $ref if present
            JsonElement resolved = ResolveRef(msgRef, doc, resolver);

            // Try to extract the name (checking traits for fallback)
            if (TryGetPropertyWithTraits(resolved, "name"u8, doc, resolver, out JsonElement nameEl) &&
                nameEl.ValueKind == JsonValueKind.String)
            {
                messageName = nameEl.GetString()!;
            }
            else
            {
                // Extract name from ref path
                AsyncApiDocument.Reference msgAsRef = msgRef;
                if (msgAsRef.Ref.IsNotUndefined())
                {
                    string refStr = msgAsRef.Ref.GetString()!;
                    int lastSlash = refStr.LastIndexOf('/');
                    if (lastSlash >= 0)
                    {
                        messageName = refStr[(lastSlash + 1)..];
                    }
                }
            }

            // Check contentType (from message/traits or root defaultContentType)
            contentType = GetMessageContentType(resolved, doc, resolver);
            ValidateContentType(contentType, messageName);

            // Check schemaFormat for unsupported formats
            ValidateSchemaFormat(resolved, messageName);

            // Extract message bindings as raw JSON
            string? messageBindingsJson = null;
            if (TryGetPropertyWithTraits(resolved, "bindings"u8, doc, resolver, out JsonElement bindingsEl) &&
                bindingsEl.ValueKind == JsonValueKind.Object)
            {
                messageBindingsJson = bindingsEl.ToString();
            }

            // Get payload (checking traits for fallback)
            if (TryGetPropertyWithTraits(resolved, "payload"u8, doc, resolver, out JsonElement payloadEl))
            {
                // Handle multiFormatSchema wrapping
                payloadEl = UnwrapMultiFormatSchema(payloadEl);

                payloadPointer = ResolveSchemaPointer(payloadEl, msgRef, "payload"u8, doc, resolver);
                if (payloadPointer is not null)
                {
                    this.schemaTypeMap.TryGetValue(payloadPointer, out payloadTypeName);
                }
            }

            // Get headers (checking traits for fallback)
            if (TryGetPropertyWithTraits(resolved, "headers"u8, doc, resolver, out JsonElement headersEl))
            {
                // Handle multiFormatSchema wrapping
                headersEl = UnwrapMultiFormatSchema(headersEl);

                headersPointer = ResolveSchemaPointer(headersEl, msgRef, "headers"u8, doc, resolver);
                if (headersPointer is not null)
                {
                    this.schemaTypeMap.TryGetValue(headersPointer, out headersTypeName);
                }
            }

            messages.Add(new MessageInfo(messageName, payloadPointer, payloadTypeName, headersPointer, headersTypeName, contentType, messageBindingsJson));
            index++;
        }

        return messages;
    }

    private static List<ChannelParameter> CollectChannelParameters(JsonElement operation, AsyncApiDocument doc, IAsyncApiReferenceResolver? resolver = null)
    {
        List<ChannelParameter> parameters = [];

        // Resolve the channel $ref to get the channel object (checking operation traits)
        if (!TryGetPropertyWithTraits(operation, "channel"u8, doc, resolver, out JsonElement channelRef))
        {
            return parameters;
        }

        JsonElement channelElement = ResolveRef(channelRef, doc, resolver);
        if (channelElement.ValueKind != JsonValueKind.Object)
        {
            return parameters;
        }

        AsyncApiDocument.Channel channel = channelElement;
        if (channel.ParametersValue.IsUndefined())
        {
            return parameters;
        }

        foreach (var paramProp in channel.ParametersValue.EnumerateObject())
        {
            string paramName = paramProp.Name;
            AsyncApiDocument.Parameter param = paramProp.Value.Match(
                matchReference: static (in AsyncApiDocument.Reference _) => default,
                matchParameter: static (in AsyncApiDocument.Parameter p) => p,
                defaultMatch: static (in AsyncApiDocument.Parameters.AdditionalPropertiesEntity _) => default);

            if (param.IsUndefined())
            {
                continue;
            }

            string? description = param.Description.IsNotUndefined()
                ? param.Description.GetString()
                : null;

            string? defaultValue = param.Default.IsNotUndefined()
                ? param.Default.GetString()
                : null;

            string[]? enumValues = null;
            if (param.Enum.IsNotUndefined())
            {
                List<string> values = [];
                foreach (var v in param.Enum.EnumerateArray())
                {
                    values.Add((string)v);
                }

                enumValues = [.. values];
            }

            parameters.Add(new ChannelParameter(paramName, description, enumValues, defaultValue));
        }

        return parameters;
    }

    private ReplyInfo? CollectReplyInfo(JsonElement operation, AsyncApiDocument doc, IAsyncApiReferenceResolver? resolver = null)
    {
        if (!TryGetPropertyWithTraits(operation, "reply"u8, doc, resolver, out JsonElement replyEl) ||
            replyEl.ValueKind != JsonValueKind.Object)
        {
            return null;
        }

        // Resolve reply channel address
        string replyChannelAddress = string.Empty;
        if (replyEl.TryGetProperty("channel"u8, out JsonElement channelRef))
        {
            JsonElement resolvedChannel = ResolveRef(channelRef, doc, resolver);
            if (resolvedChannel.ValueKind == JsonValueKind.Object)
            {
                AsyncApiDocument.Channel channel = resolvedChannel;
                if (channel.Address.IsNotUndefined() && channel.Address.ValueKind == JsonValueKind.String)
                {
                    replyChannelAddress = channel.Address.GetString()!;
                }
            }
        }

        // Check for address.location (runtime expression for dynamic reply address)
        string? addressLocationExpression = null;
        if (replyEl.TryGetProperty("address"u8, out JsonElement addressEl) &&
            addressEl.ValueKind == JsonValueKind.Object &&
            addressEl.TryGetProperty("location"u8, out JsonElement locationEl) &&
            locationEl.ValueKind == JsonValueKind.String)
        {
            // Dynamic reply address — stored separately from the static channel address.
            // (e.g., "$message.header#/replyTo" means the message header contains the reply-to address)
            addressLocationExpression = locationEl.GetString()!;
        }

        // Collect reply messages
        List<MessageInfo> replyMessages = [];
        if (replyEl.TryGetProperty("messages"u8, out JsonElement msgsEl) &&
            msgsEl.ValueKind == JsonValueKind.Array)
        {
            int index = 0;
            foreach (JsonElement msgRef in msgsEl.EnumerateArray())
            {
                JsonElement resolved = ResolveRef(msgRef, doc, resolver);
                string messageName = $"Reply{index}";

                if (TryGetPropertyWithTraits(resolved, "name"u8, doc, resolver, out JsonElement nameEl) &&
                    nameEl.ValueKind == JsonValueKind.String)
                {
                    messageName = nameEl.GetString()!;
                }
                else
                {
                    AsyncApiDocument.Reference replyMsgAsRef = msgRef;
                    if (replyMsgAsRef.Ref.IsNotUndefined())
                    {
                        string refStr = replyMsgAsRef.Ref.GetString()!;
                        int lastSlash = refStr.LastIndexOf('/');
                        if (lastSlash >= 0)
                        {
                            messageName = refStr[(lastSlash + 1)..];
                        }
                    }
                }

                string? payloadPointer = null;
                string? payloadTypeName = null;
                string? headersPointer = null;
                string? headersTypeName = null;

                if (TryGetPropertyWithTraits(resolved, "payload"u8, doc, resolver, out JsonElement payloadEl))
                {
                    payloadEl = UnwrapMultiFormatSchema(payloadEl);
                    payloadPointer = ResolveSchemaPointer(payloadEl, msgRef, "payload"u8, doc, resolver);
                    if (payloadPointer is not null)
                    {
                        this.schemaTypeMap.TryGetValue(payloadPointer, out payloadTypeName);
                    }
                }

                if (TryGetPropertyWithTraits(resolved, "headers"u8, doc, resolver, out JsonElement headersEl))
                {
                    headersEl = UnwrapMultiFormatSchema(headersEl);
                    headersPointer = ResolveSchemaPointer(headersEl, msgRef, "headers"u8, doc, resolver);
                    if (headersPointer is not null)
                    {
                        this.schemaTypeMap.TryGetValue(headersPointer, out headersTypeName);
                    }
                }

                replyMessages.Add(new MessageInfo(messageName, payloadPointer, payloadTypeName, headersPointer, headersTypeName, null, null));
                index++;
            }
        }

        // Extract correlation ID location from the operation's request messages
        string? correlationIdLocation = null;
        if (operation.TryGetProperty("messages"u8, out JsonElement opMsgsEl) &&
            opMsgsEl.ValueKind == JsonValueKind.Array)
        {
            foreach (JsonElement msgRef in opMsgsEl.EnumerateArray())
            {
                JsonElement resolved = ResolveRef(msgRef, doc, resolver);
                if (TryGetPropertyWithTraits(resolved, "correlationId"u8, doc, resolver, out JsonElement corrIdEl) &&
                    corrIdEl.ValueKind == JsonValueKind.Object &&
                    corrIdEl.TryGetProperty("location"u8, out JsonElement locEl) &&
                    locEl.ValueKind == JsonValueKind.String)
                {
                    correlationIdLocation = locEl.GetString();
                    break;
                }
            }
        }

        return new ReplyInfo(replyChannelAddress, addressLocationExpression, replyMessages, correlationIdLocation);
    }

