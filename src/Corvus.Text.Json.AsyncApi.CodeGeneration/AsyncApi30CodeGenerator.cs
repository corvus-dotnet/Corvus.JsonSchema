                segments.Add((false, template[pos..braceStart]));
            }

            int braceEnd = template.IndexOf('}', braceStart);
            string paramName = template[(braceStart + 1)..braceEnd];
            segments.Add((true, paramName));
            pos = braceEnd + 1;
        }

        // Calculate total byte count (literal lengths are known at compile time)
        int literalByteCount = segments.Where(s => !s.IsParam).Sum(s => System.Text.Encoding.UTF8.GetByteCount(s.Value));

        // Emit byte count calculation
        w.Write($"int channelByteCount = {literalByteCount}");
        foreach ((bool isParam, string value) in segments.Where(s => s.IsParam))
        {
            w.Write($" + Encoding.UTF8.GetByteCount({ToCamelCase(value)})");
        }

        w.WriteLine(";");

        // Rent buffer and copy segments
        w.WriteLine("byte[] channelRental = ArrayPool<byte>.Shared.Rent(channelByteCount);");
        w.WriteLine("int channelPos = 0;");

        foreach ((bool isParam, string value) in segments)
        {
            if (isParam)
            {
                w.WriteLine($"channelPos += Encoding.UTF8.GetBytes({ToCamelCase(value)}, channelRental.AsSpan(channelPos));");
            }
            else
            {
                string escaped = EscapeString(value);
                int byteCount = System.Text.Encoding.UTF8.GetByteCount(value);
                w.WriteLine($"\"{escaped}\"u8.CopyTo(channelRental.AsSpan(channelPos));");
                w.WriteLine($"channelPos += {byteCount};");
            }
        }

        w.WriteLine("ReadOnlyMemory<byte> channelUtf8 = channelRental.AsMemory(0, channelPos);");
    }

    private static string EscapeString(string value)
    {
        return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    private static string EscapeRawString(string value)
    {
        // Raw string literals delimited by """ cannot contain sequences of 3+ consecutive quotes.
        // JSON content shouldn't have this, but escape defensively.
        return value.Replace("\"\"\"", "\\\"\\\"\\\"");
    }

    // ═══════════════════════════════════════════════════════════════════
    // Runtime expression parsing
    // ═══════════════════════════════════════════════════════════════════

    /// <summary>
    /// Parses an AsyncAPI runtime expression (e.g., <c>$message.header#/replyTo</c>)
    /// and returns a code expression that extracts the value at runtime.
    /// </summary>
    /// <param name="expression">The runtime expression string.</param>
    /// <param name="payloadVar">The variable name holding the payload value.</param>
    /// <param name="headersVar">The variable name holding the headers value (may be "default").</param>
    /// <returns>A C# expression string that evaluates the runtime expression, or <see langword="null"/>
    /// if the expression cannot be parsed.</returns>
    internal static string? EmitRuntimeExpressionAccessor(string expression, string payloadVar, string headersVar)
    {
        AsyncApiRuntimeExpression parsed = AsyncApiRuntimeExpression.Parse(expression);

        return parsed.Kind switch
        {
            AsyncApiRuntimeExpressionKind.MessageHeader => EmitPointerNavigation(headersVar, parsed.JsonPointer!),
            AsyncApiRuntimeExpressionKind.MessagePayload => EmitPointerNavigation(payloadVar, parsed.JsonPointer!),
            _ => null,
        };
    }

    private static string EmitPointerNavigation(string sourceVar, string pointer)
    {
        // Convert JSON Pointer "/foo/bar" into chained GetProperty calls.
        // For simple single-segment pointers like "/replyTo", emit:
        //   sourceVar.GetProperty("replyTo"u8).GetString()!
        // For multi-segment pointers like "/nested/path", emit chained navigation.
        if (!pointer.StartsWith("/", StringComparison.Ordinal))
        {
            return $"{sourceVar}.ToString()";
        }

        string[] segments = pointer[1..].Split('/');
        string nav = sourceVar;
        foreach (string segment in segments)
        {
            // Unescape JSON Pointer (~1 = /, ~0 = ~)
            string unescaped = segment.Replace("~1", "/").Replace("~0", "~");
            nav = $"{nav}.GetProperty(\"{EscapeString(unescaped)}\"u8)";
        }

        return $"{nav}.GetString()!";
    }
