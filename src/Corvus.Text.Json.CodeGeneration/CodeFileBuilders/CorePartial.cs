using Corvus.Json.CodeGeneration;

namespace Corvus.Text.Json.CodeGeneration;

/// <summary>
/// Provides the core file for a type declaration.
/// </summary>
public sealed class CorePartial : ICodeFileBuilder
{
    private CorePartial()
    {
    }

    /// <summary>
    /// Gets a singleton instance of the <see cref="CorePartial"/> builder.
    /// </summary>
    public static CorePartial Instance { get; } = new();

    /// <inheritdoc/>
    public CodeGenerator EmitFile(CodeGenerator generator, TypeDeclaration typeDeclaration)
    {
        FrameworkType addExplicitUsings = typeDeclaration.AddExplicitUsings() ? FrameworkType.All : FrameworkType.NotEmitted;

        generator
            .BeginFile(typeDeclaration, string.Empty)
                .AppendAutoGeneratedHeader()
                .AppendLine()
                .AppendLine("#nullable enable")
                .AppendLine()
                .AppendUsings(
                    new("global::System", addExplicitUsings),
                    new("global::System.Diagnostics"),
                    new("global::System.Diagnostics.CodeAnalysis"),
                    "global::System.Buffers",
                    "global::System.Buffers.Text",
                    "global::System.Runtime.CompilerServices",
                    "global::Corvus.Text.Json",
                    "global::Corvus.Text.Json.Internal")
                .AppendLine()
                .BeginTypeDeclarationNesting(typeDeclaration)
                    .AppendDocumentation(typeDeclaration)
                    .AppendDebuggerDisplay()
                    .BeginPartialStructDeclaration(
                        typeDeclaration.DotnetAccessibility(),
                        typeDeclaration.DotnetTypeName(),
                        interfaces: [
                            typeDeclaration.GetIJsonElementInterface(forMutable: false),
                            "IFormattable",
                            new("ISpanFormattable", FrameworkType.Net80OrGreater),
                            new("IUtf8SpanFormattable", FrameworkType.Net80OrGreater),
                            ])
                        .PushJsonSchemaClassNameAndScope()
                        .PushJsonPropertyNamesEscapedClassNameAndScope()
                        .PushJsonPropertyNamesClassNameAndScope()
                        .PushBuilderClassNameAndScope()
                        .PushSourceClassNameAndScope()
                        .PushConstantsClassNameAndScope()
                        .PushMutableClassNameAndScope()
                        .AppendBackingFields()
                        .AppendInternalConstructor(typeDeclaration)
                        .AppendDefaultInstanceStaticProperty(typeDeclaration)
                        .AppendArrayRankStaticProperty(typeDeclaration)
                        .AppendArrayDimensionStaticProperty(typeDeclaration)
                        .AppendArrayValueBufferSizeStaticProperty(typeDeclaration)
                        .AppendTryGetNumericValues(typeDeclaration)
                        .AppendConstInstanceStaticProperty(typeDeclaration)
                        .AppendArrayIndexerProperties(typeDeclaration)
                        .AppendTupleItemProperties(typeDeclaration)
                        .AppendObjectIndexerProperties(typeDeclaration)
                        .AppendObjectTryGetPropertyMethods(typeDeclaration)
                        .AppendObjectProperties(typeDeclaration)
                        .AppendCoreTypeAndFormatValueGetters(typeDeclaration)
                        .AppendGetArrayLength(typeDeclaration)
                        .AppendGetPropertyCount(typeDeclaration)
                        .AppendEnumerateArray(typeDeclaration)
                        .AppendEnumerateObject(typeDeclaration)
                        .AppendValueKindProperty()
                        .AppendTokenTypeProperty()
                        .AppendConversionToCompositionTypes(typeDeclaration)
                        .AppendCoreTypeAndFormatConversionOperators(typeDeclaration, forMutable: false)
                        .AppendBinaryOperator(typeDeclaration, "bool", "==", "return left.Equals(right);", "<c>True</c> if the values are equal.")
                        .AppendBinaryOperator(typeDeclaration, "bool", "!=", "return !left.Equals(right);", "<c>True</c> if the values are not equal.")
                        .AppendBinaryOperator(typeDeclaration, "JsonElement", "bool", "==", "return left.Equals(right);", "<c>True</c> if the values are equal.")
                        .AppendBinaryOperator(typeDeclaration, "JsonElement", "bool", "!=", "return !left.Equals(right);", "<c>True</c> if the values are not equal.")
                        .AppendJsonElementConversionOperator(typeDeclaration)
                        .AppendFromFactoryMethod(typeDeclaration)
                        .AppendParseValue(typeDeclaration)
                        .AppendEqualsOverloads(typeDeclaration)
                        .AppendValueEqualsOverloads(typeDeclaration)
                        .AppendWriteToMethod()
                        .AppendGetHashCodeAndToStringMethods(typeDeclaration, forMutable: false)
                        .AppendEvaluateSchemaMethod()
                        .AppendCheckValidInstance()
                        .AppendCreateInstance(typeDeclaration)
                        .AppendDebuggerDisplayProperty(typeDeclaration)
                        .AppendIJsonElementExplicitImplementation()
                        .AppendTryGetAsCompositionTypeMethods(typeDeclaration)
                        .AppendMatchMethods(typeDeclaration)
                        .AppendJsonPropertyNames(typeDeclaration)
                        .AppendJsonPropertyNamesEscaped(typeDeclaration)
                        .PopMutableClassNameAndScope()
                        .PopConstantsClassNameAndScope()
                        .PopSourceClassNameAndScope()
                        .PopBuilderClassNameAndScope()
                        .PopJsonPropertyNamesClassNameAndScope()
                        .PopJsonPropertyNamesEscapedClassNameAndScope()
                        .PopJsonSchemaClassNameAndScope()
                    .EndClassStructOrEnumDeclaration()
                .EndTypeDeclarationNesting(typeDeclaration)
                .EndNamespace()
            .EndFile(typeDeclaration, string.Empty);

        return generator;
    }
}