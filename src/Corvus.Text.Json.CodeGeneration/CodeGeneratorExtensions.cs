    /// <param name="typeDeclaration">The type declaration for which to emit the properties.</param>
    /// <returns>A reference to the generator having completed the operation.</returns>
    public static CodeGenerator AppendSubschemaEvaluationPathStaticProperties(this CodeGenerator generator, TypeDeclaration typeDeclaration)
    {
        if (typeDeclaration.AllOfCompositionTypes() is IReadOnlyDictionary<IAllOfSubschemaValidationKeyword, IReadOnlyCollection<TypeDeclaration>> allOfDictionary)
        {
            foreach (IAllOfSubschemaValidationKeyword keyword in allOfDictionary.Keys)
            {
                if (generator.IsCancellationRequested)
                {
                    return generator;
                }

                IReadOnlyCollection<TypeDeclaration> subschemaTypes = allOfDictionary[keyword];
                int i = 0;
                foreach (TypeDeclaration subschemaType in subschemaTypes)
                {
                    if (generator.IsCancellationRequested)
                    {
                        return generator;
                    }

                    ReducedTypeDeclaration reducedType = subschemaType.ReducedTypeDeclaration();
                    string evaluationPathProperty = generator.GetPropertyNameInScope($"{keyword.Keyword}{i}SchemaEvaluationPath");
                    string evaluationPath = SymbolDisplay.FormatLiteral(keyword.GetPathModifier(reducedType, i), true);
                    generator
                        .AppendLineIndent(
                            "private static readonly JsonSchemaPathProvider ",
                            evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                            evaluationPath,
                            "u8, buffer, out written);");
                    i++;
                }
            }
        }

        if (typeDeclaration.AnyOfCompositionTypes() is IReadOnlyDictionary<IAnyOfSubschemaValidationKeyword, IReadOnlyCollection<TypeDeclaration>> anyOfDictionary)
        {
            foreach (IAnyOfSubschemaValidationKeyword keyword in anyOfDictionary.Keys)
            {
                if (generator.IsCancellationRequested)
                {
                    return generator;
                }

                IReadOnlyCollection<TypeDeclaration> subschemaTypes = anyOfDictionary[keyword];
                int i = 0;
                foreach (TypeDeclaration subschemaType in subschemaTypes)
                {
                    if (generator.IsCancellationRequested)
                    {
                        return generator;
                    }

                    ReducedTypeDeclaration reducedType = subschemaType.ReducedTypeDeclaration();
                    string evaluationPathProperty = generator.GetPropertyNameInScope($"{keyword.Keyword}{i}SchemaEvaluationPath");
                    string evaluationPath = SymbolDisplay.FormatLiteral(keyword.GetPathModifier(reducedType, i), true);
                    generator
                        .AppendLineIndent(
                            "private static readonly JsonSchemaPathProvider ",
                            evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                            evaluationPath,
                            "u8, buffer, out written);");
                    i++;
                }
            }
        }

        if (typeDeclaration.OneOfCompositionTypes() is IReadOnlyDictionary<IOneOfSubschemaValidationKeyword, IReadOnlyCollection<TypeDeclaration>> oneOfDictionary)
        {
            foreach (IOneOfSubschemaValidationKeyword keyword in oneOfDictionary.Keys)
            {
                if (generator.IsCancellationRequested)
                {
                    return generator;
                }

                IReadOnlyCollection<TypeDeclaration> subschemaTypes = oneOfDictionary[keyword];
                int i = 0;
                foreach (TypeDeclaration subschemaType in subschemaTypes)
                {
                    if (generator.IsCancellationRequested)
                    {
                        return generator;
                    }

                    ReducedTypeDeclaration reducedType = subschemaType.ReducedTypeDeclaration();
                    string evaluationPathProperty = generator.GetPropertyNameInScope($"{keyword.Keyword}{i}SchemaEvaluationPath");
                    string evaluationPath = SymbolDisplay.FormatLiteral(keyword.GetPathModifier(reducedType, i), true);
                    generator
                        .AppendLineIndent(
                            "private static readonly JsonSchemaPathProvider ",
                            evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                            evaluationPath,
                            "u8, buffer, out written);");
                    i++;
                }
            }
        }

        foreach (INotValidationKeyword keyword in typeDeclaration.Keywords().OfType<INotValidationKeyword>())
        {
            if (generator.IsCancellationRequested)
            {
                return generator;
            }

            if (!keyword.TryGetNotType(typeDeclaration, out ReducedTypeDeclaration? reducedType) || reducedType is not ReducedTypeDeclaration rdt)
            {
                continue;
            }

            string evaluationPathProperty = generator.GetPropertyNameInScope($"{keyword.Keyword}SchemaEvaluationPath");
            string evaluationPath = SymbolDisplay.FormatLiteral(keyword.GetPathModifier(rdt), true);
            generator
                .AppendLineIndent(
                    "private static readonly JsonSchemaPathProvider ",
                    evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                    evaluationPath,
                    "u8, buffer, out written);");
        }

        if (typeDeclaration.IfSubschemaType() is SingleSubschemaKeywordTypeDeclaration ifType)
        {
            string evaluationPathProperty = generator.GetPropertyNameInScope($"{ifType.Keyword.Keyword}SchemaEvaluationPath");
            string evaluationPath = SymbolDisplay.FormatLiteral(ifType.KeywordPathModifier, true);
            generator
                .AppendLineIndent(
                    "private static readonly JsonSchemaPathProvider ",
                    evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                    evaluationPath,
                    "u8, buffer, out written);");
        }

        if (typeDeclaration.ThenSubschemaType() is SingleSubschemaKeywordTypeDeclaration thenType)
        {
            string evaluationPathProperty = generator.GetPropertyNameInScope($"{thenType.Keyword.Keyword}SchemaEvaluationPath");
            string evaluationPath = SymbolDisplay.FormatLiteral(thenType.KeywordPathModifier, true);
            generator
                .AppendLineIndent(
                    "private static readonly JsonSchemaPathProvider ",
                    evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                    evaluationPath,
                    "u8, buffer, out written);");
        }

        if (typeDeclaration.ElseSubschemaType() is SingleSubschemaKeywordTypeDeclaration elseType)
        {
            string evaluationPathProperty = generator.GetPropertyNameInScope($"{elseType.Keyword.Keyword}SchemaEvaluationPath");
            string evaluationPath = SymbolDisplay.FormatLiteral(elseType.KeywordPathModifier, true);
            generator
                .AppendLineIndent(
                    "private static readonly JsonSchemaPathProvider ",
                    evaluationPathProperty, " = static (buffer, out written) => JsonSchemaEvaluation.TryCopyPath(",
                    evaluationPath,
                    "u8, buffer, out written);");
        }

        return generator;
    }

