        private string EmitExpressionRef(JMESPathNode arg)
        {
            if (arg is not ExpressionRefNode exprRef)
            {
                throw new JMESPathException("invalid-type: Expected an expression reference (&expr).");
            }

            return EmitAsStaticMethod(exprRef.Expression);
        }

        /// <summary>
        /// Emits the right-side expression of a projection as a separate static helper
        /// method. This prevents deeply nested lambdas that overflow the C# compiler stack
        /// when projections are chained (e.g., <c>a[*].b[*].c[*]...</c>). The AST nests
        /// projections inside SubExpressionNodes, so checking the immediate node type is
        /// insufficient — we unconditionally extract to guarantee flat method structure.
        /// </summary>
        private string EmitProjectionRight(StringBuilder body, JMESPathNode right, string indent, string itemVar)
        {
            if (right is CurrentNode)
            {
                return itemVar;
            }

            string helperName = EmitAsStaticMethod(right);
            string resultVar = NextVar();
            L(body, indent, $"JsonElement {resultVar} = {helperName}({itemVar}, workspace);");
            return resultVar;
        }

        private string EmitAsStaticMethod(JMESPathNode expression)
        {
            int refIdx = exprRefCounter++;
            string methodName = $"ExprRef{refIdx}";

            // Build the expression-ref method body
            Emitter innerEmitter = new()
            {
                varCounter = 0,
                nameCounter = this.nameCounter,
                literalCounter = this.literalCounter,
                exprRefCounter = this.exprRefCounter,
            };

            StringBuilder innerBody = new(512);
            string innerResult = innerEmitter.EmitExpression(innerBody, expression, "        ", "data");

            // Merge inner emitter's fields into ours
            StaticFields.AddRange(innerEmitter.StaticFields);
            NameFields.AddRange(innerEmitter.NameFields);
            HelperMethods.AddRange(innerEmitter.HelperMethods);

