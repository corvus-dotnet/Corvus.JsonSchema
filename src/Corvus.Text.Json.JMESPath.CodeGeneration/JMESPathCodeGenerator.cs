using System.Text;
using Corvus.Text.Json.JMESPath;

namespace Corvus.Text.Json.JMESPath.CodeGeneration;

/// <summary>
/// Generates static C# evaluation code from a JMESPath expression string.
/// </summary>
/// <remarks>
/// <para>
/// The generator walks the JMESPath AST and emits inline C# code that calls
/// <c>JMESPathCodeGenHelpers</c> methods. All JMESPath constructs are supported
/// directly — there is no runtime fallback.
/// </para>
/// </remarks>
public static class JMESPathCodeGenerator
{
    private const string H = "JMESPathCodeGenHelpers";

    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JMESPath expression.
    /// </summary>
    /// <param name="expression">The JMESPath expression string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <param name="accessibility">The accessibility modifier (e.g. <c>"internal"</c> or <c>"public"</c>).</param>
    /// <param name="isPartial">Whether to emit the <c>partial</c> modifier on the class.</param>
    /// <returns>A complete C# source file as a string.</returns>
    /// <exception cref="JMESPathException">
    /// Thrown if <paramref name="expression"/> is not a valid JMESPath expression.
    /// </exception>
    public static string Generate(
        string expression,
        string className,
        string namespaceName,
        string accessibility = "internal",
        bool isPartial = false)
    {
        byte[] utf8 = Encoding.UTF8.GetBytes(expression);
        JMESPathNode ast = Parser.Parse(utf8);

        Emitter emitter = new();
        StringBuilder body = new(4096);

        string resultVar = emitter.EmitExpression(body, ast, "        ", "data");

        StringBuilder sb = new(8192);
        L(sb, "", "// <auto-generated/>");
        L(sb, "", "#nullable enable");
        L(sb, "", "using System;");
        L(sb, "", "using Corvus.Text.Json;");
        L(sb, "", "using Corvus.Text.Json.JMESPath;");
        Blank(sb);
        L(sb, "", $"namespace {namespaceName};");
        Blank(sb);

        string partialKeyword = isPartial ? " partial" : string.Empty;
        L(sb, "", $"{accessibility} static{partialKeyword} class {className}");
        L(sb, "", "{");

        // Emit static literal fields inside the class
        if (emitter.StaticFields.Count > 0)
        {
            L(sb, "", $"#pragma warning disable SA1306");
            L(sb, "", $"#pragma warning disable SA1311");
            foreach ((string fieldName, string literal) in emitter.StaticFields)
            {
                L(sb, "    ", literal);
            }

            L(sb, "", $"#pragma warning restore SA1311");
            L(sb, "", $"#pragma warning restore SA1306");
            Blank(sb);
        }

        // Emit static name fields inside the class
        foreach ((string fieldName, string declaration) in emitter.NameFields)
        {
            L(sb, "    ", declaration);
        }

        if (emitter.NameFields.Count > 0)
        {
            Blank(sb);
        }

        // Emit Evaluate method
        L(sb, "    ", "public static JsonElement Evaluate(in JsonElement data, JsonWorkspace workspace)");
        L(sb, "    ", "{");
        sb.Append(body);
        L(sb, "        ", $"return {resultVar};");
        L(sb, "    ", "}");

        // Emit expression-ref helper methods
        foreach (string helperMethod in emitter.HelperMethods)
        {
            Blank(sb);
            sb.Append(helperMethod);
        }

        // Close class — no trailing newline (SA1518)
        sb.Append('}');

        return sb.ToString();
    }

    private static void L(StringBuilder sb, string indent, string line)
    {
        sb.Append(indent).Append(line).Append('\n');
    }

    private static void Blank(StringBuilder sb)
    {
        sb.Append('\n');
    }

    private static string Utf8Literal(byte[] bytes)
    {
        StringBuilder sb = new(bytes.Length * 4 + 4);
        sb.Append("new byte[] { ");
        for (int i = 0; i < bytes.Length; i++)
        {
            if (i > 0)
            {
                sb.Append(", ");
            }

            sb.Append("0x");
            sb.Append(bytes[i].ToString("X2"));
        }

        sb.Append(" }");
        return sb.ToString();
    }

    private static string EscapeStringLiteral(string value)
    {
        StringBuilder sb = new(value.Length + 4);
        foreach (char ch in value)
        {
            switch (ch)
            {
                case '"': sb.Append("\\\""); break;
                case '\\': sb.Append("\\\\"); break;
                case '\n': sb.Append("\\n"); break;
                case '\r': sb.Append("\\r"); break;
                case '\t': sb.Append("\\t"); break;
                case '\0': sb.Append("\\0"); break;
                default:
                    if (ch < 0x20)
                    {
                        sb.Append($"\\u{(int)ch:X4}");
                    }
                    else
                    {
                        sb.Append(ch);
                    }

                    break;
            }
        }

        return sb.ToString();
    }

    /// <summary>
    /// Walks the JMESPath AST and emits C# code into a StringBuilder.
    /// Each call to <see cref="EmitExpression"/> appends code to the body
    /// and returns the name of the variable holding the result.
    /// </summary>
    private sealed class Emitter
    {
        private int varCounter;
        private int nameCounter;
        private int literalCounter;
        private int exprRefCounter;

        public List<(string FieldName, string Declaration)> StaticFields { get; } = [];

        public List<(string FieldName, string Declaration)> NameFields { get; } = [];

        public List<string> HelperMethods { get; } = [];

        public string EmitExpression(StringBuilder body, JMESPathNode node, string indent, string inputVar)
        {
            return node switch
            {
                CurrentNode => inputVar,
                IdentifierNode id => EmitIdentifier(body, id, indent, inputVar),
                RawStringNode raw => EmitRawString(body, raw, indent),
                LiteralNode lit => EmitLiteral(body, lit, indent),
                SubExpressionNode sub => EmitSubExpression(body, sub, indent, inputVar),
                IndexNode idx => EmitIndex(body, idx, indent, inputVar),
                SliceNode sl => EmitSlice(body, sl, indent, inputVar),
                PipeNode pipe => EmitPipe(body, pipe, indent, inputVar),
                OrNode or => EmitOr(body, or, indent, inputVar),
                AndNode and => EmitAnd(body, and, indent, inputVar),
                NotNode not => EmitNot(body, not, indent, inputVar),
                ComparisonNode cmp => EmitComparison(body, cmp, indent, inputVar),
                MultiSelectListNode msl => EmitMultiSelectList(body, msl, indent, inputVar),
                MultiSelectHashNode msh => EmitMultiSelectHash(body, msh, indent, inputVar),
                ListProjectionNode lp => EmitListProjection(body, lp, indent, inputVar),
                ValueProjectionNode vp => EmitValueProjection(body, vp, indent, inputVar),
                FlattenProjectionNode fp => EmitFlattenProjection(body, fp, indent, inputVar),
                FilterProjectionNode filt => EmitFilterProjection(body, filt, indent, inputVar),
                FunctionCallNode fn => EmitFunctionCall(body, fn, indent, inputVar),
                ExpressionRefNode => throw new JMESPathException("Expression references (&expr) cannot appear at the top level."),
                _ => throw new JMESPathException($"Unsupported AST node type: {node.GetType().Name}."),
            };
        }

        private string NextVar() => $"v{varCounter++}";

        private string EmitIdentifier(StringBuilder body, IdentifierNode node, string indent, string inputVar)
        {
            string resultVar = NextVar();
            string nameField = $"s_name{nameCounter++}";

            NameFields.Add((nameField, $"private static readonly byte[] {nameField} = {Utf8Literal(node.Name)};"));

            L(body, indent, $"JsonElement {resultVar} = default;");
            L(body, indent, $"if ({inputVar}.ValueKind == JsonValueKind.Object)");
            L(body, indent, "{");
            L(body, indent, $"    {inputVar}.TryGetProperty({nameField}, out {resultVar});");
            L(body, indent, "}");
            Blank(body);

            return resultVar;
        }

        private string EmitRawString(StringBuilder body, RawStringNode node, string indent)
        {
            string resultVar = NextVar();
            string fieldName = $"s_literal{literalCounter++}";
            string escaped = EscapeStringLiteral(Encoding.UTF8.GetString(node.Value));
            string jsonEscaped = EscapeStringLiteral($"\"{Encoding.UTF8.GetString(node.Value)}\"");

            // Use the helper to create the element statically
            StaticFields.Add((fieldName, $"private static readonly JsonElement {fieldName} = {H}.StringToElement(\"{escaped}\");"));

            L(body, indent, $"JsonElement {resultVar} = {fieldName};");
            return resultVar;
        }

        private string EmitLiteral(StringBuilder body, LiteralNode node, string indent)
        {
            string resultVar = NextVar();
            string fieldName = $"s_literal{literalCounter++}";
            StaticFields.Add((fieldName, $"private static readonly JsonElement {fieldName} = JsonElement.ParseValue({Utf8Literal(node.JsonValue)});"));

            L(body, indent, $"JsonElement {resultVar} = {fieldName};");
            return resultVar;
        }

        private string EmitSubExpression(StringBuilder body, SubExpressionNode node, string indent, string inputVar)
        {
            string leftVar = EmitExpression(body, node.Left, indent, inputVar);
            string resultVar = NextVar();
            L(body, indent, $"JsonElement {resultVar} = default;");
            L(body, indent, $"if (!{leftVar}.IsNullOrUndefined())");
            L(body, indent, "{");
            string rightVar = EmitExpression(body, node.Right, indent + "    ", leftVar);
            L(body, indent, $"    {resultVar} = {rightVar};");
            L(body, indent, "}");
            Blank(body);
            return resultVar;
        }

        private string EmitIndex(StringBuilder body, IndexNode node, string indent, string inputVar)
        {
            string resultVar = NextVar();
            int index = node.Index;
            L(body, indent, $"JsonElement {resultVar} = default;");
            L(body, indent, $"if ({inputVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");
            L(body, indent, $"    int len_{resultVar} = {inputVar}.GetArrayLength();");
            if (index < 0)
            {
                L(body, indent, $"    int idx_{resultVar} = len_{resultVar} + ({index});");
            }
            else
            {
                L(body, indent, $"    int idx_{resultVar} = {index};");
            }

            L(body, indent, $"    if (idx_{resultVar} >= 0 && idx_{resultVar} < len_{resultVar})");
            L(body, indent, "    {");
            L(body, indent, $"        {resultVar} = {inputVar}[idx_{resultVar}];");
            L(body, indent, "    }");
            L(body, indent, "}");
            Blank(body);
            return resultVar;
        }

        private string EmitSlice(StringBuilder body, SliceNode node, string indent, string inputVar)
        {
            string resultVar = NextVar();
            string startVal = node.Start.HasValue ? node.Start.Value.ToString() : "null";
            string stopVal = node.Stop.HasValue ? node.Stop.Value.ToString() : "null";
            string stepVal = node.Step.HasValue ? node.Step.Value.ToString() : "1";

            L(body, indent, $"JsonElement {resultVar} = default;");
            L(body, indent, $"if ({inputVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");
            L(body, indent, $"    int len_{resultVar} = {inputVar}.GetArrayLength();");
            L(body, indent, $"    int step_{resultVar} = {stepVal};");

            if (node.Step.HasValue && node.Step.Value == 0)
            {
                L(body, indent, $"    throw new JMESPathException(\"Slice step cannot be 0.\");");
            }
            else
            {
                // Positive step defaults
                L(body, indent, $"    int start_{resultVar};");
                L(body, indent, $"    int stop_{resultVar};");
                L(body, indent, $"    if (step_{resultVar} > 0)");
                L(body, indent, "    {");
                if (node.Start.HasValue)
                {
                    L(body, indent, $"        start_{resultVar} = {H}.NormalizeSliceIndex({node.Start.Value}, len_{resultVar}, true, true);");
                }
                else
                {
                    L(body, indent, $"        start_{resultVar} = 0;");
                }

                if (node.Stop.HasValue)
                {
                    L(body, indent, $"        stop_{resultVar} = {H}.NormalizeSliceIndex({node.Stop.Value}, len_{resultVar}, false, true);");
                }
                else
                {
                    L(body, indent, $"        stop_{resultVar} = len_{resultVar};");
                }

                L(body, indent, "    }");
                L(body, indent, "    else");
                L(body, indent, "    {");
                if (node.Start.HasValue)
                {
                    L(body, indent, $"        start_{resultVar} = {H}.NormalizeSliceIndex({node.Start.Value}, len_{resultVar}, true, false);");
                }
                else
                {
                    L(body, indent, $"        start_{resultVar} = len_{resultVar} - 1;");
                }

                if (node.Stop.HasValue)
                {
                    L(body, indent, $"        stop_{resultVar} = {H}.NormalizeSliceIndex({node.Stop.Value}, len_{resultVar}, false, false);");
                }
                else
                {
                    L(body, indent, $"        stop_{resultVar} = -1;");
                }

                L(body, indent, "    }");

                L(body, indent, $"    var doc_{resultVar} = JsonElement.CreateBuilder(workspace, ({inputVar}, start_{resultVar}, stop_{resultVar}, step_{resultVar}), static (in (JsonElement __src, int __start, int __stop, int __step) __ctx, ref JsonElement.ArrayBuilder __b) =>");
                L(body, indent, "    {");
                L(body, indent, $"        if (__ctx.__step > 0)");
                L(body, indent, "        {");
                L(body, indent, $"            for (int __i = __ctx.__start; __i < __ctx.__stop; __i += __ctx.__step)");
                L(body, indent, "            {");
                L(body, indent, $"                __b.AddItem(__ctx.__src[__i]);");
                L(body, indent, "            }");
                L(body, indent, "        }");
                L(body, indent, "        else");
                L(body, indent, "        {");
                L(body, indent, $"            for (int __i = __ctx.__start; __i > __ctx.__stop; __i += __ctx.__step)");
                L(body, indent, "            {");
                L(body, indent, $"                __b.AddItem(__ctx.__src[__i]);");
                L(body, indent, "            }");
                L(body, indent, "        }");
                L(body, indent, $"    }}, len_{resultVar} + 2);");

                L(body, indent, $"    {resultVar} = (JsonElement)doc_{resultVar}.RootElement;");
            }

            L(body, indent, "}");
            Blank(body);
            return resultVar;
        }

        private string EmitPipe(StringBuilder body, PipeNode node, string indent, string inputVar)
        {
            // Try general n-stage pipe fusion.
            if (FusedPipePlanner.TryBuildPlan(node, out JMESPathNode source, out PipeStage[] stages))
            {
                return EmitFusedPipeline(body, source, stages, indent, inputVar);
            }

            string leftVar = EmitExpression(body, node.Left, indent, inputVar);
            return EmitExpression(body, node.Right, indent, leftVar);
        }

        private string EmitFusedPipeline(
            StringBuilder body,
            JMESPathNode sourceNode,
            PipeStage[] stages,
            string indent,
            string inputVar)
        {
            // Emit source evaluation.
            string srcVar = EmitExpression(body, sourceNode, indent, inputVar);

            string resultVar = NextVar();
            L(body, indent, $"JsonElement {resultVar} = default;");
            L(body, indent, $"if ({srcVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");

            string inner = indent + "    ";
            string builderVar = $"__b{varCounter++}";
            L(body, inner, $"JMESPathSequenceBuilder {builderVar} = default;");
            L(body, inner, "try");
            L(body, inner, "{");

            string tryInner = inner + "    ";

            // Walk through phases: alternating streaming groups and barriers.
            int stageIdx = 0;

            // Phase 1: Collect from source enumerator.
            EmitCollectFromSource(body, stages, ref stageIdx, tryInner, srcVar, builderVar);
            Blank(body);

            // Check if we have a terminal hash project (last streaming stage).
            int lastStreamingIdx = -1;
            for (int i = stages.Length - 1; i >= 0; i--)
            {
                if (!stages[i].IsBarrier)
                {
                    lastStreamingIdx = i;
                    break;
                }
            }

            bool hasTerminalHash = lastStreamingIdx >= 0 && stages[lastStreamingIdx] is PipeStage.HashProject;

            // Process remaining barriers and streaming phases.
            while (stageIdx < stages.Length)
            {
                if (stages[stageIdx].IsBarrier)
                {
                    EmitBarrierInline(body, stages[stageIdx], tryInner, builderVar);
                    stageIdx++;
                    Blank(body);
                }
                else
                {
                    // Streaming stages after a barrier — check if terminal hash project.
                    int firstStreaming = stageIdx;
                    while (stageIdx < stages.Length && !stages[stageIdx].IsBarrier)
                    {
                        stageIdx++;
                    }

                    int endStreaming = stageIdx;

                    if (hasTerminalHash && firstStreaming == lastStreamingIdx && firstStreaming == endStreaming - 1)
                    {
                        // Terminal hash project — materialize directly.
                        EmitTerminalHashProject(body, (PipeStage.HashProject)stages[firstStreaming], tryInner, builderVar, resultVar);
                        goto doneBody;
                    }

                    // Collect from builder to new builder.
                    EmitCollectFromBuilder(body, stages, firstStreaming, endStreaming, tryInner, builderVar);
                    Blank(body);
                }
            }

            // Final materialization.
            string countVar = NextVar();
            L(body, tryInner, $"int {countVar} = {builderVar}.Count;");
            L(body, tryInner, $"if ({countVar} == 0)");
            L(body, tryInner, "{");
            L(body, tryInner, $"    {resultVar} = {H}.EmptyArrayElement;");
            L(body, tryInner, "}");
            L(body, tryInner, "else");
            L(body, tryInner, "{");
            L(body, tryInner, $"    {resultVar} = {builderVar}.ToElement(workspace);");
            L(body, tryInner, "}");

            doneBody:
            L(body, inner, "}");
            L(body, inner, "finally");
            L(body, inner, "{");
            L(body, inner, $"    {builderVar}.ReturnArray();");
            L(body, inner, "}");

            L(body, indent, "}");
            Blank(body);
            return resultVar;
        }

        /// <summary>
        /// Emits the initial collection phase from the source array enumerator.
        /// </summary>
        private void EmitCollectFromSource(
            StringBuilder body,
            PipeStage[] stages,
            ref int stageIdx,
            string indent,
            string srcVar,
            string builderVar)
        {
            int firstStreaming = stageIdx;
            while (stageIdx < stages.Length && !stages[stageIdx].IsBarrier)
            {
                stageIdx++;
            }

            int endStreaming = stageIdx;

            string itemVar = NextVar();
            L(body, indent, $"foreach (JsonElement {itemVar} in {srcVar}.EnumerateArray())");
            L(body, indent, "{");

            if (firstStreaming < endStreaming)
            {
                EmitStreamingStages(body, stages, firstStreaming, endStreaming, indent + "    ", itemVar, builderVar);
            }
            else
            {
                L(body, indent, $"    {builderVar}.Add({itemVar});");
            }

            L(body, indent, "}");
        }

        /// <summary>
        /// Emits code to iterate the current builder through streaming stages into a new builder,
        /// then swap.
        /// </summary>
        private void EmitCollectFromBuilder(
            StringBuilder body,
            PipeStage[] stages,
            int firstStreaming,
            int endStreaming,
            string indent,
            string builderVar)
        {
            string countVar = NextVar();
            string newBuilderVar = $"__b{varCounter++}";
            string idxVar = NextVar();

            L(body, indent, $"int {countVar} = {builderVar}.Count;");
            L(body, indent, $"JMESPathSequenceBuilder {newBuilderVar} = default;");
            L(body, indent, $"for (int {idxVar} = 0; {idxVar} < {countVar}; {idxVar}++)");
            L(body, indent, "{");

            string itemVar = NextVar();
            string loopIndent = indent + "    ";
            L(body, loopIndent, $"JsonElement {itemVar} = {builderVar}[{idxVar}];");

            EmitStreamingStages(body, stages, firstStreaming, endStreaming, loopIndent, itemVar, newBuilderVar);

            L(body, indent, "}");

            // Swap builders.
            L(body, indent, $"{builderVar}.ReturnArray();");
            L(body, indent, $"{builderVar} = {newBuilderVar};");
        }

        /// <summary>
        /// Recursively emits streaming stages for a single element, with fan-out for Flatten.
        /// </summary>
        private void EmitStreamingStages(
            StringBuilder body,
            PipeStage[] stages,
            int idx,
            int end,
            string indent,
            string elementVar,
            string builderVar)
        {
            if (idx >= end)
            {
                L(body, indent, $"{builderVar}.Add({elementVar});");
                return;
            }

            PipeStage stage = stages[idx];

            switch (stage)
            {
                case PipeStage.Filter filter:
                {
                    string condVar = EmitExpression(body, filter.Condition, indent, elementVar);
                    L(body, indent, $"if ({H}.IsTruthy({condVar}))");
                    L(body, indent, "{");

                    string innerIndent = indent + "    ";
                    if (filter.Projection is not null)
                    {
                        string projVar = EmitExpression(body, filter.Projection, innerIndent, elementVar);
                        L(body, innerIndent, $"if (!{projVar}.IsNullOrUndefined())");
                        L(body, innerIndent, "{");
                        EmitStreamingStages(body, stages, idx + 1, end, innerIndent + "    ", projVar, builderVar);
                        L(body, innerIndent, "}");
                    }
                    else
                    {
                        EmitStreamingStages(body, stages, idx + 1, end, innerIndent, elementVar, builderVar);
                    }

                    L(body, indent, "}");
                    break;
                }

                case PipeStage.Project project:
                {
                    string projVar = EmitExpression(body, project.Expression, indent, elementVar);
                    L(body, indent, $"if (!{projVar}.IsNullOrUndefined())");
                    L(body, indent, "{");
                    EmitStreamingStages(body, stages, idx + 1, end, indent + "    ", projVar, builderVar);
                    L(body, indent, "}");
                    break;
                }

                case PipeStage.Flatten flatten:
                {
                    if (flatten.Projection is not null)
                    {
                        string flatProjVar = EmitExpression(body, flatten.Projection, indent, elementVar);
                        L(body, indent, $"if (!{flatProjVar}.IsNullOrUndefined())");
                        L(body, indent, "{");

                        string fi = indent + "    ";
                        L(body, fi, $"if ({flatProjVar}.ValueKind == JsonValueKind.Array)");
                        L(body, fi, "{");

                        string innerVar = NextVar();
                        string fi2 = fi + "    ";
                        L(body, fi2, $"foreach (JsonElement {innerVar} in {flatProjVar}.EnumerateArray())");
                        L(body, fi2, "{");
                        EmitStreamingStages(body, stages, idx + 1, end, fi2 + "    ", innerVar, builderVar);
                        L(body, fi2, "}");

                        L(body, fi, "}");
                        L(body, fi, "else");
                        L(body, fi, "{");
                        EmitStreamingStages(body, stages, idx + 1, end, fi + "    ", flatProjVar, builderVar);
                        L(body, fi, "}");

                        L(body, indent, "}");
                    }
                    else
                    {
                        L(body, indent, $"if ({elementVar}.ValueKind == JsonValueKind.Array)");
                        L(body, indent, "{");

                        string innerVar = NextVar();
                        string fi = indent + "    ";
                        L(body, fi, $"foreach (JsonElement {innerVar} in {elementVar}.EnumerateArray())");
                        L(body, fi, "{");
                        EmitStreamingStages(body, stages, idx + 1, end, fi + "    ", innerVar, builderVar);
                        L(body, fi, "}");

                        L(body, indent, "}");
                        L(body, indent, "else");
                        L(body, indent, "{");
                        EmitStreamingStages(body, stages, idx + 1, end, indent + "    ", elementVar, builderVar);
                        L(body, indent, "}");
                    }

                    break;
                }

                case PipeStage.MapExpr mapExpr:
                {
                    string mapResultVar = EmitExpression(body, mapExpr.Expression, indent, elementVar);
                    string mappedVar = NextVar();
                    L(body, indent, $"JsonElement {mappedVar} = {mapResultVar}.IsNullOrUndefined() ? {H}.NullElement : {mapResultVar};");
                    EmitStreamingStages(body, stages, idx + 1, end, indent, mappedVar, builderVar);
                    break;
                }

                case PipeStage.HashProject hashProject:
                {
                    // Non-terminal hash project — build inline object.
                    string objVar = EmitInlineHashObject(body, hashProject.Hash, indent, elementVar);
                    EmitStreamingStages(body, stages, idx + 1, end, indent, objVar, builderVar);
                    break;
                }
            }
        }

        /// <summary>
        /// Emits inline code for a barrier stage.
        /// </summary>
        private void EmitBarrierInline(
            StringBuilder body,
            PipeStage stage,
            string indent,
            string builderVar)
        {
            switch (stage)
            {
                case PipeStage.Sort:
                    L(body, indent, $"{H}.ApplySortBarrier(ref {builderVar});");
                    break;

                case PipeStage.SortBy sortBy:
                {
                    string keyMethod = EmitAsStaticMethod(sortBy.KeyExpression);
                    L(body, indent, $"{H}.ApplySortByBarrier(ref {builderVar}, {keyMethod}, workspace);");
                    break;
                }

                case PipeStage.Reverse:
                    L(body, indent, $"{H}.ApplyReverseBarrier(ref {builderVar});");
                    break;

                case PipeStage.Slice slice:
                {
                    string startArg = slice.Start.HasValue ? slice.Start.Value.ToString() : "null";
                    string stopArg = slice.Stop.HasValue ? slice.Stop.Value.ToString() : "null";
                    L(body, indent, $"{H}.ApplySliceBarrier(ref {builderVar}, {startArg}, {stopArg}, {slice.Step});");
                    break;
                }
            }
        }

        /// <summary>
        /// Emits a terminal hash project: copies builder to rented array, then uses CreateBuilder
        /// with inline object construction.
        /// </summary>
        private void EmitTerminalHashProject(
            StringBuilder body,
            PipeStage.HashProject hashProject,
            string indent,
            string builderVar,
            string resultVar)
        {
            MultiSelectHashNode hashNode = hashProject.Hash;
            int n = hashNode.Pairs.Length;

            // Register static key fields.
            string[] keyFields = new string[n];
            for (int i = 0; i < n; i++)
            {
                keyFields[i] = $"s_name{nameCounter++}";
                NameFields.Add((keyFields[i], $"private static readonly byte[] {keyFields[i]} = {Utf8Literal(hashNode.Pairs[i].Key)};"));
            }

            string countVar = NextVar();
            L(body, indent, $"int {countVar} = {builderVar}.Count;");
            L(body, indent, $"if ({countVar} == 0)");
            L(body, indent, "{");
            L(body, indent, $"    {resultVar} = {H}.EmptyArrayElement;");
            L(body, indent, "}");
            L(body, indent, "else");
            L(body, indent, "{");

            string elseIndent = indent + "    ";
            string itemsVar = $"__items{varCounter++}";
            string idxVar = NextVar();

            // Copy builder to rented array (builder is ref struct, can't capture in tuple).
            L(body, elseIndent, $"JsonElement[] {itemsVar} = System.Buffers.ArrayPool<JsonElement>.Shared.Rent({countVar});");
            L(body, elseIndent, $"for (int {idxVar} = 0; {idxVar} < {countVar}; {idxVar}++)");
            L(body, elseIndent, "{");
            L(body, elseIndent, $"    {itemsVar}[{idxVar}] = {builderVar}[{idxVar}];");
            L(body, elseIndent, "}");
            Blank(body);

            L(body, elseIndent, "try");
            L(body, elseIndent, "{");

            string tryIndent = elseIndent + "    ";
            string docVar = $"__doc{varCounter++}";
            string itemVar = NextVar();

            // CreateBuilder with ArrayBuilder callback.
            L(body, tryIndent, $"var {docVar} = JsonElement.CreateBuilder(workspace, ({itemsVar}, {countVar}, workspace), static (in (JsonElement[] __items, int __count, JsonWorkspace workspace) __ctx, ref JsonElement.ArrayBuilder __ab) =>");
            L(body, tryIndent, "{");

            string cbIndent = tryIndent + "    ";
            L(body, cbIndent, "JsonWorkspace workspace = __ctx.workspace;");
            string loopIdxVar = NextVar();
            L(body, cbIndent, $"for (int {loopIdxVar} = 0; {loopIdxVar} < __ctx.__count; {loopIdxVar}++)");
            L(body, cbIndent, "{");

            string itemBodyIndent = cbIndent + "    ";
            L(body, itemBodyIndent, $"JsonElement {itemVar} = __ctx.__items[{loopIdxVar}];");
            L(body, itemBodyIndent, $"if (!{itemVar}.IsNullOrUndefined())");
            L(body, itemBodyIndent, "{");

            string objBodyIndent = itemBodyIndent + "    ";

            // Emit value expressions inline.
            string[] valVars = new string[n];
            for (int i = 0; i < n; i++)
            {
                valVars[i] = EmitExpression(body, hashNode.Pairs[i].Value, objBodyIndent, itemVar);
            }

            // Coerce null/undefined values.
            string[] coercedVars = new string[n];
            for (int i = 0; i < n; i++)
            {
                coercedVars[i] = NextVar();
                L(body, objBodyIndent, $"JsonElement {coercedVars[i]} = {valVars[i]}.IsNullOrUndefined() ? {H}.NullElement : {valVars[i]};");
            }

            // Build context tuple for ObjectBuilder callback.
            string ctxExpr = n == 1
                ? $"ValueTuple.Create({coercedVars[0]})"
                : $"({string.Join(", ", coercedVars)})";
            string ctxType = n == 1
                ? "ValueTuple<JsonElement>"
                : $"({string.Join(", ", Enumerable.Range(0, n).Select(_ => "JsonElement"))})";

            L(body, objBodyIndent, $"__ab.AddItem({ctxExpr}, static (in {ctxType} __ic, ref JsonElement.ObjectBuilder __ob) =>");
            L(body, objBodyIndent, "{");
            for (int i = 0; i < n; i++)
            {
                string itemRef = n == 1 ? "__ic.Item1" : $"__ic.Item{i + 1}";
                L(body, objBodyIndent, $"    __ob.AddProperty({keyFields[i]}, {itemRef});");
            }

            L(body, objBodyIndent, "});");
            L(body, itemBodyIndent, "}");
            L(body, cbIndent, "}");
            L(body, tryIndent, $"}}, {countVar} + 2);");
            L(body, tryIndent, $"{resultVar} = (JsonElement){docVar}.RootElement;");

            L(body, elseIndent, "}");
            L(body, elseIndent, "finally");
            L(body, elseIndent, "{");
            L(body, elseIndent, $"    {itemsVar}.AsSpan(0, {countVar}).Clear();");
            L(body, elseIndent, $"    System.Buffers.ArrayPool<JsonElement>.Shared.Return({itemsVar});");
            L(body, elseIndent, "}");

            L(body, indent, "}");
        }

