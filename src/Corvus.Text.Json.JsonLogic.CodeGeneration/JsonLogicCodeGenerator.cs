        private string EmitIf(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            // Flatten nested if-else chains: collect (condition, consequent) pairs + optional else
            List<(STJ.JsonElement cond, STJ.JsonElement then)> pairs = new();
            STJ.JsonElement? elseExpr = null;
            FlattenIfChain(operands, pairs, ref elseExpr);

            // Try uniform comparison optimization
            if (pairs.Count >= 3 && TryEmitUniformComparisonChain(sb, pairs, elseExpr, indent, dataVar, out string? ucResult))
            {
                return ucResult!;
            }

            // Edge case: 0 pairs (e.g., {"if":[true]}) — just return the else expression
            if (pairs.Count == 0)
            {
                if (elseExpr is not null)
                {
                    string v = EmitExpression(sb, elseExpr.Value, indent, dataVar);
                    MaterializeIfDeferred(sb, v, indent);
                    return v;
                }

                string nv = NextVar();
                L(sb, indent, $"JsonElement {nv} = JsonLogicHelpers.NullElement();");
                return nv;
            }

            // Standard flat if chain using do-while(false)/break pattern
            // (conditions may emit statements, so we can't use else-if syntax)
            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, "do");
            L(sb, indent, "{");

            // Save var cache state — each branch is a separate scope
            Dictionary<string, string> savedCache = new(_varCache);

            for (int i = 0; i < pairs.Count; i++)
            {
                string condVar = EmitExpression(sb, pairs[i].cond, indent + "    ", dataVar);
                MaterializeIfDeferred(sb, condVar, indent + "    ");
                L(sb, indent + "    ", $"if (JsonLogicHelpers.IsTruthy({condVar}))");
                L(sb, indent + "    ", "{");

                Dictionary<string, string> branchSave = new(_varCache);
                string thenVar = EmitExpression(sb, pairs[i].then, indent + "        ", dataVar);
                MaterializeIfDeferred(sb, thenVar, indent + "        ");
                _varCache.Clear();
                foreach (KeyValuePair<string, string> kvp in branchSave)
                {
                    _varCache[kvp.Key] = kvp.Value;
                }

                L(sb, indent + "        ", $"{result} = {thenVar};");
                L(sb, indent + "        ", "break;");
                L(sb, indent + "    ", "}");
                Blank(sb);
            }

            if (elseExpr is not null)
            {
                string elseVar = EmitExpression(sb, elseExpr.Value, indent + "    ", dataVar);
                MaterializeIfDeferred(sb, elseVar, indent + "    ");
                L(sb, indent + "    ", $"{result} = {elseVar};");
            }
            else
            {
                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
            }

            // Restore original cache
            _varCache.Clear();
            foreach (KeyValuePair<string, string> kvp in savedCache)
            {
                _varCache[kvp.Key] = kvp.Value;
            }

            L(sb, indent, "}");
            L(sb, indent, "while (false);");
            Blank(sb);
            return result;
        }

        /// <summary>
        /// Flattens nested if-else chains into a flat list of (condition, consequent) pairs.
        /// e.g., {"if":[c1, t1, {"if":[c2, t2, {"if":[c3, t3, e]}]}]}
        /// becomes [(c1,t1), (c2,t2), (c3,t3)] with else=e.
        /// </summary>
        private static void FlattenIfChain(
            STJ.JsonElement[] operands,
            List<(STJ.JsonElement cond, STJ.JsonElement then)> pairs,
            ref STJ.JsonElement? elseExpr)
        {
            int i = 0;
            while (i < operands.Length - 1)
            {
                pairs.Add((operands[i], operands[i + 1]));
                i += 2;
            }

            if (i < operands.Length)
            {
                STJ.JsonElement tail = operands[i];

                // If the else branch is itself an {"if": [...]}, flatten recursively
                if (tail.ValueKind == STJ.JsonValueKind.Object && tail.TryGetProperty("if", out STJ.JsonElement nestedArgs)
                    && nestedArgs.ValueKind == STJ.JsonValueKind.Array)
                {
                    STJ.JsonElement[] nestedOps = GetOperands(nestedArgs);
                    FlattenIfChain(nestedOps, pairs, ref elseExpr);
                }
                else
                {
                    elseExpr = tail;
                }
            }
        }

        /// <summary>
        /// Detects the "uniform comparison chain" pattern: all conditions compare
        /// the same variable against different literal thresholds with the same operator.
        /// Emits a flat if-else chain using native double comparisons.
        /// </summary>
        private bool TryEmitUniformComparisonChain(
            StringBuilder sb,
            List<(STJ.JsonElement cond, STJ.JsonElement then)> pairs,
            STJ.JsonElement? elseExpr,
            string indent,
            string dataVar,
            out string? result)
        {
            result = null;

            // Extract the comparison pattern from the first condition
            if (!TryExtractComparison(pairs[0].cond, out string? op0, out string? varPath0, out _))
            {
                return false;
            }

            // All conditions must use the same operator and same variable path
            for (int i = 1; i < pairs.Count; i++)
            {
                if (!TryExtractComparison(pairs[i].cond, out string? op, out string? varPath, out _)
                    || op != op0 || varPath != varPath0)
                {
                    return false;
                }
            }

            // All consequents must be literal numbers
            for (int i = 0; i < pairs.Count; i++)
            {
                if (!IsLiteralNumber(pairs[i].then))
                {
                    return false;
                }
            }

            // Emit optimized chain: extract variable once, compare against thresholds
            string xVar = EmitStaticVarPath(sb, varPath0!, null, indent, dataVar);

            result = NextVar();
            L(sb, indent, $"JsonElement {result};");

            // Try double fast-path for the variable
            string dblVar = result + "_xd";
            L(sb, indent, $"if (!{xVar}.IsNullOrUndefined() && JsonLogicHelpers.TryCoerceToDouble({xVar}, out double {dblVar}))");
            L(sb, indent, "{");

            string cmpOp = op0 switch { ">" => ">", ">=" => ">=", "<" => "<", "<=" => "<=", _ => ">" };

            for (int i = 0; i < pairs.Count; i++)
            {
                TryExtractComparison(pairs[i].cond, out _, out _, out double threshold);
                double thenVal = GetLiteralDouble(pairs[i].then);
                string keyword = i == 0 ? "if" : "else if";
                L(sb, indent + "    ", $"{keyword} ({dblVar} {cmpOp} {FormatDouble(threshold)})");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = JsonLogicHelpers.DoubleToElement({FormatDouble(thenVal)}, workspace);");
                L(sb, indent + "    ", "}");
            }

            if (elseExpr is not null && IsLiteralNumber(elseExpr.Value))
            {
                double elseVal = GetLiteralDouble(elseExpr.Value);
                L(sb, indent + "    ", "else");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = JsonLogicHelpers.DoubleToElement({FormatDouble(elseVal)}, workspace);");
                L(sb, indent + "    ", "}");
            }
            else
            {
                L(sb, indent + "    ", "else");
                L(sb, indent + "    ", "{");
                if (elseExpr is not null)
                {
                    string elseVar = EmitExpression(sb, elseExpr.Value, indent + "        ", dataVar);
                    MaterializeIfDeferred(sb, elseVar, indent + "        ");
                    L(sb, indent + "        ", $"{result} = {elseVar};");
                }
                else
                {
                    L(sb, indent + "        ", $"{result} = JsonLogicHelpers.NullElement();");
                }

                L(sb, indent + "    ", "}");
            }

            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
            L(sb, indent, "}");
            Blank(sb);
            return true;
        }

        /// <summary>
        /// Tries to emit a predicate expression as a native bool variable,
        /// avoiding the BooleanElement/IsTruthy roundtrip.
        /// Handles comparison operators and negation.
        /// </summary>
        private bool TryEmitPredicateAsBool(
            StringBuilder sb,
            STJ.JsonElement expr,
            string indent,
            string dataVar,
            out string? boolVar)
        {
            boolVar = null;
            if (expr.ValueKind != STJ.JsonValueKind.Object)
            {
                return false;
            }

            foreach (STJ.JsonProperty prop in expr.EnumerateObject())
            {
                if (prop.Name is ">" or ">=" or "<" or "<=")
                {
                    STJ.JsonElement[] operands = GetOperands(prop.Value);
                    if (operands.Length < 2)
                    {
                        return false;
                    }

                    string result = NextVar();
                    boolVar = result + "_b";

                    // Build per-operand conditions and double expressions.
                    // For literal numbers, skip EmitExpression entirely to avoid dead code.
                    string? lhsCond = null;
                    string lhsDbl;
                    if (IsLiteralNumber(operands[0]))
                    {
                        lhsDbl = FormatDouble(GetLiteralDouble(operands[0]));
                    }
                    else
                    {
                        string lhsVar = EmitExpression(sb, operands[0], indent, dataVar);
                        if (_deferredDoubleVars.Contains(lhsVar))
                        {
                            lhsCond = $"{lhsVar}_id";
                            lhsDbl = $"{lhsVar}_dv";
                        }
                        else
                        {
                            lhsCond = $"JsonLogicHelpers.TryCoerceToDouble({lhsVar}, out double {result}_ld)";
                            lhsDbl = $"{result}_ld";
                        }
                    }

                    string? rhsCond = null;
                    string rhsDbl;
                    if (IsLiteralNumber(operands[1]))
                    {
                        rhsDbl = FormatDouble(GetLiteralDouble(operands[1]));
                    }
                    else
                    {
                        string rhsVar = EmitExpression(sb, operands[1], indent, dataVar);
                        if (_deferredDoubleVars.Contains(rhsVar))
                        {
                            rhsCond = $"{rhsVar}_id";
                            rhsDbl = $"{rhsVar}_dv";
                        }
                        else
                        {
                            rhsCond = $"JsonLogicHelpers.TryCoerceToDouble({rhsVar}, out double {result}_rd)";
                            rhsDbl = $"{result}_rd";
                        }
                    }

                    string cmpOp = prop.Name switch { ">" => ">", ">=" => ">=", "<" => "<", "<=" => "<=", _ => ">" };

                    // Build the combined condition
                    List<string> allConds = new();
                    if (lhsCond is not null)
                    {
                        allConds.Add(lhsCond);
                    }

                    if (rhsCond is not null)
                    {
                        allConds.Add(rhsCond);
                    }

                    allConds.Add($"{lhsDbl} {cmpOp} {rhsDbl}");

                    L(sb, indent, $"bool {boolVar} = {string.Join(" && ", allConds)};");
                    return true;
                }

                if (prop.Name == "!")
                {
                    STJ.JsonElement[] operands = GetOperands(prop.Value);
                    if (operands.Length >= 1 && TryEmitPredicateAsBool(sb, operands[0], indent, dataVar, out string? innerBool))
                    {
                        boolVar = NextVar() + "_b";
                        L(sb, indent, $"bool {boolVar} = !{innerBool};");
                        return true;
                    }

                    return false;
                }

                break;
            }

            return false;
        }

        /// <summary>
        /// Tries to extract a comparison pattern from a condition.
        /// </summary>
        private static bool TryExtractComparison(STJ.JsonElement cond, out string? op, out string? varPath, out double threshold)
        {
            op = null;
            varPath = null;
            threshold = 0;

            if (cond.ValueKind != STJ.JsonValueKind.Object)
            {
                return false;
            }

            foreach (STJ.JsonProperty prop in cond.EnumerateObject())
            {
                if (prop.Name is not (">" or ">=" or "<" or "<="))
                {
                    return false;
                }

                op = prop.Name;

                if (prop.Value.ValueKind != STJ.JsonValueKind.Array || prop.Value.GetArrayLength() < 2)
                {
                    return false;
                }

                STJ.JsonElement lhs = prop.Value[0];
                STJ.JsonElement rhs = prop.Value[1];

                // LHS must be {"var": "path"}
                if (lhs.ValueKind != STJ.JsonValueKind.Object)
                {
                    return false;
                }

                string? lhsVarPath = null;
                foreach (STJ.JsonProperty lhsProp in lhs.EnumerateObject())
                {
                    if (lhsProp.Name != "var")
                    {
                        return false;
                    }

                    if (lhsProp.Value.ValueKind == STJ.JsonValueKind.String)
                    {
                        lhsVarPath = lhsProp.Value.GetString();
                    }
                    else if (lhsProp.Value.ValueKind == STJ.JsonValueKind.Array && lhsProp.Value.GetArrayLength() > 0 && lhsProp.Value[0].ValueKind == STJ.JsonValueKind.String)
                    {
                        lhsVarPath = lhsProp.Value[0].GetString();
                    }
                    else
                    {
                        return false;
                    }

                    break;
                }

                if (lhsVarPath is null)
                {
                    return false;
                }

                // RHS must be a literal number
                if (rhs.ValueKind != STJ.JsonValueKind.Number || !rhs.TryGetDouble(out threshold))
                {
                    return false;
                }

                varPath = lhsVarPath;
                return true;
            }

            return false;
        }

        // ─── STRING ─────────────────────────────────────────────
        private string EmitCat(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyString();");
                return v;
            }

            string[] opVars = EmitOperands(sb, operands, indent, dataVar);

            // Materialize any deferred doubles
            for (int i = 0; i < opVars.Length; i++)
            {
                MaterializeIfDeferred(sb, opVars[i], indent);
            }

            string result = NextVar();
            string bufVar = result + "_buf";
            string posVar = result + "_pos";

            // Inline buffer approach: rent from ArrayPool, AppendCoercedToBuffer per operand
            L(sb, indent, $"byte[] {bufVar} = System.Buffers.ArrayPool<byte>.Shared.Rent(256);");
            L(sb, indent, $"int {posVar} = 1;"); // Start at 1 for leading quote
            L(sb, indent, $"{bufVar}[0] = (byte)'\"';");
            L(sb, indent, "try");
            L(sb, indent, "{");

            for (int i = 0; i < opVars.Length; i++)
            {
                L(sb, indent + "    ", $"JsonLogicHelpers.AppendCoercedToBuffer({opVars[i]}, ref {bufVar}, ref {posVar});");
            }

            L(sb, indent + "    ", $"JsonLogicHelpers.GrowCatBufferIfNeeded(ref {bufVar}, {posVar}, 1);");
            L(sb, indent + "    ", $"{bufVar}[{posVar}++] = (byte)'\"';");
            L(sb, indent, "}");
            L(sb, indent, "catch");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"System.Buffers.ArrayPool<byte>.Shared.Return({bufVar});");
            L(sb, indent + "    ", "throw;");
            L(sb, indent, "}");
            Blank(sb);
            L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.StringFromQuotedUtf8Span(new ReadOnlySpan<byte>({bufVar}, 0, {posVar}), workspace);");
            L(sb, indent, $"System.Buffers.ArrayPool<byte>.Shared.Return({bufVar});");

            Blank(sb);
            return result;
        }

        private string EmitSubstr(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyString();");
                return v;
            }

            // Emit source operand
            string[] opVars = new string[operands.Length];
            for (int i = 0; i < operands.Length; i++)
            {
                opVars[i] = EmitExpression(sb, operands[i], indent, dataVar);
            }

            // Materialize all deferred doubles (start/length must be elements for CoerceToBigNumber)
            for (int i = 0; i < opVars.Length; i++)
            {
                MaterializeIfDeferred(sb, opVars[i], indent);
            }

            string result = NextVar();

            // Compute start
            string startExpr;
            if (opVars.Length > 1 && IsLiteralNumber(operands[1]))
            {
                startExpr = ((int)GetLiteralDouble(operands[1])).ToString();
            }
            else if (opVars.Length > 1)
            {
                startExpr = $"(int)(long)JsonLogicHelpers.CoerceToBigNumber({opVars[1]})";
            }
            else
            {
                startExpr = "0";
            }

            // Compute length
            string lenExpr;
            if (opVars.Length > 2 && IsLiteralNumber(operands[2]))
            {
                lenExpr = ((int)GetLiteralDouble(operands[2])).ToString();
            }
            else if (opVars.Length > 2)
            {
                lenExpr = $"(int)(long)JsonLogicHelpers.CoerceToBigNumber({opVars[2]})";
            }
            else
            {
                lenExpr = "int.MaxValue";
            }

            L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.SubstrFromElement({opVars[0]}, {startExpr}, {lenExpr}, workspace);");
            Blank(sb);
            return result;
        }

        // ─── ARRAY ──────────────────────────────────────────────
        private string EmitIn(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(false);");
                return v;
            }

            // When the haystack is a constant literal array, we know the type at
            // code-gen time — skip the runtime ValueKind checks and iterate a
            // static JsonElement[] directly (no array document built per call).
            if (IsConstantLiteralArray(operands[1]))
            {
                string needleVar = EmitExpression(sb, operands[0], indent, dataVar);
                MaterializeIfDeferred(sb, needleVar, indent);

                string arrField = GetOrCreateStaticArray(operands[1]);
                string result = NextVar();
                string foundVar = result + "_f";
                L(sb, indent, $"bool {foundVar} = false;");
                string idxVar = NextVar();
                L(sb, indent, $"for (int {idxVar} = 0; {idxVar} < {arrField}.Length; {idxVar}++)");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"if (JsonLogicHelpers.StrictElementEquals({needleVar}, {arrField}[{idxVar}]))");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{foundVar} = true;");
                L(sb, indent + "        ", "break;");
                L(sb, indent + "    ", "}");
                L(sb, indent, "}");
                Blank(sb);
                L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.BooleanElement({foundVar});");
                return result;
            }

            string[] opVars = EmitOperands(sb, operands, indent, dataVar);
            for (int i = 0; i < opVars.Length; i++)
            {
                MaterializeIfDeferred(sb, opVars[i], indent);
            }

            string res = NextVar();
            L(sb, indent, $"JsonElement {res};");
            L(sb, indent, $"if ({opVars[1]}.ValueKind == JsonValueKind.String)");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"if ({opVars[0]}.ValueKind != JsonValueKind.String)");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{res} = JsonLogicHelpers.BooleanElement(false);");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "else");
            L(sb, indent + "    ", "{");
            string hVar = res + "_h";
            string nVar = res + "_n";
            L(sb, indent + "        ", $"string {hVar} = {opVars[1]}.GetString() ?? \"\";");
            L(sb, indent + "        ", $"string {nVar} = {opVars[0]}.GetString() ?? \"\";");
            L(sb, indent + "        ", $"{res} = JsonLogicHelpers.BooleanElement({hVar}.IndexOf({nVar}, System.StringComparison.Ordinal) >= 0);");
            L(sb, indent + "    ", "}");
            L(sb, indent, "}");
            L(sb, indent, $"else if ({opVars[1]}.ValueKind == JsonValueKind.Array)");
            L(sb, indent, "{");
            string fVar = res + "_f";
            L(sb, indent + "    ", $"bool {fVar} = false;");
            string itemVar = res + "_it";
            L(sb, indent + "    ", $"foreach (JsonElement {itemVar} in {opVars[1]}.EnumerateArray())");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"if (JsonLogicHelpers.StrictElementEquals({opVars[0]}, {itemVar}))");
            L(sb, indent + "        ", "{");
            L(sb, indent + "            ", $"{fVar} = true;");
            L(sb, indent + "            ", "break;");
            L(sb, indent + "        ", "}");
            L(sb, indent + "    ", "}");
            Blank(sb);
            L(sb, indent + "    ", $"{res} = JsonLogicHelpers.BooleanElement({fVar});");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{res} = JsonLogicHelpers.BooleanElement(false);");
            L(sb, indent, "}");
            Blank(sb);
            return res;
        }

        private string EmitMerge(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyArray();");
                return v;
            }

            // Check if ALL operands are constant literal arrays — if so, pre-compute
            // the entire merged result at code-gen time as a single static field.
            bool allOperandsConstant = true;
            for (int i = 0; i < operands.Length; i++)
            {
                if (!IsConstantLiteralArray(operands[i]))
                {
                    allOperandsConstant = false;
                    break;
                }
            }

            if (allOperandsConstant)
            {
                // Build the flattened JSON array string at code-gen time
                var jsonBuilder = new StringBuilder();
                jsonBuilder.Append('[');
                bool first = true;
                for (int i = 0; i < operands.Length; i++)
                {
                    foreach (STJ.JsonElement sub in operands[i].EnumerateArray())
                    {
                        if (!first)
                        {
                            jsonBuilder.Append(',');
                        }

                        jsonBuilder.Append(sub.GetRawText());
                        first = false;
                    }
                }

                jsonBuilder.Append(']');

                string field = GetOrCreateStaticLiteral(jsonBuilder.ToString());
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = {field};");
                return v;
            }

            string result = NextVar();
            string bufVar = result + "_buf";
            L(sb, indent, $"var {bufVar} = new ElementBuffer();");
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, "try");
            L(sb, indent, "{");

            for (int i = 0; i < operands.Length; i++)
            {
                if (IsConstantLiteralArray(operands[i]))
                {
                    // Pre-build as a static JsonElement[] for direct array indexing.
                    string field = GetOrCreateStaticArray(operands[i]);
                    string idxVar = NextVar();
                    L(sb, indent + "    ", $"for (int {idxVar} = 0; {idxVar} < {field}.Length; {idxVar}++)");
                    L(sb, indent + "    ", "{");
                    L(sb, indent + "        ", $"{bufVar}.Add({field}[{idxVar}]);");
                    L(sb, indent + "    ", "}");
                    Blank(sb);
                }
                else if (operands[i].ValueKind == STJ.JsonValueKind.Array)
                {
                    // Array with operator sub-items — compile each and add
                    foreach (STJ.JsonElement item in operands[i].EnumerateArray())
                    {
                        string itemVar = EmitExpression(sb, item, indent + "    ", dataVar);
                        MaterializeIfDeferred(sb, itemVar, indent + "    ");
                        L(sb, indent + "    ", $"{bufVar}.Add({itemVar});");
                    }

                    Blank(sb);
                }
                else
                {
                    // Dynamic operand — evaluate, then flatten if array
                    string opVar = EmitExpression(sb, operands[i], indent + "    ", dataVar);
                    MaterializeIfDeferred(sb, opVar, indent + "    ");
                    L(sb, indent + "    ", $"if ({opVar}.ValueKind == JsonValueKind.Array)");
                    L(sb, indent + "    ", "{");
                    string mergeItem = NextVar();
                    L(sb, indent + "        ", $"foreach (JsonElement {mergeItem} in {opVar}.EnumerateArray())");
                    L(sb, indent + "        ", "{");
                    L(sb, indent + "            ", $"{bufVar}.Add({mergeItem});");
                    L(sb, indent + "        ", "}");
                    L(sb, indent + "    ", "}");
                    L(sb, indent + "    ", "else");
                    L(sb, indent + "    ", "{");
                    L(sb, indent + "        ", $"{bufVar}.Add({opVar});");
                    L(sb, indent + "    ", "}");
                    Blank(sb);
                }
            }

            L(sb, indent + "    ", $"{result} = {bufVar}.ToArrayResult(workspace);");
            L(sb, indent, "}");
            L(sb, indent, "finally");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{bufVar}.Dispose();");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitMap(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
            STJ.JsonElement[] compiled = GetOperands(args);
            if (compiled.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyArray();");
                return v;
            }

            string arrVar = EmitExpression(sb, compiled[0], indent, dataVar);
            MaterializeIfDeferred(sb, arrVar, indent);
            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.EmptyArray();");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            string bufVar = result + "_buf";
            L(sb, indent + "    ", $"var {bufVar} = new ElementBuffer();");
            L(sb, indent + "    ", "try");
            L(sb, indent + "    ", "{");
            string itemVar = result + "_item";
            L(sb, indent + "        ", $"foreach (JsonElement {itemVar} in {arrVar}.EnumerateArray())");
            L(sb, indent + "        ", "{");
            string bodyVar = EmitExpression(sb, compiled[1], indent + "            ", itemVar);
            MaterializeIfDeferred(sb, bodyVar, indent + "            ");
            L(sb, indent + "            ", $"{bufVar}.Add({bodyVar});");
            L(sb, indent + "        ", "}");
            Blank(sb);
            L(sb, indent + "        ", $"{result} = {bufVar}.ToArrayResult(workspace);");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "finally");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{bufVar}.Dispose();");
            L(sb, indent + "    ", "}");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitFilter(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
