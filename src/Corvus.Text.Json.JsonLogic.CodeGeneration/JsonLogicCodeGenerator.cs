            L(sb, indent + "    ", "}");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitReduce(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
            if (args.ValueKind != STJ.JsonValueKind.Array || args.GetArrayLength() < 3)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            // Detect reduce(map(arr, mapBody), reduceBody, init) → fused single loop
            STJ.JsonElement sourceExpr = args[0];
            if (sourceExpr.ValueKind == STJ.JsonValueKind.Object
                && sourceExpr.TryGetProperty("map", out STJ.JsonElement mapArgs)
                && mapArgs.ValueKind == STJ.JsonValueKind.Array && mapArgs.GetArrayLength() >= 2)
            {
                return EmitFusedMapReduce(sb, mapArgs, args[1], args[2], indent, dataVar);
            }

            bool canOptimize = BodyUsesOnlyReduceVars(args[1]);

            string arrVar = EmitExpression(sb, sourceExpr, indent, dataVar);
            MaterializeIfDeferred(sb, arrVar, indent);
            string initVar = EmitExpression(sb, args[2], indent, dataVar);

            // For the empty-array path we need a materialized init
            bool initIsDeferred = IsDeferredDouble(initVar);

            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");

            // Empty array → return init
            if (initIsDeferred)
            {
                L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result} = {initVar}_id ? JsonLogicHelpers.DoubleToElement({initVar}_dv, workspace) : {initVar};");
                L(sb, indent, "}");
            }
            else
            {
                MaterializeIfDeferred(sb, initVar, indent);
                L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result} = {initVar};");
                L(sb, indent, "}");
            }

            L(sb, indent, "else");
            L(sb, indent, "{");
            string accVar = result + "_acc";
            string itemVar = result + "_item";

            if (canOptimize)
            {
                // Accumulator as deferred double triple — mirrors RT's EvalResult
                L(sb, indent + "    ", $"double {accVar}_dv = 0;");
                L(sb, indent + "    ", $"bool {accVar}_id = false;");
                L(sb, indent + "    ", $"JsonElement {accVar} = default;");

                if (initIsDeferred)
                {
                    L(sb, indent + "    ", $"{accVar}_dv = {initVar}_dv;");
                    L(sb, indent + "    ", $"{accVar}_id = {initVar}_id;");
                    L(sb, indent + "    ", $"{accVar} = {initVar};");
                }
                else if (IsLiteralNumber(args[2]))
                {
                    double initDbl = GetLiteralDouble(args[2]);
                    L(sb, indent + "    ", $"{accVar}_dv = {FormatDouble(initDbl)};");
                    L(sb, indent + "    ", $"{accVar}_id = true;");
                }
                else
                {
                    L(sb, indent + "    ", $"{accVar}_id = JsonLogicHelpers.TryCoerceToDouble({initVar}, out {accVar}_dv);");
                    L(sb, indent + "    ", $"{accVar} = {initVar};");
                }

                _deferredDoubleVars.Add(accVar);

                // Use a writable copy of the foreach variable so MaterializeIfDeferred can reassign it
                string rawItemVar = itemVar + "_raw";
                L(sb, indent + "    ", $"foreach (JsonElement {rawItemVar} in {arrVar}.EnumerateArray())");
                L(sb, indent + "    ", "{");

                // Item as deferred double triple
                L(sb, indent + "        ", $"JsonElement {itemVar} = {rawItemVar};");
                L(sb, indent + "        ", $"bool {itemVar}_id = JsonLogicHelpers.TryCoerceToDouble({itemVar}, out double {itemVar}_dv);");
                _deferredDoubleVars.Add(itemVar);

                // Set reduce context so var("current") → itemVar, var("accumulator") → accVar
                string? savedCurrent = _reduceCurrentVar;
                string? savedAcc = _reduceAccVar;
                _reduceCurrentVar = itemVar;
                _reduceAccVar = accVar;

                string bodyVar = EmitExpression(sb, args[1], indent + "        ", "default");

                _reduceCurrentVar = savedCurrent;
                _reduceAccVar = savedAcc;

                // Copy body result back to accumulator — preserve deferred state
                if (IsDeferredDouble(bodyVar))
                {
                    L(sb, indent + "        ", $"{accVar}_dv = {bodyVar}_dv;");
                    L(sb, indent + "        ", $"{accVar}_id = {bodyVar}_id;");
                    L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                }
                else
                {
                    L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                    L(sb, indent + "        ", $"{accVar}_id = false;");
                }

                _deferredDoubleVars.Remove(bodyVar);
                _deferredDoubleVars.Remove(itemVar);
                L(sb, indent + "    ", "}");

                // Materialize final accumulator
                Blank(sb);
                L(sb, indent + "    ", $"if ({accVar}_id)");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = JsonLogicHelpers.DoubleToElement({accVar}_dv, workspace);");
                L(sb, indent + "    ", "}");
                L(sb, indent + "    ", "else");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = {accVar};");
                L(sb, indent + "    ", "}");
                _deferredDoubleVars.Remove(accVar);
            }
            else
            {
                // Fallback: use CreateObjectBuilder for context
                if (!initIsDeferred)
                {
                    L(sb, indent + "    ", $"JsonElement {accVar} = {initVar};");
                }
                else
                {
                    L(sb, indent + "    ", $"JsonElement {accVar} = {initVar}_id ? JsonLogicHelpers.DoubleToElement({initVar}_dv, workspace) : {initVar};");
                }

                string ctxDocVar = result + "_ctxDoc";
                string ctxRootVar = result + "_ctxRoot";
                L(sb, indent + "    ", $"JsonDocumentBuilder<JsonElement.Mutable> {ctxDocVar} = JsonElement.CreateObjectBuilder(workspace, 2);");
                L(sb, indent + "    ", $"JsonElement.Mutable {ctxRootVar} = {ctxDocVar}.RootElement;");
                L(sb, indent + "    ", $"foreach (JsonElement {itemVar} in {arrVar}.EnumerateArray())");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{ctxRootVar}.SetProperty(\"current\", {itemVar});");
                L(sb, indent + "        ", $"{ctxRootVar}.SetProperty(\"accumulator\", {accVar});");
                string ctxDataExpr = $"((JsonElement){ctxRootVar})";
                string bodyVar = EmitExpression(sb, args[1], indent + "        ", ctxDataExpr);
                MaterializeIfDeferred(sb, bodyVar, indent + "        ");
                L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                L(sb, indent + "    ", "}");
                Blank(sb);
                L(sb, indent + "    ", $"{result} = {accVar};");
            }

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitFusedMapReduce(StringBuilder sb, STJ.JsonElement mapArgs, STJ.JsonElement reduceBody, STJ.JsonElement initExpr, string indent, string dataVar)
        {
            bool canOptimize = BodyUsesOnlyReduceVars(reduceBody);

            // mapArgs[0] = source array expression, mapArgs[1] = map body
            string arrVar = EmitExpression(sb, mapArgs[0], indent, dataVar);
            MaterializeIfDeferred(sb, arrVar, indent);
            string initVar = EmitExpression(sb, initExpr, indent, dataVar);

            bool initIsDeferred = IsDeferredDouble(initVar);

            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");

            if (initIsDeferred)
            {
                L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result} = {initVar}_id ? JsonLogicHelpers.DoubleToElement({initVar}_dv, workspace) : {initVar};");
                L(sb, indent, "}");
            }
            else
            {
                MaterializeIfDeferred(sb, initVar, indent);
                L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result} = {initVar};");
                L(sb, indent, "}");
            }

            L(sb, indent, "else");
            L(sb, indent, "{");
            string accVar = result + "_acc";
            string itemVar = result + "_item";

            if (canOptimize)
            {
                // Accumulator as deferred double triple
                L(sb, indent + "    ", $"double {accVar}_dv = 0;");
                L(sb, indent + "    ", $"bool {accVar}_id = false;");
                L(sb, indent + "    ", $"JsonElement {accVar} = default;");

                if (initIsDeferred)
                {
                    L(sb, indent + "    ", $"{accVar}_dv = {initVar}_dv;");
                    L(sb, indent + "    ", $"{accVar}_id = {initVar}_id;");
                    L(sb, indent + "    ", $"{accVar} = {initVar};");
                }
                else if (IsLiteralNumber(initExpr))
                {
                    double initDbl = GetLiteralDouble(initExpr);
                    L(sb, indent + "    ", $"{accVar}_dv = {FormatDouble(initDbl)};");
                    L(sb, indent + "    ", $"{accVar}_id = true;");
                }
                else
                {
                    L(sb, indent + "    ", $"{accVar}_id = JsonLogicHelpers.TryCoerceToDouble({initVar}, out {accVar}_dv);");
                    L(sb, indent + "    ", $"{accVar} = {initVar};");
                }

                _deferredDoubleVars.Add(accVar);

                L(sb, indent + "    ", $"foreach (JsonElement {itemVar} in {arrVar}.EnumerateArray())");
                L(sb, indent + "    ", "{");

                // Inline the map body with item as data
                string mappedVar = EmitExpression(sb, mapArgs[1], indent + "        ", itemVar);

                // mappedVar may already be a deferred double (e.g., from arithmetic map body)
                // Set reduce context: current = mappedVar, accumulator = accVar
                string? savedCurrent = _reduceCurrentVar;
                string? savedAcc = _reduceAccVar;
                _reduceCurrentVar = mappedVar;
                _reduceAccVar = accVar;

                string bodyVar = EmitExpression(sb, reduceBody, indent + "        ", "default");

                _reduceCurrentVar = savedCurrent;
                _reduceAccVar = savedAcc;

                // Copy body result back to accumulator — preserve deferred state
                if (IsDeferredDouble(bodyVar))
                {
                    L(sb, indent + "        ", $"{accVar}_dv = {bodyVar}_dv;");
                    L(sb, indent + "        ", $"{accVar}_id = {bodyVar}_id;");
                    L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                }
                else
                {
                    MaterializeIfDeferred(sb, bodyVar, indent + "        ");
                    L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                    L(sb, indent + "        ", $"{accVar}_id = false;");
                }

                _deferredDoubleVars.Remove(bodyVar);
                _deferredDoubleVars.Remove(mappedVar);
                L(sb, indent + "    ", "}");

                // Materialize final accumulator
                Blank(sb);
                L(sb, indent + "    ", $"if ({accVar}_id)");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = JsonLogicHelpers.DoubleToElement({accVar}_dv, workspace);");
                L(sb, indent + "    ", "}");
                L(sb, indent + "    ", "else");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = {accVar};");
                L(sb, indent + "    ", "}");
                _deferredDoubleVars.Remove(accVar);
            }
            else
            {
                if (!initIsDeferred)
                {
                    L(sb, indent + "    ", $"JsonElement {accVar} = {initVar};");
                }
                else
                {
                    L(sb, indent + "    ", $"JsonElement {accVar} = {initVar}_id ? JsonLogicHelpers.DoubleToElement({initVar}_dv, workspace) : {initVar};");
                }

                string ctxDocVar = result + "_ctxDoc";
                string ctxRootVar = result + "_ctxRoot";
                L(sb, indent + "    ", $"JsonDocumentBuilder<JsonElement.Mutable> {ctxDocVar} = JsonElement.CreateObjectBuilder(workspace, 2);");
                L(sb, indent + "    ", $"JsonElement.Mutable {ctxRootVar} = {ctxDocVar}.RootElement;");
                L(sb, indent + "    ", $"foreach (JsonElement {itemVar} in {arrVar}.EnumerateArray())");
                L(sb, indent + "    ", "{");

                string mappedVar = EmitExpression(sb, mapArgs[1], indent + "        ", itemVar);
                MaterializeIfDeferred(sb, mappedVar, indent + "        ");
                L(sb, indent + "        ", $"{ctxRootVar}.SetProperty(\"current\", {mappedVar});");
                L(sb, indent + "        ", $"{ctxRootVar}.SetProperty(\"accumulator\", {accVar});");
                string ctxDataExpr = $"((JsonElement){ctxRootVar})";
                string bodyVar = EmitExpression(sb, reduceBody, indent + "        ", ctxDataExpr);
                MaterializeIfDeferred(sb, bodyVar, indent + "        ");
                L(sb, indent + "        ", $"{accVar} = {bodyVar};");
                L(sb, indent + "    ", "}");

                Blank(sb);
                L(sb, indent + "    ", $"{result} = {accVar};");
            }

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitQuantifier(StringBuilder sb, STJ.JsonElement args, string kind, string indent, string dataVar)
        {
            STJ.JsonElement[] compiled = GetOperands(args);
            if (compiled.Length < 2)
            {
                bool defaultVal = kind == "all" || kind == "none";
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement({(defaultVal ? "true" : "false")});");
                return v;
            }

            string arrVar = EmitExpression(sb, compiled[0], indent, dataVar);
            MaterializeIfDeferred(sb, arrVar, indent);
            string result = NextVar();
            string emptyResult = kind == "none" ? "true" : "false";
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if ({arrVar}.ValueKind != JsonValueKind.Array || {arrVar}.GetArrayLength() == 0)");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BooleanElement({emptyResult});");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");

            string flagVar = result + "_f";
            string flagDefault = kind == "some" ? "false" : "true";
            L(sb, indent + "    ", $"bool {flagVar} = {flagDefault};");
            string itemVar = result + "_item";
            L(sb, indent + "    ", $"foreach (JsonElement {itemVar} in {arrVar}.EnumerateArray())");
            L(sb, indent + "    ", "{");

            string truthyVar;
            if (TryEmitPredicateAsBool(sb, compiled[1], indent + "        ", itemVar, out string? boolPred))
            {
                truthyVar = boolPred!;
            }
            else
            {
                string predVar = EmitExpression(sb, compiled[1], indent + "        ", itemVar);
                MaterializeIfDeferred(sb, predVar, indent + "        ");
                truthyVar = result + "_t";
                L(sb, indent + "        ", $"bool {truthyVar} = JsonLogicHelpers.IsTruthy({predVar});");
            }

            switch (kind)
            {
                case "all":
                    L(sb, indent + "        ", $"if (!{truthyVar}) {{ {flagVar} = false; break; }}");
                    break;
                case "none":
                    L(sb, indent + "        ", $"if ({truthyVar}) {{ {flagVar} = false; break; }}");
                    break;
                case "some":
                    L(sb, indent + "        ", $"if ({truthyVar}) {{ {flagVar} = true; break; }}");
                    break;
            }

            L(sb, indent + "    ", "}");
            Blank(sb);
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BooleanElement({flagVar});");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        // ─── DATA ───────────────────────────────────────────────
        private string EmitMissing(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            string[] opVars = EmitOperands(sb, operands, indent, dataVar);
            for (int i = 0; i < opVars.Length; i++)
            {
                MaterializeIfDeferred(sb, opVars[i], indent);
            }

            string result = NextVar();
            string bufVar = result + "_buf";
            L(sb, indent, $"var {bufVar} = new ElementBuffer();");
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, "try");
            L(sb, indent, "{");

            for (int i = 0; i < opVars.Length; i++)
            {
                // Optimize literal string operands with no dots → inline TryGetProperty
                if (operands[i].ValueKind == STJ.JsonValueKind.String)
                {
                    string? pathStr = operands[i].GetString();
                    if (pathStr?.Contains('.') == false && pathStr.Length > 0)
                    {
                        string resolved = NextVar();
                        L(sb, indent + "    ", $"if ({dataVar}.ValueKind != JsonValueKind.Object || !{dataVar}.TryGetProperty(\"{EscapeCSharpString(pathStr)}\"u8, out JsonElement {resolved}) || {resolved}.IsNullOrUndefined())");
                        L(sb, indent + "    ", "{");
                        L(sb, indent + "        ", $"{bufVar}.Add({opVars[i]});");
                        L(sb, indent + "    ", "}");
                        Blank(sb);
                        continue;
                    }
                }

                // General case — use ResolveVar
                L(sb, indent + "    ", $"if ({opVars[i]}.ValueKind == JsonValueKind.Array)");
                L(sb, indent + "    ", "{");
                string mItem = NextVar();
                L(sb, indent + "        ", $"foreach (JsonElement {mItem} in {opVars[i]}.EnumerateArray())");
                L(sb, indent + "        ", "{");
                L(sb, indent + "            ", $"if (JsonLogicHelpers.ResolveVar({dataVar}, {mItem}).IsNullOrUndefined())");
                L(sb, indent + "            ", "{");
                L(sb, indent + "                ", $"{bufVar}.Add({mItem});");
                L(sb, indent + "            ", "}");
                L(sb, indent + "        ", "}");
                L(sb, indent + "    ", "}");
                L(sb, indent + "    ", "else");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"if (JsonLogicHelpers.ResolveVar({dataVar}, {opVars[i]}).IsNullOrUndefined())");
                L(sb, indent + "        ", "{");
                L(sb, indent + "            ", $"{bufVar}.Add({opVars[i]});");
                L(sb, indent + "        ", "}");
                L(sb, indent + "    ", "}");
                Blank(sb);
            }

            L(sb, indent + "    ", $"{result} = {bufVar}.ToArrayResult(workspace);");
            L(sb, indent, "}");
            L(sb, indent, "finally");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{bufVar}.Dispose();");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitMissingSome(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
            STJ.JsonElement[] compiled = GetOperands(args);
            if (compiled.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyArray();");
                return v;
            }

            string neededVar = EmitExpression(sb, compiled[0], indent, dataVar);
            string pathsVar = EmitExpression(sb, compiled[1], indent, dataVar);
            string result = NextVar();

            string neededIntVar = result + "_n";
            L(sb, indent, $"int {neededIntVar} = 0;");
            L(sb, indent, $"if ({neededVar}.ValueKind == JsonValueKind.Number && {neededVar}.TryGetDouble(out double {result}_nd))");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{neededIntVar} = (int){result}_nd;");
            L(sb, indent, "}");
            Blank(sb);

            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if ({pathsVar}.ValueKind != JsonValueKind.Array)");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.EmptyArray();");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            string bufVar = result + "_buf";
            L(sb, indent + "    ", $"var {bufVar} = new ElementBuffer();");
            L(sb, indent + "    ", "try");
            L(sb, indent + "    ", "{");
            string foundVar = result + "_found";
            L(sb, indent + "        ", $"int {foundVar} = 0;");
            string pathItem = result + "_p";
            L(sb, indent + "        ", $"foreach (JsonElement {pathItem} in {pathsVar}.EnumerateArray())");
            L(sb, indent + "        ", "{");
            string resolvedVar = result + "_r";
            L(sb, indent + "            ", $"JsonElement {resolvedVar} = JsonLogicHelpers.ResolveVar({dataVar}, {pathItem});");
            L(sb, indent + "            ", $"if ({resolvedVar}.IsNullOrUndefined())");
            L(sb, indent + "            ", "{");
            L(sb, indent + "                ", $"{bufVar}.Add({pathItem});");
            L(sb, indent + "            ", "}");
            L(sb, indent + "            ", "else");
            L(sb, indent + "            ", "{");
            L(sb, indent + "                ", $"{foundVar}++;");
            L(sb, indent + "            ", "}");
            L(sb, indent + "        ", "}");
            Blank(sb);
            L(sb, indent + "        ", $"if ({foundVar} >= {neededIntVar})");
            L(sb, indent + "        ", "{");
            L(sb, indent + "            ", $"{result} = JsonLogicHelpers.EmptyArray();");
            L(sb, indent + "        ", "}");
            L(sb, indent + "        ", "else");
            L(sb, indent + "        ", "{");
            L(sb, indent + "            ", $"{result} = {bufVar}.ToArrayResult(workspace);");
            L(sb, indent + "        ", "}");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "finally");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{bufVar}.Dispose();");
            L(sb, indent + "    ", "}");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

