        private string EmitSubstr(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyString();");
                return v;
            }

            string[] opVars = EmitOperands(sb, operands, indent, dataVar);
            string result = NextVar();
            string strVar = result + "_s";
            L(sb, indent, $"string? {strVar} = JsonLogicHelpers.CoerceToString({opVars[0]});");
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if ({strVar} is null)");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.EmptyString();");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");

            string startVar = result + "_start";
            if (opVars.Length > 1)
            {
                L(sb, indent + "    ", $"int {startVar} = (int)(long)CoerceToBigNumber({opVars[1]});");
            }
            else
            {
                L(sb, indent + "    ", $"int {startVar} = 0;");
            }

            L(sb, indent + "    ", $"if ({startVar} < 0) {{ {startVar} = System.Math.Max(0, {strVar}.Length + {startVar}); }}");
            L(sb, indent + "    ", $"if ({startVar} >= {strVar}.Length)");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.EmptyString();");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "else");
            L(sb, indent + "    ", "{");

            string lenVar = result + "_len";
            if (opVars.Length > 2)
            {
                L(sb, indent + "        ", $"int {lenVar} = (int)(long)CoerceToBigNumber({opVars[2]});");
                L(sb, indent + "        ", $"{lenVar} = {lenVar} < 0 ? System.Math.Max(0, {strVar}.Length - {startVar} + {lenVar}) : {lenVar};");
            }
            else
            {
                L(sb, indent + "        ", $"int {lenVar} = {strVar}.Length - {startVar};");
            }

            L(sb, indent + "        ", $"{lenVar} = System.Math.Min({lenVar}, {strVar}.Length - {startVar});");
            L(sb, indent + "        ", $"if ({lenVar} <= 0)");
            L(sb, indent + "        ", "{");
            L(sb, indent + "            ", $"{result} = JsonLogicHelpers.EmptyString();");
            L(sb, indent + "        ", "}");
            L(sb, indent + "        ", "else");
            L(sb, indent + "        ", "{");
            L(sb, indent + "            ", $"{result} = JsonLogicHelpers.StringToElement({strVar}.Substring({startVar}, {lenVar}));");
            L(sb, indent + "        ", "}");
            L(sb, indent + "    ", "}");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

