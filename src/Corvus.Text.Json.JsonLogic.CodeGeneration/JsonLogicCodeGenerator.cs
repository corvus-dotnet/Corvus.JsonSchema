using System.Diagnostics;
using System.Globalization;
using System.Text;
using Corvus.Numerics;
using STJ = System.Text.Json;

namespace Corvus.Text.Json.JsonLogic.CodeGeneration;

/// <summary>
/// Generates static C# evaluation code from a JsonLogic rule JSON string.
/// The generated code directly evaluates the rule without delegate dispatch
/// or compilation cache, calling <c>JsonLogicHelpers</c> for shared operations.
/// </summary>
public static class JsonLogicCodeGenerator
{
    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JsonLogic rule.
    /// </summary>
    /// <param name="ruleJson">The JsonLogic rule as a JSON string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <returns>A complete C# source file as a string.</returns>
    public static string Generate(string ruleJson, string className, string namespaceName)
    {
        return Generate(ruleJson, className, namespaceName, null);
    }

    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JsonLogic rule,
    /// with support for custom operators defined in <c>.jlops</c> files.
    /// </summary>
    /// <param name="ruleJson">The JsonLogic rule as a JSON string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <param name="customOperators">
    /// Optional custom operator definitions parsed from <c>.jlops</c> files.
    /// </param>
    /// <param name="maxDepth">
    /// The maximum JSON nesting depth to allow when parsing <paramref name="ruleJson"/>.
    /// Defaults to 64. Increase for deeply-nested rules.
    /// </param>
    /// <returns>A complete C# source file as a string.</returns>
    public static string Generate(string ruleJson, string className, string namespaceName, IReadOnlyList<CustomOperator>? customOperators, int maxDepth = 64)
    {
        STJ.JsonDocumentOptions options = new() { MaxDepth = maxDepth };
        using STJ.JsonDocument doc = STJ.JsonDocument.Parse(ruleJson, options);

        Dictionary<string, CustomOperator>? customOpMap = null;
        if (customOperators is { Count: > 0 })
        {
            customOpMap = new(StringComparer.Ordinal);
            foreach (CustomOperator op in customOperators)
            {
                customOpMap[op.Name] = op;
            }
        }

        Emitter emitter = new(customOpMap);
        StringBuilder bodySb = new(4096);

        string resultVar = emitter.EmitExpression(bodySb, doc.RootElement, "        ", "data");

        StringBuilder sb = new(bodySb.Length + 4096);

        L(sb, "", "// <auto-generated/>");
        L(sb, "", "#nullable enable");
        L(sb, "", "using System;");
        L(sb, "", "using Corvus.Numerics;");
        L(sb, "", "using Corvus.Text.Json;");
        L(sb, "", "using Corvus.Text.Json.JsonLogic;");
        Blank(sb);
        L(sb, "", $"namespace {namespaceName};");
        Blank(sb);
        L(sb, "", $"internal static class {className}");
        L(sb, "", "{");

        // Emit static literal fields
        foreach (KeyValuePair<string, string> kvp in emitter.StaticLiterals)
        {
            L(sb, "    ", $"private static readonly JsonElement {kvp.Value} = JsonElement.ParseValue({Utf8Literal(kvp.Key)});");
        }

        // Emit static array fields
        foreach (KeyValuePair<string, (string FieldName, string[] ElementRawTexts)> kvp in emitter.StaticArrays)
        {
            var elemBuilder = new StringBuilder();
            for (int i = 0; i < kvp.Value.ElementRawTexts.Length; i++)
            {
                if (i > 0)
                {
                    elemBuilder.Append(", ");
                }

                elemBuilder.Append($"JsonElement.ParseValue({Utf8Literal(kvp.Value.ElementRawTexts[i])})");
            }

            L(sb, "    ", $"private static readonly JsonElement[] {kvp.Value.FieldName} = [{elemBuilder}];");
        }

        if (emitter.StaticLiterals.Count > 0 || emitter.StaticArrays.Count > 0)
        {
            Blank(sb);
        }

        L(sb, "    ", "public static JsonElement Evaluate(in JsonElement data, JsonWorkspace workspace)");
        L(sb, "    ", "{");
        sb.Append(bodySb);

        // If the result is a deferred double, materialize at the return boundary
        if (emitter.IsDeferredDouble(resultVar))
        {
            L(sb, "        ", $"return {resultVar}_id ? JsonLogicHelpers.DoubleToElement({resultVar}_dv, workspace) : {resultVar};");
        }
        else
        {
            L(sb, "        ", $"return {resultVar};");
        }

        L(sb, "    ", "}");
        Blank(sb);

        EmitCustomOperatorMethods(sb, emitter.UsedCustomOperators);

        // Close class — no trailing newline (SA1518)
        sb.Append('}');

        return sb.ToString();
    }

    private static void L(StringBuilder sb, string indent, string line)
    {
        sb.Append(indent).Append(line).Append('\n');
    }

    private static void Blank(StringBuilder sb)
    {
        sb.Append('\n');
    }

    private static string Utf8Literal(string content)
    {
        StringBuilder sb = new(content.Length + 4);
        sb.Append('"');
        foreach (char c in content)
        {
            if (c == '"')
            {
                sb.Append("\\\"");
            }
            else if (c == '\\')
            {
                sb.Append("\\\\");
            }
            else
            {
                sb.Append(c);
            }
        }

        sb.Append("\"u8");
        return sb.ToString();
    }

    private static string EscapeCSharpString(string s)
    {
        return s.Replace("\\", "\\\\").Replace("\"", "\\\"");
    }

    private static bool TryParseNonNegativeInt(string s, out int value)
    {
        return int.TryParse(s, NumberStyles.None, CultureInfo.InvariantCulture, out value) && value >= 0;
    }

    private static bool AllLiteralNumbers(STJ.JsonElement[] operands)
    {
        foreach (STJ.JsonElement op in operands)
        {
            if (op.ValueKind != STJ.JsonValueKind.Number)
            {
                return false;
            }
        }

        return operands.Length > 0;
    }

    private static void EmitCustomOperatorMethods(StringBuilder sb, IReadOnlyDictionary<string, CustomOperator>? usedOps)
    {
        if (usedOps is null || usedOps.Count == 0)
        {
            return;
        }

        foreach (KeyValuePair<string, CustomOperator> kvp in usedOps)
        {
            CustomOperator op = kvp.Value;
            string methodName = $"CustomOp_{SanitizeIdentifier(op.Name)}";

            // Build parameter list: each param is a JsonElement, plus workspace
            StringBuilder paramList = new();
            for (int j = 0; j < op.Parameters.Length; j++)
            {
                if (j > 0)
                {
                    paramList.Append(", ");
                }

                paramList.Append($"in JsonElement {op.Parameters[j]}");
            }

            if (op.Parameters.Length > 0)
            {
                paramList.Append(", ");
            }

            paramList.Append("JsonWorkspace workspace");

            L(sb, "    ", $"private static JsonElement {methodName}({paramList})");
            L(sb, "    ", "{");

            if (op.IsExpression)
            {
                L(sb, "        ", $"return {op.Body};");
            }
            else
            {
                // Block body — emit each line with indentation
                string[] bodyLines = op.Body.Split('\n');
                foreach (string bodyLine in bodyLines)
                {
                    string trimmed = bodyLine.TrimEnd('\r');
                    if (trimmed.Trim().Length == 0)
                    {
                        Blank(sb);
                    }
                    else
                    {
                        // Preserve relative indentation from the .jlops file,
                        // adding base indentation for the method body.
                        string dedented = Dedent(trimmed);
                        L(sb, "        ", dedented);
                    }
                }
            }

            L(sb, "    ", "}");
            Blank(sb);
        }
    }

    private static string SanitizeIdentifier(string name)
    {
        char[] chars = new char[name.Length];
        for (int j = 0; j < name.Length; j++)
        {
            char c = name[j];
            chars[j] = char.IsLetterOrDigit(c) || c == '_' ? c : '_';
        }

        return new string(chars);
    }

    private static string Dedent(string line)
    {
        int start = 0;
        while (start < line.Length && (line[start] == ' ' || line[start] == '\t'))
        {
            start++;
        }

        return line.Substring(start);
    }

    private static STJ.JsonElement[] GetOperands(STJ.JsonElement args)
    {
        if (args.ValueKind == STJ.JsonValueKind.Array)
        {
            int count = args.GetArrayLength();
            STJ.JsonElement[] result = new STJ.JsonElement[count];
            int i = 0;
            foreach (STJ.JsonElement item in args.EnumerateArray())
            {
                result[i++] = item;
            }

            return result;
        }

        return new[] { args };
    }

    private sealed class Emitter
    {
        private readonly Dictionary<string, CustomOperator>? _customOperators;
        private readonly Dictionary<string, CustomOperator> _usedCustomOperators = new(StringComparer.Ordinal);
        private readonly Dictionary<string, string> _staticLiterals = new(StringComparer.Ordinal);
        private readonly Dictionary<string, (string FieldName, string[] ElementRawTexts)> _staticArrays = new(StringComparer.Ordinal);
        private readonly Dictionary<string, string> _varCache = new(StringComparer.Ordinal);
        private readonly HashSet<string> _deferredDoubleVars = new(StringComparer.Ordinal);
        private int _varCounter;
        private int _fieldCounter;

        // Reduce context: when set, var("current") and var("accumulator") resolve
        // to these local variable names instead of going through TryGetProperty.
        private string? _reduceCurrentVar;
        private string? _reduceAccVar;

        internal Emitter(Dictionary<string, CustomOperator>? customOperators)
        {
            _customOperators = customOperators;
        }

        internal IReadOnlyDictionary<string, CustomOperator> UsedCustomOperators => _usedCustomOperators;

        internal IReadOnlyDictionary<string, string> StaticLiterals => _staticLiterals;

        internal IReadOnlyDictionary<string, (string FieldName, string[] ElementRawTexts)> StaticArrays => _staticArrays;

        internal bool IsDeferredDouble(string varName) => _deferredDoubleVars.Contains(varName);

        internal string NextVar() => $"v{_varCounter++}";

        private string NextField() => $"s_lit{_fieldCounter++}";

        /// <summary>
        /// If <paramref name="varName"/> is a deferred double, emit materialization code
        /// so that the JsonElement variable is valid for non-arithmetic consumers.
        /// </summary>
        private void MaterializeIfDeferred(StringBuilder sb, string varName, string indent)
        {
            if (_deferredDoubleVars.Remove(varName))
            {
                L(sb, indent, $"if ({varName}_id) {{ {varName} = JsonLogicHelpers.DoubleToElement({varName}_dv, workspace); }}");
            }
        }

        private static bool IsArithmeticOp(STJ.JsonElement rule)
        {
            if (rule.ValueKind != STJ.JsonValueKind.Object)
            {
                return false;
            }

            foreach (STJ.JsonProperty prop in rule.EnumerateObject())
            {
                return prop.Name is "+" or "-" or "*" or "/" or "%";
            }

            return false;
        }

        private static bool IsLiteralNumber(STJ.JsonElement rule)
        {
            return rule.ValueKind == STJ.JsonValueKind.Number;
        }

        private static double GetLiteralDouble(STJ.JsonElement rule)
        {
            return rule.GetDouble();
        }

        private static string FormatDouble(double value)
        {
            string s = value.ToString("R", CultureInfo.InvariantCulture);
            if (!s.Contains('.') && !s.Contains('E') && !s.Contains('e'))
            {
                s += ".0";
            }

            return s;
        }

        /// <summary>
        /// Checks whether a reduce body only references var("current") and var("accumulator")
        /// — no other var paths, no var(""), no dotted paths, no nested scope-creating operators.
        /// </summary>
        private static bool BodyUsesOnlyReduceVars(STJ.JsonElement rule)
        {
            if (rule.ValueKind == STJ.JsonValueKind.Object)
            {
                bool hasProps = false;
                foreach (STJ.JsonProperty prop in rule.EnumerateObject())
                {
                    hasProps = true;
                    if (prop.Name == "var")
                    {
                        return IsReduceVarPath(prop.Value);
                    }

                    // Scope-creating operators that would bind "current"/"accumulator" to a different scope
                    if (prop.Name is "map" or "filter" or "reduce" or "all" or "some" or "none")
                    {
                        return false;
                    }

                    // Non-var operator: recurse into its arguments
                    return BodyUsesOnlyReduceVars(prop.Value);
                }

                if (!hasProps)
                {
                    return true;
                }
            }

            if (rule.ValueKind == STJ.JsonValueKind.Array)
            {
                foreach (STJ.JsonElement item in rule.EnumerateArray())
                {
                    if (!BodyUsesOnlyReduceVars(item))
                    {
                        return false;
                    }
                }

                return true;
            }

            // Literals are always safe
            return true;
        }

        private static bool IsReduceVarPath(STJ.JsonElement args)
        {
            STJ.JsonElement pathArg = args;
            if (args.ValueKind == STJ.JsonValueKind.Array)
            {
                if (args.GetArrayLength() == 0)
                {
                    return false;
                }

                pathArg = args[0];
            }

            if (pathArg.ValueKind != STJ.JsonValueKind.String)
            {
                return false;
            }

            string? path = pathArg.GetString();
            return path is "current" or "accumulator";
        }

        internal string EmitExpression(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            if (rule.ValueKind == STJ.JsonValueKind.Object)
            {
                bool hasProps = false;
                foreach (STJ.JsonProperty p in rule.EnumerateObject())
                {
                    _ = p;
                    hasProps = true;
                    break;
                }

                if (hasProps)
                {
                    return EmitOperatorCall(sb, rule, indent, dataVar);
                }
            }

            if (rule.ValueKind == STJ.JsonValueKind.Array)
            {
                return EmitArrayLiteral(sb, rule, indent, dataVar);
            }

            return EmitLiteral(sb, rule, indent);
        }

        private string EmitOperatorCall(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            foreach (STJ.JsonProperty prop in rule.EnumerateObject())
            {
                string opName = prop.Name;
                STJ.JsonElement args = prop.Value;

                switch (opName)
                {
                    case "var":
                        return EmitVar(sb, args, indent, dataVar);
                    case "map":
                        return EmitMap(sb, args, indent, dataVar);
                    case "filter":
                        return EmitFilter(sb, args, indent, dataVar);
                    case "reduce":
                        return EmitReduce(sb, args, indent, dataVar);
                    case "all":
                        return EmitQuantifier(sb, args, "all", indent, dataVar);
                    case "none":
                        return EmitQuantifier(sb, args, "none", indent, dataVar);
                    case "some":
                        return EmitQuantifier(sb, args, "some", indent, dataVar);
                    case "missing_some":
                        return EmitMissingSome(sb, args, indent, dataVar);
                }

                STJ.JsonElement[] operands = GetOperands(args);

                return opName switch
                {
                    "+" => EmitAdd(sb, operands, indent, dataVar),
                    "-" => EmitSub(sb, operands, indent, dataVar),
                    "*" => EmitMul(sb, operands, indent, dataVar),
                    "/" => EmitDiv(sb, operands, indent, dataVar),
                    "%" => EmitMod(sb, operands, indent, dataVar),
                    "min" => EmitMinMax(sb, operands, true, indent, dataVar),
                    "max" => EmitMinMax(sb, operands, false, indent, dataVar),
                    "cat" => EmitCat(sb, operands, indent, dataVar),
                    "substr" => EmitSubstr(sb, operands, indent, dataVar),
                    "and" => EmitAnd(sb, operands, indent, dataVar),
                    "or" => EmitOr(sb, operands, indent, dataVar),
                    "if" or "?:" => EmitIf(sb, operands, indent, dataVar),
                    "==" => EmitEquality(sb, operands, false, false, indent, dataVar),
                    "===" => EmitEquality(sb, operands, true, false, indent, dataVar),
                    "!=" => EmitEquality(sb, operands, false, true, indent, dataVar),
                    "!==" => EmitEquality(sb, operands, true, true, indent, dataVar),
                    "!" => EmitNot(sb, operands, indent, dataVar),
                    "!!" => EmitTruthy(sb, operands, indent, dataVar),
                    ">" => EmitComparison(sb, operands, 1, indent, dataVar),
                    ">=" => EmitComparison(sb, operands, 2, indent, dataVar),
                    "<" => EmitComparison(sb, operands, 3, indent, dataVar),
                    "<=" => EmitComparison(sb, operands, 4, indent, dataVar),
                    "in" => EmitIn(sb, operands, indent, dataVar),
                    "merge" => EmitMerge(sb, operands, indent, dataVar),
                    "missing" => EmitMissing(sb, operands, indent, dataVar),
                    "log" => EmitLog(sb, operands, indent, dataVar),
                    "asDouble" => EmitAsDouble(sb, operands, indent, dataVar),
                    "asLong" => EmitAsLong(sb, operands, indent, dataVar),
                    "asBigNumber" => EmitAsBigNumber(sb, operands, indent, dataVar),
                    "asBigInteger" => EmitAsBigInteger(sb, operands, indent, dataVar),
                    _ => EmitCustomOrThrow(sb, opName, operands, indent, dataVar),
                };
            }

            return EmitLiteral(sb, rule, indent);
        }

        // ─── CUSTOM OPERATORS ────────────────────────────────────
        private string EmitCustomOrThrow(StringBuilder sb, string opName, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (_customOperators is not null && _customOperators.TryGetValue(opName, out CustomOperator? customOp))
            {
                if (operands.Length != customOp.Parameters.Length)
                {
                    throw new InvalidOperationException(
                        $"Custom operator '{opName}' expects {customOp.Parameters.Length} argument(s) but got {operands.Length}.");
                }

                // Track that this custom operator is used so its method is emitted
                _usedCustomOperators[opName] = customOp;

                string[] opVars = EmitOperands(sb, operands, indent, dataVar);
                for (int j = 0; j < opVars.Length; j++)
                {
                    MaterializeIfDeferred(sb, opVars[j], indent);
                }

                string result = NextVar();

                // Build call: CustomOp_name(v0, v1, workspace)
                StringBuilder call = new();
                call.Append($"CustomOp_{SanitizeIdentifier(opName)}(");
                for (int j = 0; j < opVars.Length; j++)
                {
                    call.Append(opVars[j]);
                    call.Append(", ");
                }

                call.Append("workspace)");

                L(sb, indent, $"JsonElement {result} = {call};");
                Blank(sb);
                return result;
            }

            throw new InvalidOperationException($"Unknown JsonLogic operator: {opName}");
        }

        // ─── LITERALS ────────────────────────────────────────────
        private string EmitLiteral(StringBuilder sb, STJ.JsonElement value, string indent)
        {
            string v = NextVar();
            switch (value.ValueKind)
            {
                case STJ.JsonValueKind.Number:
                    string rawNum = value.GetRawText();
                    if (rawNum == "0")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                    }
                    else if (rawNum == "1")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.One();");
                    }
                    else
                    {
                        string field = GetOrCreateStaticLiteral(rawNum);
                        L(sb, indent, $"JsonElement {v} = {field};");
                    }

                    break;

                case STJ.JsonValueKind.String:
                    string rawStr = value.GetRawText();
                    if (rawStr == "\"\"")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyString();");
                    }
                    else
                    {
                        string field = GetOrCreateStaticLiteral(rawStr);
                        L(sb, indent, $"JsonElement {v} = {field};");
                    }

                    break;

                case STJ.JsonValueKind.True:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(true);");
                    break;

                case STJ.JsonValueKind.False:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(false);");
                    break;

                default:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                    break;
            }

            return v;
        }

        private string GetOrCreateStaticLiteral(string rawText)
        {
            if (_staticLiterals.TryGetValue(rawText, out string? existing))
            {
                return existing;
            }

            string fieldName = NextField();
            _staticLiterals[rawText] = fieldName;
            return fieldName;
        }

        private string GetOrCreateStaticArray(STJ.JsonElement arrayElement)
        {
            string key = arrayElement.GetRawText();
            if (_staticArrays.TryGetValue(key, out (string FieldName, string[] ElementRawTexts) existing))
            {
                return existing.FieldName;
            }

            string fieldName = NextField();
            string[] elements = new string[arrayElement.GetArrayLength()];
            int idx = 0;
            foreach (STJ.JsonElement sub in arrayElement.EnumerateArray())
            {
                elements[idx++] = sub.GetRawText();
            }

            _staticArrays[key] = (fieldName, elements);
            return fieldName;
        }

        private static bool IsConstantLiteralArray(STJ.JsonElement element)
        {
            if (element.ValueKind != STJ.JsonValueKind.Array)
            {
                return false;
            }

            foreach (STJ.JsonElement sub in element.EnumerateArray())
            {
                if (sub.ValueKind is STJ.JsonValueKind.Object or STJ.JsonValueKind.Array)
                {
                    return false;
                }
            }

            return true;
        }

        private string EmitArrayLiteral(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            int count = rule.GetArrayLength();
            if (count == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyArray();");
                return v;
            }

            string[] items = new string[count];
            int i = 0;
            foreach (STJ.JsonElement item in rule.EnumerateArray())
            {
                items[i++] = EmitExpression(sb, item, indent, dataVar);
            }

            string result = NextVar();
            string bufVar = result + "_buf";
            L(sb, indent, $"var {bufVar} = new ElementBuffer();");
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, "try");
            L(sb, indent, "{");
            for (int j = 0; j < items.Length; j++)
            {
                MaterializeIfDeferred(sb, items[j], indent + "    ");
                L(sb, indent + "    ", $"{bufVar}.Add({items[j]});");
            }

            L(sb, indent + "    ", $"{result} = {bufVar}.ToArrayResult(workspace);");
            L(sb, indent, "}");
            L(sb, indent, "finally");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{bufVar}.Dispose();");
            L(sb, indent, "}");
            return result;
        }

        private string EmitFoldedConstant(StringBuilder sb, BigNumber value, string indent)
        {
            string v = NextVar();
            byte[] buf = new byte[128];
            if (value.TryFormat(buf.AsSpan(), out int written))
            {
                string numStr = System.Text.Encoding.UTF8.GetString(buf, 0, written);
                if (numStr == "0")
                {
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                }
                else if (numStr == "1")
                {
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.One();");
                }
                else
                {
                    string field = GetOrCreateStaticLiteral(numStr);
                    L(sb, indent, $"JsonElement {v} = {field};");
                }
            }
            else
            {
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
            }

            return v;
        }

        // ─── VAR ─────────────────────────────────────────────────
        private string EmitVar(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
            STJ.JsonElement pathArg;
            STJ.JsonElement? defaultArg = null;

            if (args.ValueKind == STJ.JsonValueKind.Array)
            {
                int count = args.GetArrayLength();
                if (count == 0)
                {
                    return dataVar;
                }

                pathArg = args[0];
                if (count >= 2)
                {
                    defaultArg = args[1];
                }
            }
            else
            {
                pathArg = args;
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.Null || pathArg.ValueKind == STJ.JsonValueKind.Undefined)
            {
                return dataVar;
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.String)
            {
                string? path = pathArg.GetString();
                if (string.IsNullOrEmpty(path))
                {
                    return dataVar;
                }

                return EmitStaticVarPath(sb, path!, defaultArg, indent, dataVar);
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.Number)
            {
                return EmitStaticVarPath(sb, pathArg.GetRawText(), defaultArg, indent, dataVar);
            }

            // Computed path (object expression)
            return EmitDynamicVarPath(sb, pathArg, defaultArg, indent, dataVar);
        }

        private string EmitStaticVarPath(StringBuilder sb, string path, STJ.JsonElement? defaultArg, string indent, string dataVar)
        {
            // Reduce context intercept: if we're inside a reduce body and the path is
            // "current" or "accumulator", return the local variable directly.
            if (_reduceCurrentVar is not null && defaultArg is null && !path.Contains('.'))
            {
                if (path == "current")
                {
                    return _reduceCurrentVar;
                }

                if (path == "accumulator")
                {
                    return _reduceAccVar!;
                }
            }

            // Cache simple var lookups (single segment, no default) within the same data scope
            if (defaultArg is null)
            {
                string cacheKey = $"{dataVar}:{path}";
                if (_varCache.TryGetValue(cacheKey, out string? cached))
                {
                    return cached;
                }
            }

            string[] segments = path.Split('.');
            string result = NextVar();

            if (segments.Length == 1 && defaultArg is null)
            {
                string seg = segments[0];
                if (TryParseNonNegativeInt(seg, out int index))
                {
                    L(sb, indent, $"JsonElement {result};");
                    L(sb, indent, $"if ({dataVar}.ValueKind == JsonValueKind.Object && {dataVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {result}_p))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = {result}_p;");
                    L(sb, indent, "}");
                    L(sb, indent, $"else if ({dataVar}.ValueKind == JsonValueKind.Array && {index} < {dataVar}.GetArrayLength())");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = {dataVar}[{index}];");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
                    L(sb, indent, "}");
                }
                else
                {
                    L(sb, indent, $"JsonElement {result} = {dataVar}.ValueKind == JsonValueKind.Object && {dataVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {result}_p)");
                    L(sb, indent + "    ", $"? {result}_p : JsonLogicHelpers.NullElement();");
                }

                // Cache for reuse
                _varCache[$"{dataVar}:{path}"] = result;

                Blank(sb);
                return result;
            }

            // Multi-segment or with default
            L(sb, indent, $"JsonElement {result} = default;");
            string flagVar = result + "_ok";
            L(sb, indent, $"bool {flagVar} = true;");
            string curVar = result + "_cur";
            L(sb, indent, $"JsonElement {curVar} = {dataVar};");

            for (int i = 0; i < segments.Length; i++)
            {
                string seg = segments[i];
                if (TryParseNonNegativeInt(seg, out int index))
                {
                    string propVar = $"{result}_s{i}";
                    L(sb, indent, $"if ({flagVar} && {curVar}.ValueKind == JsonValueKind.Object && {curVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {propVar}))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{curVar} = {propVar};");
                    L(sb, indent, "}");
                    L(sb, indent, $"else if ({flagVar} && {curVar}.ValueKind == JsonValueKind.Array && {index} >= 0 && {index} < {curVar}.GetArrayLength())");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{curVar} = {curVar}[{index}];");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{flagVar} = false;");
                    L(sb, indent, "}");
                }
                else
                {
                    L(sb, indent, $"if ({flagVar} && ({curVar}.ValueKind != JsonValueKind.Object || !{curVar}.TryGetProperty({Utf8Literal(seg)}, out {curVar})))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{flagVar} = false;");
                    L(sb, indent, "}");
                }

                Blank(sb);
            }

            L(sb, indent, $"if ({flagVar})");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = {curVar};");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            if (defaultArg is not null)
            {
                string defVar = EmitExpression(sb, defaultArg.Value, indent + "    ", dataVar);
                L(sb, indent + "    ", $"{result} = {defVar};");
            }
            else
            {
                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
            }

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitDynamicVarPath(StringBuilder sb, STJ.JsonElement pathExpr, STJ.JsonElement? defaultArg, string indent, string dataVar)
        {
            string pathVar = EmitExpression(sb, pathExpr, indent, dataVar);
            string result = NextVar();
            L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.ResolveVar({dataVar}, {pathVar});");
            if (defaultArg is not null)
            {
                L(sb, indent, $"if ({result}.IsNullOrUndefined())");
                L(sb, indent, "{");
                string defVar = EmitExpression(sb, defaultArg.Value, indent + "    ", dataVar);
                L(sb, indent + "    ", $"{result} = {defVar};");
                L(sb, indent, "}");
            }

            Blank(sb);
            return result;
        }

