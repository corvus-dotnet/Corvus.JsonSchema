        private string EmitLog(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            return EmitExpression(sb, operands[0], indent, dataVar);
        }

        private string EmitAsDouble(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                return v;
            }

            string opVar = EmitExpression(sb, operands[0], indent, dataVar);
            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if (JsonLogicHelpers.TryCoerceToDouble({opVar}, out double {result}_d))");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BigNumberToElement((BigNumber){result}_d, workspace);");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

