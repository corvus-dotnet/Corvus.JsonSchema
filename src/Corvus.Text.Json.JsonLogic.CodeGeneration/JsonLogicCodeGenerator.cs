        /// <summary>
        /// Emits a deferred-double arithmetic result. Returns the result variable name.
        /// The result is registered as a deferred double (has companion _dv, _id variables).
        /// Operands that are literals get inlined as double constants; operands that are
        /// deferred doubles chain directly; others get TryCoerceToDouble guards.
        /// </summary>
        private string EmitDeferredArithmetic(
            StringBuilder sb,
            STJ.JsonElement[] operands,
            string doubleOp,
            string bigNumberOp,
            string bigNumberIdentity,
            string indent,
            string dataVar)
        {
            // Emit all operands first (preserves evaluation order)
            string[] opVars = new string[operands.Length];
            for (int i = 0; i < operands.Length; i++)
            {
                opVars[i] = EmitExpression(sb, operands[i], indent, dataVar);
            }

            string result = NextVar();
            L(sb, indent, $"double {result}_dv = 0;");
            L(sb, indent, $"bool {result}_id = false;");
            L(sb, indent, $"JsonElement {result} = default;");

            // Build the double-path condition and expression
            // For each operand: literal numbers → inline constant, deferred doubles → check _id,
            // others → TryCoerceToDouble
            List<string> conditions = new();
            List<string> doubleExprs = new();
            List<string> bigNumberExprs = new();

            for (int i = 0; i < operands.Length; i++)
            {
                if (IsLiteralNumber(operands[i]))
                {
                    double d = GetLiteralDouble(operands[i]);
                    doubleExprs.Add(FormatDouble(d));
                    bigNumberExprs.Add($"BigNumber.Parse(\"{operands[i].GetRawText()}\")");
                }
                else if (_deferredDoubleVars.Contains(opVars[i]))
                {
                    conditions.Add($"{opVars[i]}_id");
                    doubleExprs.Add($"{opVars[i]}_dv");
                    bigNumberExprs.Add($"JsonLogicHelpers.CoerceToBigNumber({opVars[i]}_id ? JsonLogicHelpers.DoubleToElement({opVars[i]}_dv, workspace) : {opVars[i]})");
                }
                else
                {
                    string dVar = $"{result}_t{i}";
                    conditions.Add($"JsonLogicHelpers.TryCoerceToDouble({opVars[i]}, out double {dVar})");
                    doubleExprs.Add(dVar);
                    bigNumberExprs.Add($"JsonLogicHelpers.CoerceToBigNumber({opVars[i]})");
                }
            }

            // Build double expression string
            StringBuilder dblExpr = new();
            for (int i = 0; i < doubleExprs.Count; i++)
            {
                if (i > 0)
                {
                    dblExpr.Append($" {doubleOp} ");
                }

                dblExpr.Append(doubleExprs[i]);
            }

            // Build BigNumber expression string
            StringBuilder bnExpr = new();
            bnExpr.Append(bigNumberIdentity);
            foreach (string bnOp in bigNumberExprs)
            {
                bnExpr.Append($" {bigNumberOp} {bnOp}");
            }

            if (conditions.Count > 0)
            {
                L(sb, indent, $"if ({string.Join(" && ", conditions)})");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result}_dv = {dblExpr};");
                L(sb, indent + "    ", $"{result}_id = true;");
                L(sb, indent, "}");
                L(sb, indent, "else");
                L(sb, indent, "{");

                // Materialize any deferred operands for BigNumber path
                for (int i = 0; i < operands.Length; i++)
                {
                    if (_deferredDoubleVars.Contains(opVars[i]))
                    {
                        L(sb, indent + "    ", $"if ({opVars[i]}_id) {{ {opVars[i]} = JsonLogicHelpers.DoubleToElement({opVars[i]}_dv, workspace); }}");
                    }
                }

                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BigNumberToElement({bnExpr}, workspace);");
                L(sb, indent, "}");
            }
            else
            {
                // All operands are compile-time known doubles (literals only)
                L(sb, indent, $"{result}_dv = {dblExpr};");
                L(sb, indent, $"{result}_id = true;");
            }

            Blank(sb);
            _deferredDoubleVars.Add(result);
            return result;
        }

        /// <summary>
        /// Emits a deferred-double binary arithmetic op (a op b).
        /// Correctly handles subtraction where BigNumber fallback is JsonLogicHelpers.CoerceToBigNumber(a) op JsonLogicHelpers.CoerceToBigNumber(b).
        /// </summary>
        private string EmitDeferredBinaryArithmetic(
            StringBuilder sb,
            STJ.JsonElement[] operands,
            string op,
            string indent,
            string dataVar)
        {
            Debug.Assert(operands.Length == 2, "Binary arithmetic requires exactly 2 operands.");

            string lhsVar = EmitExpression(sb, operands[0], indent, dataVar);
            string rhsVar = EmitExpression(sb, operands[1], indent, dataVar);

            string result = NextVar();
            L(sb, indent, $"double {result}_dv = 0;");
            L(sb, indent, $"bool {result}_id = false;");
            L(sb, indent, $"JsonElement {result} = default;");

            // Build per-operand: condition, double expression, BigNumber expression
            string[] vars = [lhsVar, rhsVar];
            List<string> conditions = new();
            string[] dblExprs = new string[2];
            string[] bnExprs = new string[2];

            for (int i = 0; i < 2; i++)
            {
                if (IsLiteralNumber(operands[i]))
                {
                    double d = GetLiteralDouble(operands[i]);
                    dblExprs[i] = FormatDouble(d);
                    bnExprs[i] = $"BigNumber.Parse(\"{operands[i].GetRawText()}\")";
                }
                else if (_deferredDoubleVars.Contains(vars[i]))
                {
                    conditions.Add($"{vars[i]}_id");
                    dblExprs[i] = $"{vars[i]}_dv";
                    bnExprs[i] = $"JsonLogicHelpers.CoerceToBigNumber({vars[i]}_id ? JsonLogicHelpers.DoubleToElement({vars[i]}_dv, workspace) : {vars[i]})";
                }
                else
                {
                    string tVar = $"{result}_t{i}";
                    conditions.Add($"JsonLogicHelpers.TryCoerceToDouble({vars[i]}, out double {tVar})");
                    dblExprs[i] = tVar;
                    bnExprs[i] = $"JsonLogicHelpers.CoerceToBigNumber({vars[i]})";
                }
            }

            if (conditions.Count > 0)
            {
                L(sb, indent, $"if ({string.Join(" && ", conditions)})");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result}_dv = {dblExprs[0]} {op} {dblExprs[1]};");
                L(sb, indent + "    ", $"{result}_id = true;");
                L(sb, indent, "}");
                L(sb, indent, "else");
                L(sb, indent, "{");

                for (int i = 0; i < 2; i++)
                {
                    if (_deferredDoubleVars.Contains(vars[i]))
                    {
                        L(sb, indent + "    ", $"if ({vars[i]}_id) {{ {vars[i]} = JsonLogicHelpers.DoubleToElement({vars[i]}_dv, workspace); }}");
                    }
                }

                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BigNumberToElement({bnExprs[0]} {op} {bnExprs[1]}, workspace);");
                L(sb, indent, "}");
            }
            else
            {
                L(sb, indent, $"{result}_dv = {dblExprs[0]} {op} {dblExprs[1]};");
                L(sb, indent, $"{result}_id = true;");
            }

            Blank(sb);
            _deferredDoubleVars.Add(result);
            return result;
        }

        private string EmitAdd(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                return v;
            }

            if (operands.Length == 1)
            {
                string opVar = EmitExpression(sb, operands[0], indent, dataVar);
                MaterializeIfDeferred(sb, opVar, indent);
                string v = NextVar();
                L(sb, indent, $"if (!JsonLogicHelpers.TryCoerceToNumber({opVar}, workspace, out JsonElement {v}))");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{v} = JsonLogicHelpers.Zero();");
                L(sb, indent, "}");
                Blank(sb);
                return v;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber sum = BigNumber.Zero;
                foreach (STJ.JsonElement op in operands)
                {
                    sum += BigNumber.Parse(op.GetRawText());
                }

                return EmitFoldedConstant(sb, sum, indent);
            }

            return EmitDeferredArithmetic(sb, operands, "+", "+", "BigNumber.Zero", indent, dataVar);
        }

        private string EmitSub(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                return v;
            }

            if (operands.Length == 1)
            {
                // Unary negation — produce a deferred double
                string opVar = EmitExpression(sb, operands[0], indent, dataVar);
                string result = NextVar();
                L(sb, indent, $"double {result}_dv = 0;");
                L(sb, indent, $"bool {result}_id = false;");
                L(sb, indent, $"JsonElement {result} = default;");

                if (IsLiteralNumber(operands[0]))
                {
                    double d = GetLiteralDouble(operands[0]);
                    L(sb, indent, $"{result}_dv = {FormatDouble(-d)};");
                    L(sb, indent, $"{result}_id = true;");
                }
                else if (_deferredDoubleVars.Contains(opVar))
                {
                    L(sb, indent, $"if ({opVar}_id)");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result}_dv = -{opVar}_dv;");
                    L(sb, indent + "    ", $"{result}_id = true;");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BigNumberToElement(-JsonLogicHelpers.CoerceToBigNumber({opVar}), workspace);");
                    L(sb, indent, "}");
                }
                else
                {
                    L(sb, indent, $"if (JsonLogicHelpers.TryCoerceToDouble({opVar}, out double {result}_t))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result}_dv = -{result}_t;");
                    L(sb, indent + "    ", $"{result}_id = true;");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BigNumberToElement(-JsonLogicHelpers.CoerceToBigNumber({opVar}), workspace);");
                    L(sb, indent, "}");
                }

                Blank(sb);
                _deferredDoubleVars.Add(result);
                return result;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber diff = BigNumber.Parse(operands[0].GetRawText()) - BigNumber.Parse(operands[1].GetRawText());
                return EmitFoldedConstant(sb, diff, indent);
            }

            return EmitDeferredBinaryArithmetic(sb, operands, "-", indent, dataVar);
        }

        private string EmitMul(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                return v;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber product = BigNumber.One;
                foreach (STJ.JsonElement op in operands)
                {
                    product *= BigNumber.Parse(op.GetRawText());
                }

                return EmitFoldedConstant(sb, product, indent);
            }

            return EmitDeferredArithmetic(sb, operands, "*", "*", "BigNumber.One", indent, dataVar);
        }

        private string EmitDiv(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber divisor = BigNumber.Parse(operands[1].GetRawText());
                if (divisor != BigNumber.Zero)
                {
                    BigNumber quotient = BigNumber.Parse(operands[0].GetRawText()) / divisor;
                    return EmitFoldedConstant(sb, quotient, indent);
                }
            }

            // Emit operands
            string lhsVar = EmitExpression(sb, operands[0], indent, dataVar);
            string rhsVar = EmitExpression(sb, operands[1], indent, dataVar);

            // Materialize any deferred doubles since div can return null
            MaterializeIfDeferred(sb, lhsVar, indent);
            MaterializeIfDeferred(sb, rhsVar, indent);

            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if (JsonLogicHelpers.TryCoerceToDouble({lhsVar}, out double {result}_d0) && JsonLogicHelpers.TryCoerceToDouble({rhsVar}, out double {result}_d1))");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = {result}_d1 == 0 ? JsonLogicHelpers.NullElement() : JsonLogicHelpers.DoubleToElement({result}_d0 / {result}_d1, workspace);");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");

            string divVar = NextVar();
            L(sb, indent + "    ", $"BigNumber {divVar} = JsonLogicHelpers.CoerceToBigNumber({rhsVar});");
            L(sb, indent + "    ", $"if ({divVar} == BigNumber.Zero)");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.NullElement();");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "else");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.BigNumberToElement(JsonLogicHelpers.CoerceToBigNumber({lhsVar}) / {divVar}, workspace);");
            L(sb, indent + "    ", "}");

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitMod(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber divisor = BigNumber.Parse(operands[1].GetRawText());
                if (divisor != BigNumber.Zero)
                {
                    BigNumber remainder = BigNumber.Parse(operands[0].GetRawText()) % divisor;
                    return EmitFoldedConstant(sb, remainder, indent);
                }
            }

            // Emit operands
            string lhsVar = EmitExpression(sb, operands[0], indent, dataVar);
            string rhsVar = EmitExpression(sb, operands[1], indent, dataVar);

            // Materialize any deferred doubles since mod can return null
            MaterializeIfDeferred(sb, lhsVar, indent);
            MaterializeIfDeferred(sb, rhsVar, indent);

            string result = NextVar();
            L(sb, indent, $"JsonElement {result};");
            L(sb, indent, $"if (JsonLogicHelpers.TryCoerceToDouble({lhsVar}, out double {result}_d0) && JsonLogicHelpers.TryCoerceToDouble({rhsVar}, out double {result}_d1))");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = {result}_d1 == 0 ? JsonLogicHelpers.NullElement() : JsonLogicHelpers.DoubleToElement({result}_d0 % {result}_d1, workspace);");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");

            string modVar = NextVar();
            L(sb, indent + "    ", $"BigNumber {modVar} = JsonLogicHelpers.CoerceToBigNumber({rhsVar});");
            L(sb, indent + "    ", $"if ({modVar} == BigNumber.Zero)");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.NullElement();");
            L(sb, indent + "    ", "}");
            L(sb, indent + "    ", "else");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.BigNumberToElement(JsonLogicHelpers.CoerceToBigNumber({lhsVar}) % {modVar}, workspace);");
            L(sb, indent + "    ", "}");

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

        private string EmitMinMax(StringBuilder sb, STJ.JsonElement[] operands, bool isMin, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                return v;
            }

            // Emit operands preserving evaluation order (don't materialize deferred doubles)
            string[] opVars = new string[operands.Length];
            for (int i = 0; i < operands.Length; i++)
            {
                opVars[i] = EmitExpression(sb, operands[i], indent, dataVar);
            }

            string result = NextVar();
            string cmpOp = isMin ? "<" : ">";

            // Build double fast path: literal numbers inline, deferred doubles via _dv,
            // general elements via TryCoerceToDouble.
            List<string> conds = new();
            string[] dblNames = new string[opVars.Length];

            // Track which operands are element-backed (can return original element instead of DoubleToElement)
            bool[] isElementBacked = new bool[opVars.Length];

            for (int i = 0; i < opVars.Length; i++)
            {
                if (IsLiteralNumber(operands[i]))
                {
                    double d = GetLiteralDouble(operands[i]);
                    dblNames[i] = FormatDouble(d);
                }
                else if (_deferredDoubleVars.Contains(opVars[i]))
                {
                    conds.Add($"{opVars[i]}_id");
                    dblNames[i] = $"{opVars[i]}_dv";
                }
                else
                {
                    string tmpD = $"{result}_d{i}";
                    conds.Add($"JsonLogicHelpers.TryCoerceToDouble({opVars[i]}, out double {tmpD})");
                    dblNames[i] = tmpD;
                    isElementBacked[i] = true;
                }
            }

            // Check if all non-literal operands are element-backed (common case: all from var lookups).
            // In that case we can return the original element instead of DoubleToElement.
            bool allElementBacked = true;
            for (int i = 0; i < opVars.Length; i++)
            {
                if (!IsLiteralNumber(operands[i]) && !isElementBacked[i])
                {
                    allElementBacked = false;
                    break;
                }
            }

            L(sb, indent, $"JsonElement {result};");

            if (conds.Count > 0)
            {
                L(sb, indent, $"if ({string.Join(" && ", conds)})");
                L(sb, indent, "{");

                if (allElementBacked)
                {
                    // Track both best double and best element — return original element
                    string bestD = $"{result}_bestD";
                    string bestE = $"{result}_bestE";

                    // First operand: determine initial best
                    if (isElementBacked[0])
                    {
                        L(sb, indent + "    ", $"double {bestD} = {dblNames[0]};");
                        L(sb, indent + "    ", $"JsonElement {bestE} = {opVars[0]};");
                    }
                    else
                    {
                        // Literal — inline the double but need to create element for tracking
                        L(sb, indent + "    ", $"double {bestD} = {dblNames[0]};");
                        L(sb, indent + "    ", $"JsonElement {bestE} = JsonElement.ParseValue(\"{FormatDoubleForJson(GetLiteralDouble(operands[0]))}\"u8);");
                    }

                    for (int i = 1; i < dblNames.Length; i++)
                    {
                        if (isElementBacked[i])
                        {
                            L(sb, indent + "    ", $"if ({dblNames[i]} {cmpOp} {bestD}) {{ {bestD} = {dblNames[i]}; {bestE} = {opVars[i]}; }}");
                        }
                        else
                        {
                            // Literal operand
                            L(sb, indent + "    ", $"if ({dblNames[i]} {cmpOp} {bestD}) {{ {bestD} = {dblNames[i]}; {bestE} = JsonElement.ParseValue(\"{FormatDoubleForJson(GetLiteralDouble(operands[i]))}\"u8); }}");
                        }
                    }

                    L(sb, indent + "    ", $"{result} = {bestE};");
                }
                else
                {
                    // Mixed deferred doubles / elements — can't track elements, use DoubleToElement
                    string bestD = $"{result}_bestD";
                    L(sb, indent + "    ", $"double {bestD} = {dblNames[0]};");
                    for (int i = 1; i < dblNames.Length; i++)
                    {
                        L(sb, indent + "    ", $"if ({dblNames[i]} {cmpOp} {bestD}) {{ {bestD} = {dblNames[i]}; }}");
                    }

                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.DoubleToElement({bestD}, workspace);");
                }

                L(sb, indent, "}");
                L(sb, indent, "else");
                L(sb, indent, "{");

                // Fallback: materialize deferred doubles, use TryCoerceToNumber + CompareNumbers
                for (int i = 0; i < opVars.Length; i++)
                {
                    if (_deferredDoubleVars.Contains(opVars[i]))
                    {
                        L(sb, indent + "    ", $"if ({opVars[i]}_id) {{ {opVars[i]} = JsonLogicHelpers.DoubleToElement({opVars[i]}_dv, workspace); }}");
                    }
                }

                EmitMinMaxSlow(sb, opVars, result, isMin, indent + "    ");
                L(sb, indent, "}");
            }
            else
            {
                // All operands are literal numbers — constant fold at compile time
                double best = GetLiteralDouble(operands[0]);
                for (int i = 1; i < operands.Length; i++)
                {
                    double d = GetLiteralDouble(operands[i]);
                    if (isMin ? d < best : d > best)
                    {
                        best = d;
                    }
                }

                string litField = GetOrCreateStaticLiteral($"{FormatDoubleForJson(best)}");
                L(sb, indent, $"{result} = {litField};");
            }

            Blank(sb);
            return result;
        }

        private static string FormatDoubleForJson(double value)
        {
            string s = value.ToString("G17", System.Globalization.CultureInfo.InvariantCulture);

            // Ensure it looks like a number (not scientific notation that JSON doesn't support)
            if (s.Contains('E') || s.Contains('e'))
            {
                // For very large/small numbers, format as decimal
                return value.ToString("R", System.Globalization.CultureInfo.InvariantCulture);
            }

            return s;
        }

        private void EmitMinMaxSlow(StringBuilder sb, string[] opVars, string result, bool isMin, string indent)
        {
            string cmpOp = isMin ? "<" : ">";
            L(sb, indent, "do");
            L(sb, indent, "{");
            string bestVar = result + "_best";
            L(sb, indent + "    ", $"if (!JsonLogicHelpers.TryCoerceToNumber({opVars[0]}, workspace, out JsonElement {bestVar}))");
            L(sb, indent + "    ", "{");
            L(sb, indent + "        ", $"{result} = JsonLogicHelpers.NullElement();");
            L(sb, indent + "        ", "break;");
            L(sb, indent + "    ", "}");
            Blank(sb);

            for (int i = 1; i < opVars.Length; i++)
            {
                string candVar = $"{result}_c{i}";
                L(sb, indent + "    ", $"if (!JsonLogicHelpers.TryCoerceToNumber({opVars[i]}, workspace, out JsonElement {candVar}))");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{result} = JsonLogicHelpers.NullElement();");
                L(sb, indent + "        ", "break;");
                L(sb, indent + "    ", "}");
                Blank(sb);
                L(sb, indent + "    ", $"if (JsonLogicHelpers.CompareNumbers({candVar}, {bestVar}) {cmpOp} 0)");
                L(sb, indent + "    ", "{");
                L(sb, indent + "        ", $"{bestVar} = {candVar};");
                L(sb, indent + "    ", "}");
                Blank(sb);
            }

            L(sb, indent + "    ", $"{result} = {bestVar};");
            L(sb, indent, "}");
            L(sb, indent, "while (false);");
        }

        // ─── COMPARISON ─────────────────────────────────────────
        private string EmitComparison(StringBuilder sb, STJ.JsonElement[] operands, int compOp, string indent, string dataVar)
        {
            if (operands.Length < 2)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(false);");
                return v;
            }

            // Emit operands (preserves evaluation order)
            string[] opVars = new string[operands.Length];
            for (int i = 0; i < operands.Length; i++)
            {
                opVars[i] = EmitExpression(sb, operands[i], indent, dataVar);
            }

            string result = NextVar();

            // Try inline double comparison: if all operands are deferred-doubles or literal numbers,
            // emit direct double comparison without CompareCoerced.
            // Also optimize the common case where all operands can use TryCoerceToDouble.
            // Build a condition + double expression for each operand.
            List<string> conds = new();
            string[] dblNames = new string[opVars.Length];

            for (int i = 0; i < opVars.Length; i++)
            {
                if (IsLiteralNumber(operands[i]))
                {
                    double d = GetLiteralDouble(operands[i]);
                    dblNames[i] = FormatDouble(d);
                }
                else if (_deferredDoubleVars.Contains(opVars[i]))
                {
                    // Deferred double — null guard + use _dv
                    conds.Add($"!{opVars[i]}.IsNullOrUndefined()");
                    conds.Add($"{opVars[i]}_id");
                    dblNames[i] = $"{opVars[i]}_dv";
                }
                else
                {
                    // General case — need null guard + TryCoerceToDouble
                    conds.Add($"!{opVars[i]}.IsNullOrUndefined()");
                    string tmpD = $"{result}_cd{i}";
                    conds.Add($"JsonLogicHelpers.TryCoerceToDouble({opVars[i]}, out double {tmpD})");
                    dblNames[i] = tmpD;
                }
            }

            string compOpStr = compOp switch { 1 => ">", 2 => ">=", 3 => "<", 4 => "<=", _ => ">" };

            // Build double comparison expression
            StringBuilder dblCmp = new();
            for (int i = 0; i < opVars.Length - 1; i++)
            {
                if (i > 0)
                {
                    dblCmp.Append(" && ");
                }

                dblCmp.Append($"{dblNames[i]} {compOpStr} {dblNames[i + 1]}");
            }

            if (conds.Count > 0)
            {
                L(sb, indent, $"JsonElement {result};");
                L(sb, indent, $"if ({string.Join(" && ", conds)})");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BooleanElement({dblCmp});");
                L(sb, indent, "}");
                L(sb, indent, "else");
                L(sb, indent, "{");

                // Fallback: materialize any deferred doubles and use CompareCoerced
                for (int i = 0; i < opVars.Length; i++)
                {
                    if (_deferredDoubleVars.Contains(opVars[i]))
                    {
                        L(sb, indent + "    ", $"if ({opVars[i]}_id) {{ {opVars[i]} = JsonLogicHelpers.DoubleToElement({opVars[i]}_dv, workspace); }}");
                    }
                }

                if (opVars.Length == 2)
                {
                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BooleanElement(JsonLogicHelpers.CompareCoerced({opVars[0]}, {opVars[1]}, {compOp}));");
                }
                else
                {
                    StringBuilder fallback = new();
                    for (int i = 0; i < opVars.Length - 1; i++)
                    {
                        if (i > 0)
                        {
                            fallback.Append(" && ");
                        }

                        fallback.Append($"JsonLogicHelpers.CompareCoerced({opVars[i]}, {opVars[i + 1]}, {compOp})");
                    }

                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.BooleanElement({fallback});");
                }

                L(sb, indent, "}");
            }
            else
            {
                // Pure literals — just emit the comparison directly
                L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.BooleanElement({dblCmp});");
            }

            Blank(sb);
            return result;
        }

        // ─── EQUALITY ───────────────────────────────────────────
        private string EmitEquality(StringBuilder sb, STJ.JsonElement[] operands, bool strict, bool negate, string indent, string dataVar)
        {
            if (operands.Length < 2)
            {
                bool defaultVal = negate;
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement({(defaultVal ? "true" : "false")});");
                return v;
            }

            string[] opVars = EmitOperands(sb, operands, indent, dataVar);
            for (int i = 0; i < opVars.Length; i++)
            {
                MaterializeIfDeferred(sb, opVars[i], indent);
            }

            string result = NextVar();
            string method = strict ? "JsonLogicHelpers.StrictElementEquals" : "JsonLogicHelpers.CoercingElementEquals";
            string negStr = negate ? "!" : "";
            L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.BooleanElement({negStr}{method}({opVars[0]}, {opVars[1]}));");
            Blank(sb);
            return result;
        }

