        // ─── HELPERS ────────────────────────────────────────────
        private string[] EmitOperands(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            string[] vars = new string[operands.Length];
            for (int i = 0; i < operands.Length; i++)
            {
                vars[i] = EmitExpression(sb, operands[i], indent, dataVar);
            }

            return vars;
        }
