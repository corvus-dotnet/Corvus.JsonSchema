                    if (i > 0)
                    {
                        cond.Append(" && ");
                    }

                    cond.Append($"CompareCoerced({opVars[i]}, {opVars[i + 1]}, {compOp})");
                }

                L(sb, indent, $"JsonElement {result} = JsonLogicHelpers.BooleanElement({cond});");
            }

            Blank(sb);
            return result;
        }

