        // ─── ARITHMETIC ─────────────────────────────────────────
        private string EmitAdd(StringBuilder sb, STJ.JsonElement[] operands, string indent, string dataVar)
        {
            if (operands.Length == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                return v;
            }

            if (operands.Length == 1)
            {
                string opVar = EmitExpression(sb, operands[0], indent, dataVar);
                string v = NextVar();
                L(sb, indent, $"if (!JsonLogicHelpers.TryCoerceToNumber({opVar}, workspace, out JsonElement {v}))");
                L(sb, indent, "{");
                L(sb, indent + "    ", $"{v} = JsonLogicHelpers.Zero();");
                L(sb, indent, "}");
                Blank(sb);
                return v;
            }

            // Constant folding
            if (AllLiteralNumbers(operands))
            {
                BigNumber sum = BigNumber.Zero;
                foreach (STJ.JsonElement op in operands)
                {
                    sum += BigNumber.Parse(op.GetRawText());
                }

                return EmitFoldedConstant(sb, sum, indent);
            }

            // General case
            string[] opVars = EmitOperands(sb, operands, indent, dataVar);
            string sumVar = NextVar();
            L(sb, indent, $"BigNumber {sumVar} = BigNumber.Zero;");
            foreach (string opVar in opVars)
            {
                L(sb, indent, $"{sumVar} += CoerceToBigNumber({opVar});");
            }

            string result = NextVar();
            L(sb, indent, $"JsonElement {result} = BigNumberToElement({sumVar}, workspace);");
            Blank(sb);
            return result;
        }

