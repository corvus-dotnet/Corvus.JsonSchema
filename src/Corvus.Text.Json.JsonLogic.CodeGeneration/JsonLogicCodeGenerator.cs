using System.Globalization;
using System.Text;
using Corvus.Numerics;
using STJ = System.Text.Json;

namespace Corvus.Text.Json.JsonLogic.CodeGeneration;

/// <summary>
/// Generates static C# evaluation code from a JsonLogic rule JSON string.
/// The generated code directly evaluates the rule without delegate dispatch
/// or compilation cache, calling <c>JsonLogicHelpers</c> for shared operations.
/// </summary>
public static class JsonLogicCodeGenerator
{
    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JsonLogic rule.
    /// </summary>
    /// <param name="ruleJson">The JsonLogic rule as a JSON string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <returns>A complete C# source file as a string.</returns>
    public static string Generate(string ruleJson, string className, string namespaceName)
    {
        using STJ.JsonDocument doc = STJ.JsonDocument.Parse(ruleJson);
        Emitter emitter = new();
        StringBuilder sb = new(4096);

        L(sb, "", "// <auto-generated/>");
        L(sb, "", "#nullable enable");
        L(sb, "", "using Corvus.Numerics;");
        L(sb, "", "using Corvus.Text.Json;");
        L(sb, "", "using Corvus.Text.Json.JsonLogic;");
        Blank(sb);
        L(sb, "", $"namespace {namespaceName};");
        Blank(sb);
        L(sb, "", $"internal static class {className}");
        L(sb, "", "{");
        L(sb, "    ", "public static JsonElement Evaluate(in JsonElement data, JsonWorkspace workspace)");
        L(sb, "    ", "{");

        string resultVar = emitter.EmitExpression(sb, doc.RootElement, "        ", "data");

        L(sb, "        ", $"return {resultVar};");
        L(sb, "    ", "}");
        Blank(sb);

        EmitHelperMethods(sb);

        // Close class — no trailing newline (SA1518)
        sb.Append('}');

        return sb.ToString();
    }

    private static void L(StringBuilder sb, string indent, string line)
    {
        sb.Append(indent).Append(line).Append('\n');
    }

    private static void Blank(StringBuilder sb)
    {
        sb.Append('\n');
    }

    private static string Utf8Literal(string content)
    {
        StringBuilder sb = new(content.Length + 4);
        sb.Append('"');
        foreach (char c in content)
        {
            if (c == '"')
            {
                sb.Append("\\\"");
            }
            else if (c == '\\')
            {
                sb.Append("\\\\");
            }
            else
            {
                sb.Append(c);
            }
        }

        sb.Append("\"u8");
        return sb.ToString();
    }

    private static bool TryParseNonNegativeInt(string s, out int value)
    {
        return int.TryParse(s, NumberStyles.None, CultureInfo.InvariantCulture, out value) && value >= 0;
    }

    private static bool AllLiteralNumbers(STJ.JsonElement[] operands)
    {
        foreach (STJ.JsonElement op in operands)
        {
            if (op.ValueKind != STJ.JsonValueKind.Number)
            {
                return false;
            }
        }

        return operands.Length > 0;
    }

    private static void EmitHelperMethods(StringBuilder sb)
    {
        // StrictElementEquals
        L(sb, "    ", "private static bool StrictElementEquals(in JsonElement left, in JsonElement right)");
        L(sb, "    ", "{");
        L(sb, "        ", "if (left.ValueKind != right.ValueKind)");
        L(sb, "        ", "{");
        L(sb, "            ", "return false;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.IsNullOrUndefined() && right.IsNullOrUndefined())");
        L(sb, "        ", "{");
        L(sb, "            ", "return true;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.ValueKind == JsonValueKind.Number)");
        L(sb, "        ", "{");
        L(sb, "            ", "return JsonLogicHelpers.AreNumbersEqual(left, right);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.ValueKind == JsonValueKind.String)");
        L(sb, "        ", "{");
        L(sb, "            ", "return left.GetRawText() == right.GetRawText();");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "return left.ValueKind is JsonValueKind.True or JsonValueKind.False;");
        L(sb, "    ", "}");
        Blank(sb);

        // CoercingElementEquals
        L(sb, "    ", "private static bool CoercingElementEquals(in JsonElement left, in JsonElement right)");
        L(sb, "    ", "{");
        L(sb, "        ", "if (left.ValueKind == right.ValueKind)");
        L(sb, "        ", "{");
        L(sb, "            ", "return StrictElementEquals(left, right);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.IsNullOrUndefined() && right.IsNullOrUndefined())");
        L(sb, "        ", "{");
        L(sb, "            ", "return true;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.IsNullOrUndefined() || right.IsNullOrUndefined())");
        L(sb, "        ", "{");
        L(sb, "            ", "return false;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.ValueKind == JsonValueKind.Number && right.ValueKind == JsonValueKind.String)");
        L(sb, "        ", "{");
        L(sb, "            ", "return JsonLogicHelpers.TryCoerceToNumber(right, out JsonElement rn) && JsonLogicHelpers.AreNumbersEqual(left, rn);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.ValueKind == JsonValueKind.String && right.ValueKind == JsonValueKind.Number)");
        L(sb, "        ", "{");
        L(sb, "            ", "return JsonLogicHelpers.TryCoerceToNumber(left, out JsonElement ln) && JsonLogicHelpers.AreNumbersEqual(ln, right);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (left.ValueKind is JsonValueKind.True or JsonValueKind.False)");
        L(sb, "        ", "{");
        L(sb, "            ", "JsonElement leftNum = left.ValueKind == JsonValueKind.True ? JsonLogicHelpers.One() : JsonLogicHelpers.Zero();");
        L(sb, "            ", "return CoercingElementEquals(leftNum, right);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (right.ValueKind is JsonValueKind.True or JsonValueKind.False)");
        L(sb, "        ", "{");
        L(sb, "            ", "JsonElement rightNum = right.ValueKind == JsonValueKind.True ? JsonLogicHelpers.One() : JsonLogicHelpers.Zero();");
        L(sb, "            ", "return CoercingElementEquals(left, rightNum);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "return false;");
        L(sb, "    ", "}");
        Blank(sb);

        // CompareCoerced
        L(sb, "    ", "private static bool CompareCoerced(in JsonElement left, in JsonElement right, int op)");
        L(sb, "    ", "{");
        L(sb, "        ", "if (left.IsNullOrUndefined() || right.IsNullOrUndefined())");
        L(sb, "        ", "{");
        L(sb, "            ", "return false;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (!JsonLogicHelpers.TryCoerceToNumber(left, out JsonElement ln) || !JsonLogicHelpers.TryCoerceToNumber(right, out JsonElement rn))");
        L(sb, "        ", "{");
        L(sb, "            ", "return false;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "int cmp = JsonLogicHelpers.CompareNumbers(ln, rn);");
        L(sb, "        ", "return op switch");
        L(sb, "        ", "{");
        L(sb, "            ", "1 => cmp > 0,");
        L(sb, "            ", "2 => cmp >= 0,");
        L(sb, "            ", "3 => cmp < 0,");
        L(sb, "            ", "4 => cmp <= 0,");
        L(sb, "            ", "_ => false,");
        L(sb, "        ", "};");
        L(sb, "    ", "}");
        Blank(sb);

        // CoerceToBigNumber
        L(sb, "    ", "private static BigNumber CoerceToBigNumber(in JsonElement element)");
        L(sb, "    ", "{");
        L(sb, "        ", "if (element.ValueKind == JsonValueKind.Number && BigNumber.TryParse(element.GetRawText(), out BigNumber result))");
        L(sb, "        ", "{");
        L(sb, "            ", "return result;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (element.ValueKind == JsonValueKind.True)");
        L(sb, "        ", "{");
        L(sb, "            ", "return BigNumber.One;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (element.ValueKind == JsonValueKind.False || element.ValueKind == JsonValueKind.Null)");
        L(sb, "        ", "{");
        L(sb, "            ", "return BigNumber.Zero;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "if (element.ValueKind == JsonValueKind.String && JsonLogicHelpers.TryCoerceToNumber(element, out JsonElement numElem) && BigNumber.TryParse(numElem.GetRawText(), out BigNumber numResult))");
        L(sb, "        ", "{");
        L(sb, "            ", "return numResult;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "return BigNumber.Zero;");
        L(sb, "    ", "}");
        Blank(sb);

        // BigNumberToElement
        L(sb, "    ", "private static JsonElement BigNumberToElement(BigNumber value, JsonWorkspace workspace)");
        L(sb, "    ", "{");
        L(sb, "        ", "Span<byte> buffer = stackalloc byte[64];");
        L(sb, "        ", "if (value.TryFormat(buffer, out int bytesWritten))");
        L(sb, "        ", "{");
        L(sb, "            ", "return JsonLogicHelpers.NumberFromSpan(buffer.Slice(0, bytesWritten), workspace);");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "return JsonLogicHelpers.Zero();");
        L(sb, "    ", "}");
        Blank(sb);

        // ResolveVar
        L(sb, "    ", "private static JsonElement ResolveVar(in JsonElement data, in JsonElement pathElement)");
        L(sb, "    ", "{");
        L(sb, "        ", "if (pathElement.IsNullOrUndefined())");
        L(sb, "        ", "{");
        L(sb, "            ", "return data;");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "string? pathStr;");
        L(sb, "        ", "if (pathElement.ValueKind == JsonValueKind.Number)");
        L(sb, "        ", "{");
        L(sb, "            ", "pathStr = pathElement.GetRawText();");
        L(sb, "        ", "}");
        L(sb, "        ", "else if (pathElement.ValueKind == JsonValueKind.String)");
        L(sb, "        ", "{");
        L(sb, "            ", "pathStr = pathElement.GetString();");
        L(sb, "            ", "if (string.IsNullOrEmpty(pathStr))");
        L(sb, "            ", "{");
        L(sb, "                ", "return data;");
        L(sb, "            ", "}");
        L(sb, "        ", "}");
        L(sb, "        ", "else");
        L(sb, "        ", "{");
        L(sb, "            ", "return JsonLogicHelpers.NullElement();");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "string[] segments = pathStr!.Split('.');");
        L(sb, "        ", "JsonElement current = data;");
        L(sb, "        ", "foreach (string seg in segments)");
        L(sb, "        ", "{");
        L(sb, "            ", "if (current.IsNullOrUndefined())");
        L(sb, "            ", "{");
        L(sb, "                ", "return JsonLogicHelpers.NullElement();");
        L(sb, "            ", "}");
        Blank(sb);
        L(sb, "            ", "if (current.ValueKind == JsonValueKind.Object)");
        L(sb, "            ", "{");
        L(sb, "                ", "if (!current.TryGetProperty(seg, out current))");
        L(sb, "                ", "{");
        L(sb, "                    ", "return JsonLogicHelpers.NullElement();");
        L(sb, "                ", "}");
        L(sb, "            ", "}");
        L(sb, "            ", "else if (current.ValueKind == JsonValueKind.Array");
        L(sb, "                ", "&& int.TryParse(seg, System.Globalization.NumberStyles.None, System.Globalization.CultureInfo.InvariantCulture, out int idx)");
        L(sb, "                ", "&& idx >= 0 && idx < current.GetArrayLength())");
        L(sb, "            ", "{");
        L(sb, "                ", "current = current[idx];");
        L(sb, "            ", "}");
        L(sb, "            ", "else");
        L(sb, "            ", "{");
        L(sb, "                ", "return JsonLogicHelpers.NullElement();");
        L(sb, "            ", "}");
        L(sb, "        ", "}");
        Blank(sb);
        L(sb, "        ", "return current;");
        L(sb, "    ", "}");
        Blank(sb);
    }

    private static STJ.JsonElement[] GetOperands(STJ.JsonElement args)
    {
        if (args.ValueKind == STJ.JsonValueKind.Array)
        {
            int count = args.GetArrayLength();
            STJ.JsonElement[] result = new STJ.JsonElement[count];
            int i = 0;
            foreach (STJ.JsonElement item in args.EnumerateArray())
            {
                result[i++] = item;
            }

            return result;
        }

        return new[] { args };
    }

    private sealed class Emitter
    {
        private int _varCounter;

        internal string NextVar() => $"v{_varCounter++}";

        internal string EmitExpression(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            if (rule.ValueKind == STJ.JsonValueKind.Object)
            {
                bool hasProps = false;
                foreach (STJ.JsonProperty p in rule.EnumerateObject())
                {
                    _ = p;
                    hasProps = true;
                    break;
                }

                if (hasProps)
                {
                    return EmitOperatorCall(sb, rule, indent, dataVar);
                }
            }

            if (rule.ValueKind == STJ.JsonValueKind.Array)
            {
                return EmitArrayLiteral(sb, rule, indent, dataVar);
            }

            return EmitLiteral(sb, rule, indent);
        }

        private string EmitOperatorCall(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            foreach (STJ.JsonProperty prop in rule.EnumerateObject())
            {
                string opName = prop.Name;
                STJ.JsonElement args = prop.Value;

                switch (opName)
                {
                    case "var":
                        return EmitVar(sb, args, indent, dataVar);
                    case "map":
                        return EmitMap(sb, args, indent, dataVar);
                    case "filter":
                        return EmitFilter(sb, args, indent, dataVar);
                    case "reduce":
                        return EmitReduce(sb, args, indent, dataVar);
                    case "all":
                        return EmitQuantifier(sb, args, "all", indent, dataVar);
                    case "none":
                        return EmitQuantifier(sb, args, "none", indent, dataVar);
                    case "some":
                        return EmitQuantifier(sb, args, "some", indent, dataVar);
                    case "missing_some":
                        return EmitMissingSome(sb, args, indent, dataVar);
                }

                STJ.JsonElement[] operands = GetOperands(args);

                return opName switch
                {
                    "+" => EmitAdd(sb, operands, indent, dataVar),
                    "-" => EmitSub(sb, operands, indent, dataVar),
                    "*" => EmitMul(sb, operands, indent, dataVar),
                    "/" => EmitDiv(sb, operands, indent, dataVar),
                    "%" => EmitMod(sb, operands, indent, dataVar),
                    "min" => EmitMinMax(sb, operands, true, indent, dataVar),
                    "max" => EmitMinMax(sb, operands, false, indent, dataVar),
                    "cat" => EmitCat(sb, operands, indent, dataVar),
                    "substr" => EmitSubstr(sb, operands, indent, dataVar),
                    "and" => EmitAnd(sb, operands, indent, dataVar),
                    "or" => EmitOr(sb, operands, indent, dataVar),
                    "if" or "?:" => EmitIf(sb, operands, indent, dataVar),
                    "==" => EmitEquality(sb, operands, false, false, indent, dataVar),
                    "===" => EmitEquality(sb, operands, true, false, indent, dataVar),
                    "!=" => EmitEquality(sb, operands, false, true, indent, dataVar),
                    "!==" => EmitEquality(sb, operands, true, true, indent, dataVar),
                    "!" => EmitNot(sb, operands, indent, dataVar),
                    "!!" => EmitTruthy(sb, operands, indent, dataVar),
                    ">" => EmitComparison(sb, operands, 1, indent, dataVar),
                    ">=" => EmitComparison(sb, operands, 2, indent, dataVar),
                    "<" => EmitComparison(sb, operands, 3, indent, dataVar),
                    "<=" => EmitComparison(sb, operands, 4, indent, dataVar),
                    "in" => EmitIn(sb, operands, indent, dataVar),
                    "merge" => EmitMerge(sb, operands, indent, dataVar),
                    "missing" => EmitMissing(sb, operands, indent, dataVar),
                    "log" => EmitLog(sb, operands, indent, dataVar),
                    "asDouble" => EmitAsDouble(sb, operands, indent, dataVar),
                    "asLong" => EmitAsLong(sb, operands, indent, dataVar),
                    "asBigNumber" => EmitAsBigNumber(sb, operands, indent, dataVar),
                    "asBigInteger" => EmitAsBigInteger(sb, operands, indent, dataVar),
                    _ => throw new InvalidOperationException($"Unknown JsonLogic operator: {opName}"),
                };
            }

            return EmitLiteral(sb, rule, indent);
        }

        // ─── LITERALS ────────────────────────────────────────────
        private string EmitLiteral(StringBuilder sb, STJ.JsonElement value, string indent)
        {
            string v = NextVar();
            switch (value.ValueKind)
            {
                case STJ.JsonValueKind.Number:
                    string rawNum = value.GetRawText();
                    if (rawNum == "0")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
                    }
                    else if (rawNum == "1")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.One();");
                    }
                    else
                    {
                        L(sb, indent, $"JsonElement {v} = JsonElement.ParseValue({Utf8Literal(rawNum)});");
                    }

                    break;

                case STJ.JsonValueKind.String:
                    string rawStr = value.GetRawText();
                    if (rawStr == "\"\"")
                    {
                        L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyString();");
                    }
                    else
                    {
                        L(sb, indent, $"JsonElement {v} = JsonElement.ParseValue({Utf8Literal(rawStr)});");
                    }

                    break;

                case STJ.JsonValueKind.True:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(true);");
                    break;

                case STJ.JsonValueKind.False:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.BooleanElement(false);");
                    break;

                default:
                    L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.NullElement();");
                    break;
            }

            return v;
        }

        private string EmitArrayLiteral(StringBuilder sb, STJ.JsonElement rule, string indent, string dataVar)
        {
            int count = rule.GetArrayLength();
            if (count == 0)
            {
                string v = NextVar();
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.EmptyArray();");
                return v;
            }

            string[] items = new string[count];
            int i = 0;
            foreach (STJ.JsonElement item in rule.EnumerateArray())
            {
                items[i++] = EmitExpression(sb, item, indent, dataVar);
            }

            string result = NextVar();
            L(sb, indent, $"JsonDocumentBuilder<JsonElement.Mutable> {result}_doc = JsonElement.CreateArrayBuilder(workspace, {count});");
            L(sb, indent, $"JsonElement.Mutable {result}_root = {result}_doc.RootElement;");
            for (int j = 0; j < items.Length; j++)
            {
                L(sb, indent, $"{result}_root.AddItem({items[j]});");
            }

            L(sb, indent, $"JsonElement {result} = (JsonElement){result}_root;");
            return result;
        }

        private string EmitFoldedConstant(StringBuilder sb, BigNumber value, string indent)
        {
            string v = NextVar();
            byte[] buf = new byte[128];
            if (value.TryFormat(buf.AsSpan(), out int written))
            {
                string numStr = System.Text.Encoding.UTF8.GetString(buf, 0, written);
                L(sb, indent, $"JsonElement {v} = JsonElement.ParseValue({Utf8Literal(numStr)});");
            }
            else
            {
                L(sb, indent, $"JsonElement {v} = JsonLogicHelpers.Zero();");
            }

            return v;
        }

        // ─── VAR ─────────────────────────────────────────────────
        private string EmitVar(StringBuilder sb, STJ.JsonElement args, string indent, string dataVar)
        {
            STJ.JsonElement pathArg;
            STJ.JsonElement? defaultArg = null;

            if (args.ValueKind == STJ.JsonValueKind.Array)
            {
                int count = args.GetArrayLength();
                if (count == 0)
                {
                    return dataVar;
                }

                pathArg = args[0];
                if (count >= 2)
                {
                    defaultArg = args[1];
                }
            }
            else
            {
                pathArg = args;
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.Null || pathArg.ValueKind == STJ.JsonValueKind.Undefined)
            {
                return dataVar;
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.String)
            {
                string? path = pathArg.GetString();
                if (string.IsNullOrEmpty(path))
                {
                    return dataVar;
                }

                return EmitStaticVarPath(sb, path!, defaultArg, indent, dataVar);
            }

            if (pathArg.ValueKind == STJ.JsonValueKind.Number)
            {
                return EmitStaticVarPath(sb, pathArg.GetRawText(), defaultArg, indent, dataVar);
            }

            // Computed path (object expression)
            return EmitDynamicVarPath(sb, pathArg, defaultArg, indent, dataVar);
        }

        private string EmitStaticVarPath(StringBuilder sb, string path, STJ.JsonElement? defaultArg, string indent, string dataVar)
        {
            string[] segments = path.Split('.');
            string result = NextVar();

            if (segments.Length == 1 && defaultArg is null)
            {
                string seg = segments[0];
                if (TryParseNonNegativeInt(seg, out int index))
                {
                    L(sb, indent, $"JsonElement {result};");
                    L(sb, indent, $"if ({dataVar}.ValueKind == JsonValueKind.Object && {dataVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {result}_p))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = {result}_p;");
                    L(sb, indent, "}");
                    L(sb, indent, $"else if ({dataVar}.ValueKind == JsonValueKind.Array && {index} < {dataVar}.GetArrayLength())");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = {dataVar}[{index}];");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
                    L(sb, indent, "}");
                }
                else
                {
                    L(sb, indent, $"JsonElement {result} = {dataVar}.ValueKind == JsonValueKind.Object && {dataVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {result}_p)");
                    L(sb, indent + "    ", $"? {result}_p : JsonLogicHelpers.NullElement();");
                }

                Blank(sb);
                return result;
            }

            // Multi-segment or with default
            L(sb, indent, $"JsonElement {result} = default;");
            string flagVar = result + "_ok";
            L(sb, indent, $"bool {flagVar} = true;");
            string curVar = result + "_cur";
            L(sb, indent, $"JsonElement {curVar} = {dataVar};");

            for (int i = 0; i < segments.Length; i++)
            {
                string seg = segments[i];
                if (TryParseNonNegativeInt(seg, out int index))
                {
                    string propVar = $"{result}_s{i}";
                    L(sb, indent, $"if ({flagVar} && {curVar}.ValueKind == JsonValueKind.Object && {curVar}.TryGetProperty({Utf8Literal(seg)}, out JsonElement {propVar}))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{curVar} = {propVar};");
                    L(sb, indent, "}");
                    L(sb, indent, $"else if ({flagVar} && {curVar}.ValueKind == JsonValueKind.Array && {index} >= 0 && {index} < {curVar}.GetArrayLength())");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{curVar} = {curVar}[{index}];");
                    L(sb, indent, "}");
                    L(sb, indent, "else");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{flagVar} = false;");
                    L(sb, indent, "}");
                }
                else
                {
                    L(sb, indent, $"if ({flagVar} && ({curVar}.ValueKind != JsonValueKind.Object || !{curVar}.TryGetProperty({Utf8Literal(seg)}, out {curVar})))");
                    L(sb, indent, "{");
                    L(sb, indent + "    ", $"{flagVar} = false;");
                    L(sb, indent, "}");
                }

                Blank(sb);
            }

            L(sb, indent, $"if ({flagVar})");
            L(sb, indent, "{");
            L(sb, indent + "    ", $"{result} = {curVar};");
            L(sb, indent, "}");
            L(sb, indent, "else");
            L(sb, indent, "{");
            if (defaultArg is not null)
            {
                string defVar = EmitExpression(sb, defaultArg.Value, indent + "    ", dataVar);
                L(sb, indent + "    ", $"{result} = {defVar};");
            }
            else
            {
                L(sb, indent + "    ", $"{result} = JsonLogicHelpers.NullElement();");
            }

            L(sb, indent, "}");
            Blank(sb);
            return result;
        }

