using System.Collections.Immutable;
using System.Text;
using Corvus.Text.Json.JsonPath;

namespace Corvus.Text.Json.JsonPath.CodeGeneration;

/// <summary>
/// Generates static C# evaluation code from a JSONPath (RFC 9535) expression string.
/// </summary>
/// <remarks>
/// <para>
/// The generator walks the JSONPath AST and emits inline C# code that calls
/// <c>JsonPathCodeGenHelpers</c> methods. The generated code uses
/// <see cref="System.Buffers.ArrayPool{T}"/> for intermediate node-list management
/// and supports all selector, filter, and function constructs defined by RFC 9535.
/// </para>
/// </remarks>
public static class JsonPathCodeGenerator
{
    private const string H = "JsonPathCodeGenHelpers";

    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JSONPath expression.
    /// </summary>
    /// <param name="expression">The JSONPath expression string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <param name="accessibility">The accessibility modifier (e.g. <c>"internal"</c> or <c>"public"</c>).</param>
    /// <param name="isPartial">Whether to emit the <c>partial</c> modifier on the class.</param>
    /// <returns>A complete C# source file as a string.</returns>
    /// <exception cref="JsonPathException">
    /// Thrown if <paramref name="expression"/> is not a valid JSONPath expression.
    /// </exception>
    public static string Generate(
        string expression,
        string className,
        string namespaceName,
        string accessibility = "internal",
        bool isPartial = false)
    {
        byte[] utf8 = Encoding.UTF8.GetBytes(expression);
        QueryNode ast = Parser.Parse(utf8);

        Emitter emitter = new();
        StringBuilder body = new(4096);

        emitter.EmitQuery(body, ast, "        ");

        StringBuilder sb = new(8192);
        L(sb, "", "// <auto-generated/>");
        L(sb, "", "#nullable enable");
        L(sb, "", "using System;");
        L(sb, "", "using System.Buffers;");

        if (emitter.RegexFields.Count > 0)
        {
            L(sb, "", "using System.Text.RegularExpressions;");
        }

        L(sb, "", "using Corvus.Text.Json;");
        L(sb, "", "using Corvus.Text.Json.JsonPath;");
        Blank(sb);
        L(sb, "", $"namespace {namespaceName};");
        Blank(sb);

        string partialKeyword = isPartial ? " partial" : string.Empty;
        L(sb, "", $"{accessibility} static{partialKeyword} class {className}");
        L(sb, "", "{");

        // Emit static literal fields
        if (emitter.StaticFields.Count > 0)
        {
            L(sb, "", "#pragma warning disable SA1306");
            L(sb, "", "#pragma warning disable SA1311");
            foreach ((string _, string declaration) in emitter.StaticFields)
            {
                L(sb, "    ", declaration);
            }

            L(sb, "", "#pragma warning restore SA1311");
            L(sb, "", "#pragma warning restore SA1306");
            Blank(sb);
        }

        // Emit static name fields
        foreach ((string _, string declaration) in emitter.NameFields)
        {
            L(sb, "    ", declaration);
        }

        if (emitter.NameFields.Count > 0)
        {
            Blank(sb);
        }

        // Emit static regex fields
        if (emitter.RegexFields.Count > 0)
        {
            foreach ((string _, string declaration) in emitter.RegexFields)
            {
                L(sb, "    ", declaration);
            }

            Blank(sb);
        }

        // Emit Evaluate method
        L(sb, "    ", "public static JsonElement Evaluate(in JsonElement data, JsonWorkspace workspace)");
        L(sb, "    ", "{");
        sb.Append(body);
        L(sb, "    ", "}");

        // Emit helper methods
        foreach (string helperMethod in emitter.HelperMethods)
        {
            Blank(sb);
            sb.Append(helperMethod);
        }

        // Close class — no trailing newline (SA1518)
        sb.Append('}');

        return sb.ToString();
    }

    private static void L(StringBuilder sb, string indent, string line)
    {
        sb.Append(indent).Append(line).Append('\n');
    }

    private static void Blank(StringBuilder sb)
    {
        sb.Append('\n');
    }

    private static string Utf8Literal(byte[] bytes)
    {
        StringBuilder sb = new(bytes.Length * 4 + 4);
        sb.Append("new byte[] { ");
        for (int i = 0; i < bytes.Length; i++)
        {
            if (i > 0)
            {
                sb.Append(", ");
            }

            sb.Append("0x");
            sb.Append(bytes[i].ToString("X2"));
        }

        sb.Append(" }");
        return sb.ToString();
    }

    private static string EscapeStringLiteral(string value)
    {
        StringBuilder sb = new(value.Length + 4);
        foreach (char ch in value)
        {
            switch (ch)
            {
                case '"': sb.Append("\\\""); break;
                case '\\': sb.Append("\\\\"); break;
                case '\n': sb.Append("\\n"); break;
                case '\r': sb.Append("\\r"); break;
                case '\t': sb.Append("\\t"); break;
                case '\0': sb.Append("\\0"); break;
                default:
                    if (ch < 0x20)
                    {
                        sb.Append($"\\u{(int)ch:X4}");
                    }
                    else
                    {
                        sb.Append(ch);
                    }

                    break;
            }
        }

        return sb.ToString();
    }

    private static byte[] LiteralToUtf8(string rawJson)
    {
        return Encoding.UTF8.GetBytes(rawJson);
    }

    /// <summary>
    /// Unescapes a JSON string literal (including surrounding double quotes)
    /// to produce the actual string value. Handles standard JSON escape sequences:
    /// <c>\\</c>, <c>\"</c>, <c>\/</c>, <c>\b</c>, <c>\f</c>, <c>\n</c>,
    /// <c>\r</c>, <c>\t</c>, and <c>\uXXXX</c>.
    /// </summary>
    private static string UnescapeJsonString(string rawJson)
    {
        // Strip surrounding double quotes
        string content = rawJson.Substring(1, rawJson.Length - 2);

        if (content.IndexOf('\\') < 0)
        {
            return content;
        }

        StringBuilder sb = new(content.Length);
        for (int i = 0; i < content.Length; i++)
        {
            if (content[i] == '\\' && i + 1 < content.Length)
            {
                char next = content[i + 1];
                switch (next)
                {
                    case '"': sb.Append('"'); i++; break;
                    case '\\': sb.Append('\\'); i++; break;
                    case '/': sb.Append('/'); i++; break;
                    case 'b': sb.Append('\b'); i++; break;
                    case 'f': sb.Append('\f'); i++; break;
                    case 'n': sb.Append('\n'); i++; break;
                    case 'r': sb.Append('\r'); i++; break;
                    case 't': sb.Append('\t'); i++; break;
                    case 'u':
                        if (i + 5 <= content.Length)
                        {
#if NETSTANDARD2_0
                            string hex = content.Substring(i + 2, 4);
#else
                            ReadOnlySpan<char> hex = content.AsSpan(i + 2, 4);
#endif
                            if (int.TryParse(hex, System.Globalization.NumberStyles.HexNumber, null, out int codePoint))
                            {
                                sb.Append((char)codePoint);
                                i += 5;
                            }
                            else
                            {
                                sb.Append(content[i]);
                            }
                        }
                        else
                        {
                            sb.Append(content[i]);
                        }

                        break;

                    default:
                        sb.Append(content[i]);
                        break;
                }
            }
            else
            {
                sb.Append(content[i]);
            }
        }

        return sb.ToString();
    }

    private static string TranslateIRegexpForCodeGen(string pattern)
    {
        const string iRegexpDot = @"(?:[^\r\n\uD800-\uDFFF]|[\uD800-\uDBFF][\uDC00-\uDFFF])";

        StringBuilder sb = new(pattern.Length * 2);
        for (int i = 0; i < pattern.Length; i++)
        {
            char c = pattern[i];
            if (c == '\\' && i + 1 < pattern.Length)
            {
                sb.Append(c);
                sb.Append(pattern[i + 1]);
                i++;
            }
            else if (c == '[')
            {
                sb.Append(c);
                i++;
                if (i < pattern.Length && pattern[i] == '^')
                {
                    sb.Append(pattern[i]);
                    i++;
                }

                if (i < pattern.Length && pattern[i] == ']')
                {
                    sb.Append(pattern[i]);
                    i++;
                }

                while (i < pattern.Length && pattern[i] != ']')
                {
                    if (pattern[i] == '\\' && i + 1 < pattern.Length)
                    {
                        sb.Append(pattern[i]);
                        sb.Append(pattern[i + 1]);
                        i += 2;
                    }
                    else
                    {
                        sb.Append(pattern[i]);
                        i++;
                    }
                }

                if (i < pattern.Length)
                {
                    sb.Append(pattern[i]);
                }
            }
            else if (c == '.')
            {
                sb.Append(iRegexpDot);
            }
            else
            {
                sb.Append(c);
            }
        }

        return sb.ToString();
    }

    /// <summary>
    /// Walks the JSONPath AST and emits C# code into a StringBuilder.
    /// </summary>
    private sealed class Emitter
    {
        private int varCounter;
        private int nameCounter;
        private int literalCounter;
        private int regexCounter;
        private int filterQueryCounter;

        public List<(string FieldName, string Declaration)> StaticFields { get; } = [];

        public List<(string FieldName, string Declaration)> NameFields { get; } = [];

        public List<(string FieldName, string Declaration)> RegexFields { get; } = [];

        public List<string> HelperMethods { get; } = [];

        private string NextVar() => $"v{this.varCounter++}";

        // ── Top-level query emission ─────────────────────────────────────
        public void EmitQuery(StringBuilder body, QueryNode query, string indent)
        {
            ImmutableArray<SegmentNode> segments = query.Segments;

            if (segments.Length == 0)
            {
                L(body, indent, $"return {H}.SingletonArray(data, workspace);");
                return;
            }

            // Find the first non-singleton segment
            int splitIdx = 0;
            while (splitIdx < segments.Length && IsSingletonSegment(segments[splitIdx]))
            {
                splitIdx++;
            }

            // Emit singleton prefix
            string currentVar = "data";
            for (int i = 0; i < splitIdx; i++)
            {
                currentVar = this.EmitSingletonLookup(
                    body, (ChildSegmentNode)segments[i], indent, currentVar);
                L(body, indent, $"if ({currentVar}.IsNullOrUndefined()) {{ return {H}.EmptyArrayElement; }}");
                Blank(body);
            }

            if (splitIdx == segments.Length)
            {
                // All segments are singleton — wrap result in array
                L(body, indent, $"return {H}.SingletonArray({currentVar}, workspace);");
                return;
            }

            // Transition to multi mode
            L(body, indent, "JsonElement[] currentNodes = ArrayPool<JsonElement>.Shared.Rent(16);");
            L(body, indent, "int currentCount = 0;");
            L(body, indent, "try");
            L(body, indent, "{");

            string tryIndent = indent + "    ";

            // First multi segment — applied to the last singleton variable
            this.EmitFirstMultiSegment(body, segments[splitIdx], tryIndent, currentVar);

            // Remaining multi segments
            for (int i = splitIdx + 1; i < segments.Length; i++)
            {
                Blank(body);
                this.EmitSubsequentMultiSegment(body, segments[i], tryIndent);
            }

            Blank(body);
            L(body, tryIndent, $"if (currentCount == 0) {{ return {H}.EmptyArrayElement; }}");
            Blank(body);
            L(body, tryIndent, $"return {H}.BuildArray(currentNodes, currentCount, workspace);");
            L(body, indent, "}");
            L(body, indent, "finally");
            L(body, indent, "{");
            L(body, indent, "    ArrayPool<JsonElement>.Shared.Return(currentNodes);");
            L(body, indent, "}");
        }

        // ── Singleton emission ───────────────────────────────────────────
        private static bool IsSingletonSegment(SegmentNode segment)
        {
            if (segment is not ChildSegmentNode)
            {
                return false;
            }

            if (segment.Selectors.Length != 1)
            {
                return false;
            }

            return segment.Selectors[0] is NameSelectorNode or IndexSelectorNode;
        }

        private string EmitSingletonLookup(
            StringBuilder body,
            ChildSegmentNode segment,
            string indent,
            string inputVar)
        {
            SelectorNode sel = segment.Selectors[0];
            string resultVar = this.NextVar();

            if (sel is NameSelectorNode name)
            {
                string nameField = this.EmitNameField(name.Name);
                L(body, indent, $"JsonElement {resultVar} = default;");
                L(body, indent, $"if ({inputVar}.ValueKind == JsonValueKind.Object)");
                L(body, indent, "{");
                L(body, indent, $"    {inputVar}.TryGetProperty({nameField}, out {resultVar});");
                L(body, indent, "}");
            }
            else if (sel is IndexSelectorNode idx)
            {
                L(body, indent, $"JsonElement {resultVar} = default;");
                L(body, indent, $"if ({inputVar}.ValueKind == JsonValueKind.Array)");
                L(body, indent, "{");
                L(body, indent, $"    int _len_{resultVar} = {inputVar}.GetArrayLength();");
                L(body, indent, $"    int _idx_{resultVar} = {H}.NormalizeIndex({idx.Index}L, _len_{resultVar});");
                L(body, indent, $"    if (_idx_{resultVar} >= 0 && _idx_{resultVar} < _len_{resultVar})");
                L(body, indent, "    {");
                L(body, indent, $"        {resultVar} = {inputVar}[_idx_{resultVar}];");
                L(body, indent, "    }");
                L(body, indent, "}");
            }

            return resultVar;
        }

        // ── Multi-mode emission ──────────────────────────────────────────
        private void EmitFirstMultiSegment(
            StringBuilder body,
            SegmentNode segment,
            string indent,
            string singletonVar)
        {
            if (segment is DescendantSegmentNode)
            {
                this.EmitFirstDescendantSegment(body, segment, indent, singletonVar);
            }
            else
            {
                // Child segment — apply selectors directly to the singleton var
                this.EmitSelectorsOnNode(
                    body, segment.Selectors, indent, singletonVar, "currentNodes", "currentCount");
            }
        }

        private void EmitFirstDescendantSegment(
            StringBuilder body,
            SegmentNode segment,
            string indent,
            string singletonVar)
        {
            string descVar = $"_desc{this.varCounter++}";
            string descCountVar = $"_descCount{this.varCounter++}";
            L(body, indent, $"JsonElement[] {descVar} = ArrayPool<JsonElement>.Shared.Rent(64);");
            L(body, indent, $"int {descCountVar} = 0;");
            L(body, indent, "try");
            L(body, indent, "{");

            string tryIndent = indent + "    ";
            L(body, tryIndent, $"{H}.CollectDescendantsAndSelf({singletonVar}, ref {descVar}, ref {descCountVar});");

            string diVar = $"_di{this.varCounter++}";
            string dNodeVar = $"_dNode{this.varCounter++}";
            L(body, tryIndent, $"for (int {diVar} = 0; {diVar} < {descCountVar}; {diVar}++)");
            L(body, tryIndent, "{");

            string innerIndent = tryIndent + "    ";
            L(body, innerIndent, $"JsonElement {dNodeVar} = {descVar}[{diVar}];");
            this.EmitSelectorsOnNode(
                body, segment.Selectors, innerIndent, dNodeVar, "currentNodes", "currentCount");

            L(body, tryIndent, "}");

            L(body, indent, "}");
            L(body, indent, "finally");
            L(body, indent, "{");
            L(body, indent, $"    ArrayPool<JsonElement>.Shared.Return({descVar});");
            L(body, indent, "}");
        }

        private void EmitSubsequentMultiSegment(
            StringBuilder body,
            SegmentNode segment,
            string indent)
        {
            L(body, indent, "{");
            string blockIndent = indent + "    ";

            L(body, blockIndent, "JsonElement[] nextNodes = ArrayPool<JsonElement>.Shared.Rent(Math.Max(currentCount, 16));");
            L(body, blockIndent, "int nextCount = 0;");

            if (segment is DescendantSegmentNode)
            {
                string siVar = $"_si{this.varCounter++}";
                L(body, blockIndent, $"for (int {siVar} = 0; {siVar} < currentCount; {siVar}++)");
                L(body, blockIndent, "{");

                string loopIndent = blockIndent + "    ";
                string descVar = $"_desc{this.varCounter++}";
                string descCountVar = $"_descCount{this.varCounter++}";
                L(body, loopIndent, $"JsonElement[] {descVar} = ArrayPool<JsonElement>.Shared.Rent(64);");
                L(body, loopIndent, $"int {descCountVar} = 0;");
                L(body, loopIndent, "try");
                L(body, loopIndent, "{");

                string tryIndent = loopIndent + "    ";
                L(body, tryIndent, $"{H}.CollectDescendantsAndSelf(currentNodes[{siVar}], ref {descVar}, ref {descCountVar});");

                string diVar = $"_di{this.varCounter++}";
                string dNodeVar = $"_dNode{this.varCounter++}";
                L(body, tryIndent, $"for (int {diVar} = 0; {diVar} < {descCountVar}; {diVar}++)");
                L(body, tryIndent, "{");

                string descInner = tryIndent + "    ";
                L(body, descInner, $"JsonElement {dNodeVar} = {descVar}[{diVar}];");
                this.EmitSelectorsOnNode(
                    body, segment.Selectors, descInner, dNodeVar, "nextNodes", "nextCount");

                L(body, tryIndent, "}");

                L(body, loopIndent, "}");
                L(body, loopIndent, "finally");
                L(body, loopIndent, "{");
                L(body, loopIndent, $"    ArrayPool<JsonElement>.Shared.Return({descVar});");
                L(body, loopIndent, "}");

                L(body, blockIndent, "}");
            }
            else
            {
                string siVar = $"_si{this.varCounter++}";
                L(body, blockIndent, $"for (int {siVar} = 0; {siVar} < currentCount; {siVar}++)");
                L(body, blockIndent, "{");

                string loopIndent = blockIndent + "    ";
                string nodeVar = $"_node{this.varCounter++}";
                L(body, loopIndent, $"JsonElement {nodeVar} = currentNodes[{siVar}];");
                this.EmitSelectorsOnNode(
                    body, segment.Selectors, loopIndent, nodeVar, "nextNodes", "nextCount");

                L(body, blockIndent, "}");
            }

            Blank(body);
            L(body, blockIndent, "ArrayPool<JsonElement>.Shared.Return(currentNodes);");
            L(body, blockIndent, "currentNodes = nextNodes;");
            L(body, blockIndent, "currentCount = nextCount;");

            L(body, indent, "}");
        }

        // ── Selector emission ────────────────────────────────────────────
        private void EmitSelectorsOnNode(
            StringBuilder body,
            ImmutableArray<SelectorNode> selectors,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            for (int i = 0; i < selectors.Length; i++)
            {
                this.EmitSelector(body, selectors[i], indent, nodeVar, outArray, outCount);
            }
        }

        private void EmitSelector(
            StringBuilder body,
            SelectorNode selector,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            switch (selector)
            {
                case NameSelectorNode name:
                    this.EmitNameSelector(body, name, indent, nodeVar, outArray, outCount);
                    break;
                case IndexSelectorNode idx:
                    this.EmitIndexSelector(body, idx, indent, nodeVar, outArray, outCount);
                    break;
                case WildcardSelectorNode:
                    this.EmitWildcardSelector(body, indent, nodeVar, outArray, outCount);
                    break;
                case SliceSelectorNode slice:
                    this.EmitSliceSelector(body, slice, indent, nodeVar, outArray, outCount);
                    break;
                case FilterSelectorNode filter:
                    this.EmitFilterSelector(body, filter, indent, nodeVar, outArray, outCount);
                    break;
            }
        }

        private void EmitNameSelector(
            StringBuilder body,
            NameSelectorNode name,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            string nameField = this.EmitNameField(name.Name);
            string propVar = $"_prop{this.varCounter++}";
            L(body, indent, $"if ({nodeVar}.ValueKind == JsonValueKind.Object)");
            L(body, indent, "{");
            L(body, indent, $"    if ({nodeVar}.TryGetProperty({nameField}, out JsonElement {propVar}))");
            L(body, indent, "    {");
            L(body, indent, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, indent, $"        {outArray}[{outCount}++] = {propVar};");
            L(body, indent, "    }");
            L(body, indent, "}");
        }

        private void EmitIndexSelector(
            StringBuilder body,
            IndexSelectorNode idx,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            string c = this.varCounter++.ToString();
            L(body, indent, $"if ({nodeVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");
            L(body, indent, $"    int _len{c} = {nodeVar}.GetArrayLength();");
            L(body, indent, $"    int _idx{c} = {H}.NormalizeIndex({idx.Index}L, _len{c});");
            L(body, indent, $"    if (_idx{c} >= 0 && _idx{c} < _len{c})");
            L(body, indent, "    {");
            L(body, indent, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, indent, $"        {outArray}[{outCount}++] = {nodeVar}[_idx{c}];");
            L(body, indent, "    }");
            L(body, indent, "}");
        }

        private void EmitWildcardSelector(
            StringBuilder body,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            string c = this.varCounter++.ToString();
            L(body, indent, $"if ({nodeVar}.ValueKind == JsonValueKind.Object)");
            L(body, indent, "{");
            L(body, indent, $"    foreach (var _wp{c} in {nodeVar}.EnumerateObject())");
            L(body, indent, "    {");
            L(body, indent, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, indent, $"        {outArray}[{outCount}++] = _wp{c}.Value;");
            L(body, indent, "    }");
            L(body, indent, "}");
            L(body, indent, $"else if ({nodeVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");
            L(body, indent, $"    foreach (JsonElement _wi{c} in {nodeVar}.EnumerateArray())");
            L(body, indent, "    {");
            L(body, indent, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, indent, $"        {outArray}[{outCount}++] = _wi{c};");
            L(body, indent, "    }");
            L(body, indent, "}");
        }

        private void EmitSliceSelector(
            StringBuilder body,
            SliceSelectorNode slice,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            string c = this.varCounter++.ToString();
            string startExpr = slice.Start.HasValue ? $"{slice.Start.Value}L" : "null";
            string endExpr = slice.End.HasValue ? $"{slice.End.Value}L" : "null";
            string stepExpr = slice.Step.HasValue ? $"{slice.Step.Value}L" : "null";

            L(body, indent, $"if ({nodeVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");

            string ii = indent + "    ";
            L(body, ii, $"int _sLen{c} = {nodeVar}.GetArrayLength();");
            L(body, ii, $"var _slice{c} = {H}.NormalizeSlice({startExpr}, {endExpr}, {stepExpr}, _sLen{c});");
            L(body, ii, $"if (_slice{c}.Step > 0)");
            L(body, ii, "{");
            L(body, ii, $"    for (int _sI{c} = _slice{c}.Start; _sI{c} < _slice{c}.End; _sI{c} += _slice{c}.Step)");
            L(body, ii, "    {");
            L(body, ii, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, ii, $"        {outArray}[{outCount}++] = {nodeVar}[_sI{c}];");
            L(body, ii, "    }");
            L(body, ii, "}");
            L(body, ii, $"else if (_slice{c}.Step < 0)");
            L(body, ii, "{");
            L(body, ii, $"    for (int _sI{c} = _slice{c}.Start; _sI{c} > _slice{c}.End; _sI{c} += _slice{c}.Step)");
            L(body, ii, "    {");
            L(body, ii, $"        if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, ii, $"        {outArray}[{outCount}++] = {nodeVar}[_sI{c}];");
            L(body, ii, "    }");
            L(body, ii, "}");

            L(body, indent, "}");
        }

        private void EmitFilterSelector(
            StringBuilder body,
            FilterSelectorNode filter,
            string indent,
            string nodeVar,
            string outArray,
            string outCount)
        {
            string c = this.varCounter++.ToString();

            // Array case
            L(body, indent, $"if ({nodeVar}.ValueKind == JsonValueKind.Array)");
            L(body, indent, "{");
            string itemVar = $"_fi{c}";
            L(body, indent, $"    foreach (JsonElement {itemVar} in {nodeVar}.EnumerateArray())");
            L(body, indent, "    {");

            string filterIndent = indent + "        ";
            string boolVar = this.EmitFilterBool(body, filter.Expression, filterIndent, itemVar);
            L(body, filterIndent, $"if ({boolVar})");
            L(body, filterIndent, "{");
            L(body, filterIndent, $"    if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, filterIndent, $"    {outArray}[{outCount}++] = {itemVar};");
            L(body, filterIndent, "}");

            L(body, indent, "    }");
            L(body, indent, "}");

            // Object case
            L(body, indent, $"else if ({nodeVar}.ValueKind == JsonValueKind.Object)");
            L(body, indent, "{");
            string propVar = $"_fp{c}";
            L(body, indent, $"    foreach (var {propVar} in {nodeVar}.EnumerateObject())");
            L(body, indent, "    {");

            string boolVar2 = this.EmitFilterBool(body, filter.Expression, filterIndent, $"{propVar}.Value");
            L(body, filterIndent, $"if ({boolVar2})");
            L(body, filterIndent, "{");
            L(body, filterIndent, $"    if ({outCount} == {outArray}.Length) {{ {H}.Grow(ref {outArray}); }}");
            L(body, filterIndent, $"    {outArray}[{outCount}++] = {propVar}.Value;");
            L(body, filterIndent, "}");

            L(body, indent, "    }");
            L(body, indent, "}");
        }

        // ── Filter expression emission (bool context) ────────────────────
        private string EmitFilterBool(
            StringBuilder body,
            FilterExpressionNode expr,
            string indent,
            string currentVar)
        {
            switch (expr)
            {
                case ComparisonNode cmp:
                    return this.EmitComparison(body, cmp, indent, currentVar);
                case LogicalAndNode and:
                    return this.EmitLogicalAnd(body, and, indent, currentVar);
                case LogicalOrNode or:
                    return this.EmitLogicalOr(body, or, indent, currentVar);
                case LogicalNotNode not:
                    return this.EmitLogicalNot(body, not, indent, currentVar);
                case FilterQueryNode query:
                    return this.EmitFilterQueryAsTest(body, query, indent, currentVar);
                case FunctionCallNode func when func.Name == "match" || func.Name == "search":
                    return this.EmitMatchOrSearch(body, func, indent, currentVar);
                case ParenExpressionNode paren:
                    return this.EmitFilterBool(body, paren.Inner, indent, currentVar);
                default:
                    // Fallback: treat as existence (non-null = true)
                    string valVar = this.EmitFilterValue(body, expr, indent, currentVar);
                    string boolVar = this.NextVar();
                    L(body, indent, $"bool {boolVar} = {valVar}.ValueKind != JsonValueKind.Undefined;");
                    return boolVar;
            }
        }

        private string EmitComparison(
            StringBuilder body,
            ComparisonNode cmp,
            string indent,
            string currentVar)
        {
            string leftVar = this.EmitFilterValue(body, cmp.Left, indent, currentVar);
            string rightVar = this.EmitFilterValue(body, cmp.Right, indent, currentVar);
            int opInt = (int)cmp.Op;
            string resultVar = this.NextVar();
            L(body, indent, $"bool {resultVar} = {H}.CompareValues({leftVar}, {rightVar}, {opInt});");
            return resultVar;
        }

        private string EmitLogicalAnd(
            StringBuilder body,
            LogicalAndNode and,
            string indent,
            string currentVar)
        {
            string resultVar = this.NextVar();
            L(body, indent, $"bool {resultVar} = false;");
            L(body, indent, "{");

            string innerIndent = indent + "    ";
            string leftVar = this.EmitFilterBool(body, and.Left, innerIndent, currentVar);
            L(body, innerIndent, $"if ({leftVar})");
            L(body, innerIndent, "{");

            string rightIndent = innerIndent + "    ";
            string rightVar = this.EmitFilterBool(body, and.Right, rightIndent, currentVar);
            L(body, rightIndent, $"{resultVar} = {rightVar};");

            L(body, innerIndent, "}");
            L(body, indent, "}");

            return resultVar;
        }

        private string EmitLogicalOr(
            StringBuilder body,
            LogicalOrNode or,
            string indent,
            string currentVar)
        {
            string resultVar = this.NextVar();
            L(body, indent, $"bool {resultVar} = true;");
            L(body, indent, "{");

            string innerIndent = indent + "    ";
            string leftVar = this.EmitFilterBool(body, or.Left, innerIndent, currentVar);
            L(body, innerIndent, $"if (!{leftVar})");
            L(body, innerIndent, "{");

            string rightIndent = innerIndent + "    ";
            string rightVar = this.EmitFilterBool(body, or.Right, rightIndent, currentVar);
            L(body, rightIndent, $"{resultVar} = {rightVar};");

            L(body, innerIndent, "}");
            L(body, indent, "}");

            return resultVar;
        }

        private string EmitLogicalNot(
            StringBuilder body,
            LogicalNotNode not,
            string indent,
            string currentVar)
        {
            string operandVar = this.EmitFilterBool(body, not.Operand, indent, currentVar);
            string resultVar = this.NextVar();
            L(body, indent, $"bool {resultVar} = !{operandVar};");
            return resultVar;
        }

        private string EmitFilterQueryAsTest(
            StringBuilder body,
            FilterQueryNode query,
            string indent,
            string currentVar)
        {
            if (IsSingularQuery(query))
            {
                // Singular: existence test — check if the final value is defined
                string source = query.IsRelative ? currentVar : "data";
                string valVar = this.EmitSingularQueryChain(body, query.Segments, indent, source);
                string resultVar = this.NextVar();
                L(body, indent, $"bool {resultVar} = !{valVar}.IsUndefined();");
                return resultVar;
            }
            else
            {
                // Non-singular: evaluate as node list, check length > 0
                string nodesVar = this.EmitFilterQueryAsNodes(body, query, indent, currentVar);
                string resultVar = this.NextVar();
                L(body, indent, $"bool {resultVar} = !{nodesVar}.IsNullOrUndefined() && {nodesVar}.GetArrayLength() > 0;");
                return resultVar;
            }
        }

        // ── Filter expression emission (value/comparable context) ────────
        private string EmitFilterValue(
            StringBuilder body,
            FilterExpressionNode expr,
            string indent,
            string currentVar)
        {
            switch (expr)
            {
                case FilterQueryNode query:
                    return this.EmitFilterQueryAsValue(body, query, indent, currentVar);
                case LiteralNode lit:
                    return this.EmitLiteral(body, lit, indent);
                case FunctionCallNode func:
                    return this.EmitFunctionValue(body, func, indent, currentVar);
                case ParenExpressionNode paren:
                    return this.EmitFilterValue(body, paren.Inner, indent, currentVar);
                default:
                    // Shouldn't happen if parser validated types; return Undefined
                    string v = this.NextVar();
                    L(body, indent, $"JsonElement {v} = default;");
                    return v;
            }
        }

        private string EmitLiteral(
            StringBuilder body,
            LiteralNode lit,
            string indent)
        {
            string fieldRef = this.EmitLiteralFieldRef(lit);
            string resultVar = this.NextVar();
            L(body, indent, $"JsonElement {resultVar} = {fieldRef};");
            return resultVar;
        }

        private string EmitFilterQueryAsValue(
            StringBuilder body,
            FilterQueryNode query,
            string indent,
            string currentVar)
        {
            if (IsSingularQuery(query))
            {
                string source = query.IsRelative ? currentVar : "data";
                return this.EmitSingularQueryChain(body, query.Segments, indent, source);
            }
            else
            {
                // Non-singular as comparable: evaluate as nodes, unwrap with ValueOfNodes
                string nodesVar = this.EmitFilterQueryAsNodes(body, query, indent, currentVar);
                string resultVar = this.NextVar();
                L(body, indent, $"JsonElement {resultVar} = {H}.ValueOfNodes({nodesVar});");
                return resultVar;
            }
        }

        private string EmitFilterQueryAsNodes(
            StringBuilder body,
            FilterQueryNode query,
            string indent,
            string currentVar)
        {
            // Generate a helper method for the non-singular query
            string methodName = $"FilterQuery{this.filterQueryCounter++}";
            this.GenerateFilterQueryHelper(query, methodName);

            string source = query.IsRelative ? currentVar : "data";
            string resultVar = this.NextVar();
            L(body, indent, $"JsonElement {resultVar} = {methodName}({source}, data, workspace);");
            return resultVar;
        }

        // ── Function emission ────────────────────────────────────────────
        private string EmitFunctionValue(
            StringBuilder body,
            FunctionCallNode func,
            string indent,
            string currentVar)
        {
            switch (func.Name)
            {
                case "length":
                    return this.EmitLengthFunction(body, func, indent, currentVar);
                case "count":
                    return this.EmitCountFunction(body, func, indent, currentVar);
                case "value":
                    return this.EmitValueFunction(body, func, indent, currentVar);
                default:
                    // Unknown function — return Undefined
                    string v = this.NextVar();
                    L(body, indent, $"JsonElement {v} = default;");
                    return v;
            }
        }

        private string EmitLengthFunction(
            StringBuilder body,
            FunctionCallNode func,
            string indent,
            string currentVar)
        {
            string argVar = this.EmitFilterValue(body, func.Arguments[0], indent, currentVar);
            string lenVar = this.NextVar();
            string resultVar = this.NextVar();
            L(body, indent, $"int {lenVar} = {H}.LengthValue({argVar});");
            L(body, indent, $"JsonElement {resultVar} = {lenVar} >= 0 ? {H}.IntToElement({lenVar}) : default;");
            return resultVar;
        }

        private string EmitCountFunction(
            StringBuilder body,
            FunctionCallNode func,
            string indent,
            string currentVar)
        {
            // Argument must be a filter query (NodesType)
            string nodesVar;
            if (func.Arguments[0] is FilterQueryNode fq)
            {
                nodesVar = this.EmitFilterQueryAsNodes(body, fq, indent, currentVar);
            }
            else
            {
                nodesVar = this.EmitFilterValue(body, func.Arguments[0], indent, currentVar);
            }

            string countVar = this.NextVar();
            string resultVar = this.NextVar();
            L(body, indent, $"int {countVar} = {H}.CountNodes({nodesVar});");
            L(body, indent, $"JsonElement {resultVar} = {H}.IntToElement({countVar});");
            return resultVar;
        }

        private string EmitValueFunction(
            StringBuilder body,
            FunctionCallNode func,
            string indent,
            string currentVar)
        {
            string nodesVar;
            if (func.Arguments[0] is FilterQueryNode fq)
            {
                nodesVar = this.EmitFilterQueryAsNodes(body, fq, indent, currentVar);
            }
            else
            {
                nodesVar = this.EmitFilterValue(body, func.Arguments[0], indent, currentVar);
            }

            string resultVar = this.NextVar();
            L(body, indent, $"JsonElement {resultVar} = {H}.ValueOfNodes({nodesVar});");
            return resultVar;
        }

        private string EmitMatchOrSearch(
            StringBuilder body,
            FunctionCallNode func,
            string indent,
            string currentVar)
        {
            bool isMatch = func.Name == "match";
            string strVar = this.EmitFilterValue(body, func.Arguments[0], indent, currentVar);
            string resultVar = this.NextVar();
            L(body, indent, $"bool {resultVar} = false;");

            // Check if pattern is a literal string — pre-compile regex
            if (func.Arguments[1] is LiteralNode patLit &&
                patLit.Kind == LiteralKind.String)
            {
                // Unescape the JSON string literal to get the actual pattern value
                string patternStr = UnescapeJsonString(patLit.RawJson);
                string regexField = this.EmitRegexField(patternStr, isMatch);

                L(body, indent, $"if ({strVar}.ValueKind == JsonValueKind.String)");
                L(body, indent, "{");
                L(body, indent, $"    {resultVar} = {regexField}.IsMatch({strVar}.GetString()!);");
                L(body, indent, "}");
            }
            else
            {
                // Dynamic pattern — runtime regex creation
                string patVar = this.EmitFilterValue(body, func.Arguments[1], indent, currentVar);
                L(body, indent, $"if ({strVar}.ValueKind == JsonValueKind.String && {patVar}.ValueKind == JsonValueKind.String)");
                L(body, indent, "{");
                L(body, indent, "    try");
                L(body, indent, "    {");

                string anchor = isMatch ? "\"^(?:\" + " : "\"\"+ ";
                string anchorEnd = isMatch ? " + \")$\"" : string.Empty;
                L(body, indent, $"        string _dp{this.varCounter} = {H}.TranslateIRegexp({patVar}.GetString()!);");
                L(body, indent, $"        var _re{this.varCounter} = new System.Text.RegularExpressions.Regex({anchor}_dp{this.varCounter}{anchorEnd}, System.Text.RegularExpressions.RegexOptions.None, System.TimeSpan.FromSeconds(1));");
                L(body, indent, $"        {resultVar} = _re{this.varCounter}.IsMatch({strVar}.GetString()!);");
                this.varCounter++;

                L(body, indent, "    }");
                L(body, indent, "    catch (System.ArgumentException)");
                L(body, indent, "    {");
                L(body, indent, "    }");
                L(body, indent, "}");
            }

            return resultVar;
        }

        // ── Singular query chain ─────────────────────────────────────────
        private static bool IsSingularQuery(FilterQueryNode query)
        {
            foreach (SegmentNode seg in query.Segments)
            {
                if (seg is not ChildSegmentNode)
                {
                    return false;
                }

                if (seg.Selectors.Length != 1)
                {
                    return false;
                }

                if (seg.Selectors[0] is not (NameSelectorNode or IndexSelectorNode))
                {
                    return false;
                }
            }

            return true;
        }

        private string EmitSingularQueryChain(
            StringBuilder body,
            ImmutableArray<SegmentNode> segments,
            string indent,
            string source)
        {
            string currentVar = source;
            for (int i = 0; i < segments.Length; i++)
            {
                SelectorNode sel = segments[i].Selectors[0];
                string resultVar = this.NextVar();

                if (sel is NameSelectorNode name)
                {
                    string nameField = this.EmitNameField(name.Name);
                    L(body, indent, $"JsonElement {resultVar} = default;");
                    L(body, indent, $"if ({currentVar}.ValueKind == JsonValueKind.Object) {{ {currentVar}.TryGetProperty({nameField}, out {resultVar}); }}");
                }
                else if (sel is IndexSelectorNode idx)
                {
                    L(body, indent, $"JsonElement {resultVar} = default;");
                    L(body, indent, $"if ({currentVar}.ValueKind == JsonValueKind.Array)");
                    L(body, indent, "{");
                    L(body, indent, $"    int _qLen{resultVar} = {currentVar}.GetArrayLength();");
                    L(body, indent, $"    int _qIdx{resultVar} = {H}.NormalizeIndex({idx.Index}L, _qLen{resultVar});");
                    L(body, indent, $"    if (_qIdx{resultVar} >= 0 && _qIdx{resultVar} < _qLen{resultVar}) {{ {resultVar} = {currentVar}[_qIdx{resultVar}]; }}");
                    L(body, indent, "}");
                }

                currentVar = resultVar;
            }

            return currentVar;
        }

        // ── Filter query helper method generation ────────────────────────
        private void GenerateFilterQueryHelper(FilterQueryNode query, string methodName)
        {
            StringBuilder mb = new(2048);
            const string methodIndent = "    ";

            L(mb, methodIndent, $"private static JsonElement {methodName}(in JsonElement source, in JsonElement root, JsonWorkspace workspace)");
            L(mb, methodIndent, "{");

            const string bodyIndent = methodIndent + "    ";

            if (query.Segments.Length == 0)
            {
                L(mb, bodyIndent, $"return {H}.SingletonArray(source, workspace);");
                L(mb, methodIndent, "}");
                this.HelperMethods.Add(mb.ToString());
                return;
            }

            // Always use multi mode for helper methods
            L(mb, bodyIndent, "JsonElement[] currentNodes = ArrayPool<JsonElement>.Shared.Rent(16);");
            L(mb, bodyIndent, "int currentCount = 0;");
            L(mb, bodyIndent, "try");
            L(mb, bodyIndent, "{");

            const string tryIndent = bodyIndent + "    ";
            L(mb, tryIndent, "currentNodes[0] = source;");
            L(mb, tryIndent, "currentCount = 1;");
            Blank(mb);

            // Each segment transforms currentNodes → nextNodes
            for (int i = 0; i < query.Segments.Length; i++)
            {
                this.EmitHelperSegment(mb, query.Segments[i], tryIndent);
                if (i < query.Segments.Length - 1)
                {
                    Blank(mb);
                }
            }

            Blank(mb);
            L(mb, tryIndent, $"if (currentCount == 0) {{ return {H}.EmptyArrayElement; }}");
            L(mb, tryIndent, $"return {H}.BuildArray(currentNodes, currentCount, workspace);");

            L(mb, bodyIndent, "}");
            L(mb, bodyIndent, "finally");
            L(mb, bodyIndent, "{");
            L(mb, bodyIndent, "    ArrayPool<JsonElement>.Shared.Return(currentNodes);");
            L(mb, bodyIndent, "}");

            L(mb, methodIndent, "}");
            this.HelperMethods.Add(mb.ToString());
        }

        private void EmitHelperSegment(
            StringBuilder body,
            SegmentNode segment,
            string indent)
        {
            L(body, indent, "{");
            string blockIndent = indent + "    ";

            L(body, blockIndent, "JsonElement[] nextNodes = ArrayPool<JsonElement>.Shared.Rent(Math.Max(currentCount, 16));");
            L(body, blockIndent, "int nextCount = 0;");

            if (segment is DescendantSegmentNode)
            {
                string siVar = $"_hsi{this.varCounter++}";
                L(body, blockIndent, $"for (int {siVar} = 0; {siVar} < currentCount; {siVar}++)");
                L(body, blockIndent, "{");

                string loopIndent = blockIndent + "    ";
                string descVar = $"_hdesc{this.varCounter++}";
                string descCountVar = $"_hdescC{this.varCounter++}";
                L(body, loopIndent, $"JsonElement[] {descVar} = ArrayPool<JsonElement>.Shared.Rent(64);");
                L(body, loopIndent, $"int {descCountVar} = 0;");
                L(body, loopIndent, "try");
                L(body, loopIndent, "{");

                string tryIndent = loopIndent + "    ";
                L(body, tryIndent, $"{H}.CollectDescendantsAndSelf(currentNodes[{siVar}], ref {descVar}, ref {descCountVar});");

                string diVar = $"_hdi{this.varCounter++}";
                string dNodeVar = $"_hdNode{this.varCounter++}";
                L(body, tryIndent, $"for (int {diVar} = 0; {diVar} < {descCountVar}; {diVar}++)");
                L(body, tryIndent, "{");

                string descInner = tryIndent + "    ";
                L(body, descInner, $"JsonElement {dNodeVar} = {descVar}[{diVar}];");
                this.EmitSelectorsOnNode(
                    body, segment.Selectors, descInner, dNodeVar, "nextNodes", "nextCount");

                L(body, tryIndent, "}");

                L(body, loopIndent, "}");
                L(body, loopIndent, "finally");
                L(body, loopIndent, "{");
                L(body, loopIndent, $"    ArrayPool<JsonElement>.Shared.Return({descVar});");
                L(body, loopIndent, "}");

                L(body, blockIndent, "}");
            }
            else
            {
                string siVar = $"_hsi{this.varCounter++}";
                L(body, blockIndent, $"for (int {siVar} = 0; {siVar} < currentCount; {siVar}++)");
                L(body, blockIndent, "{");

                string loopIndent = blockIndent + "    ";
                string nodeVar = $"_hNode{this.varCounter++}";
                L(body, loopIndent, $"JsonElement {nodeVar} = currentNodes[{siVar}];");
                this.EmitSelectorsOnNode(
                    body, segment.Selectors, loopIndent, nodeVar, "nextNodes", "nextCount");

                L(body, blockIndent, "}");
            }

            Blank(body);
            L(body, blockIndent, "ArrayPool<JsonElement>.Shared.Return(currentNodes);");
            L(body, blockIndent, "currentNodes = nextNodes;");
            L(body, blockIndent, "currentCount = nextCount;");

            L(body, indent, "}");
        }

        // ── Field emission helpers ───────────────────────────────────────
        private string EmitNameField(byte[] name)
        {
            string fieldName = $"s_name{this.nameCounter++}";
            this.NameFields.Add((fieldName, $"private static readonly byte[] {fieldName} = {Utf8Literal(name)};"));
            return fieldName;
        }

        private string EmitLiteralFieldRef(LiteralNode lit)
        {
            switch (lit.Kind)
            {
                case LiteralKind.Null:
                    return $"{H}.NullElement";
                case LiteralKind.True:
                    return $"{H}.TrueElement";
                case LiteralKind.False:
                    return $"{H}.FalseElement";
                default:
                    string fieldName = $"s_literal{this.literalCounter++}";
                    byte[] utf8 = LiteralToUtf8(lit.RawJson);
                    this.StaticFields.Add((fieldName, $"private static readonly JsonElement {fieldName} = JsonElement.ParseValue({Utf8Literal(utf8)});"));
                    return fieldName;
            }
        }

        private string EmitRegexField(string pattern, bool fullMatch)
        {
            string fieldName = $"s_regex{this.regexCounter++}";
            string translated = TranslateIRegexpForCodeGen(pattern);
            string escaped;

            if (fullMatch)
            {
                escaped = EscapeStringLiteral("^(?:" + translated + ")$");
            }
            else
            {
                escaped = EscapeStringLiteral(translated);
            }

            this.RegexFields.Add((
                fieldName,
                $"private static readonly System.Text.RegularExpressions.Regex {fieldName} = new System.Text.RegularExpressions.Regex(\"{escaped}\", System.Text.RegularExpressions.RegexOptions.None, System.TimeSpan.FromSeconds(1));"));
            return fieldName;
        }
