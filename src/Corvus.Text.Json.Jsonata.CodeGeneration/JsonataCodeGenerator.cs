using System.Text;

namespace Corvus.Text.Json.Jsonata.CodeGeneration;

/// <summary>
/// Generates static C# evaluation code from a JSONata expression string.
/// </summary>
/// <remarks>
/// <para>
/// The generated code uses a shared <see cref="Corvus.Text.Json.Jsonata.JsonataEvaluator"/>
/// instance that caches the compiled expression delegate after the first evaluation.
/// Subsequent calls bypass parsing and compilation entirely, invoking the cached
/// delegate directly. This gives the same steady-state performance as the runtime
/// evaluator with zero per-call compilation overhead.
/// </para>
/// <para>
/// Because the generated code delegates to the full runtime evaluator, it supports
/// the complete JSONata language — including lambdas, closures, recursion, higher-order
/// functions, and all built-in functions — without any restrictions.
/// </para>
/// </remarks>
public static class JsonataCodeGenerator
{
    /// <summary>
    /// Generates a complete C# source file containing a static class with an
    /// <c>Evaluate</c> method that evaluates the given JSONata expression.
    /// </summary>
    /// <param name="expression">The JSONata expression string.</param>
    /// <param name="className">The name of the generated static class.</param>
    /// <param name="namespaceName">The namespace for the generated class.</param>
    /// <returns>A complete C# source file as a string.</returns>
    /// <exception cref="JsonataException">
    /// Thrown if <paramref name="expression"/> is not a valid JSONata expression.
    /// The expression is parsed at generation time to catch syntax errors early.
    /// </exception>
    public static string Generate(string expression, string className, string namespaceName)
    {
        // Validate by parsing — this throws JsonataException if the expression is invalid.
        // We don't use the AST; this is purely a syntax check.
        Parser.Parse(expression);

        StringBuilder sb = new(4096);

        // File header
        AppendLine(sb, string.Empty, "// <auto-generated/>");
        AppendLine(sb, string.Empty, "#nullable enable");
        AppendLine(sb, string.Empty, "using System.Runtime.CompilerServices;");
        AppendLine(sb, string.Empty, "using Corvus.Text.Json;");
        AppendLine(sb, string.Empty, "using Corvus.Text.Json.Jsonata;");
        AppendBlank(sb);

        if (!string.IsNullOrEmpty(namespaceName))
        {
            AppendLine(sb, string.Empty, $"namespace {namespaceName};");
            AppendBlank(sb);
        }

        AppendLine(sb, string.Empty, $"internal static class {className}");
        AppendLine(sb, string.Empty, "{");

        // Emit the expression as a string constant and a shared evaluator instance.
        // The evaluator caches the compiled delegate after first use.
        string escapedExpression = EscapeStringLiteral(expression);
        AppendLine(sb, "    ", $"private const string Expression = \"{escapedExpression}\";");
        AppendLine(sb, "    ", "private static readonly JsonataEvaluator Evaluator = new();");
        AppendBlank(sb);

        // Emit the simple Evaluate overload (data + workspace)
        AppendLine(sb, "    ", "/// <summary>");
        AppendLine(sb, "    ", $"/// Evaluates the JSONata expression: <c>{EscapeXmlContent(expression)}</c>.");
        AppendLine(sb, "    ", "/// </summary>");
        AppendLine(sb, "    ", "/// <param name=\"data\">The input JSON data element.</param>");
        AppendLine(sb, "    ", "/// <param name=\"workspace\">The workspace for pooled memory.</param>");
        AppendLine(sb, "    ", "/// <returns>The result of the evaluation, or a <c>default</c> <see cref=\"JsonElement\"/> if the expression produces no result.</returns>");
        AppendLine(sb, "    ", "[MethodImpl(MethodImplOptions.AggressiveOptimization)]");
        AppendLine(sb, "    ", "public static JsonElement Evaluate(in JsonElement data, JsonWorkspace workspace)");
        AppendLine(sb, "    ", "{");
        AppendLine(sb, "        ", "return Evaluator.Evaluate(Expression, data, workspace);");
        AppendLine(sb, "    ", "}");

        // Emit the full Evaluate overload (data + workspace + bindings + limits)
        AppendBlank(sb);
        AppendLine(sb, "    ", "/// <summary>");
        AppendLine(sb, "    ", "/// Evaluates the JSONata expression with external variable bindings and resource limits.");
        AppendLine(sb, "    ", "/// </summary>");
        AppendLine(sb, "    ", "/// <param name=\"data\">The input JSON data element.</param>");
        AppendLine(sb, "    ", "/// <param name=\"workspace\">The workspace for pooled memory.</param>");
        AppendLine(sb, "    ", "/// <param name=\"bindings\">Optional external variable bindings.</param>");
        AppendLine(sb, "    ", "/// <param name=\"maxDepth\">Maximum evaluation depth (default 500).</param>");
        AppendLine(sb, "    ", "/// <param name=\"timeLimitMs\">Time limit in milliseconds (0 = unlimited).</param>");
        AppendLine(sb, "    ", "/// <returns>The result of the evaluation, or a <c>default</c> <see cref=\"JsonElement\"/> if the expression produces no result.</returns>");
        AppendLine(sb, "    ", "[MethodImpl(MethodImplOptions.AggressiveOptimization)]");
        AppendLine(sb, "    ", "public static JsonElement Evaluate(");
        AppendLine(sb, "        ", "in JsonElement data,");
        AppendLine(sb, "        ", "JsonWorkspace workspace,");
        AppendLine(sb, "        ", "System.Collections.Generic.IReadOnlyDictionary<string, JsonElement>? bindings,");
        AppendLine(sb, "        ", "int maxDepth = 500,");
        AppendLine(sb, "        ", "int timeLimitMs = 0)");
        AppendLine(sb, "    ", "{");
        AppendLine(sb, "        ", "return Evaluator.Evaluate(Expression, data, workspace, bindings, maxDepth, timeLimitMs);");
        AppendLine(sb, "    ", "}");

        AppendLine(sb, string.Empty, "}");

        return sb.ToString();
    }

    private static string EscapeStringLiteral(string s)
    {
        return s
            .Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("\r", "\\r")
            .Replace("\n", "\\n")
            .Replace("\t", "\\t");
    }

    private static string EscapeXmlContent(string s)
    {
        return s
            .Replace("&", "&amp;")
            .Replace("<", "&lt;")
            .Replace(">", "&gt;");
    }

    private static void AppendLine(StringBuilder sb, string indent, string line)
    {
        sb.Append(indent).AppendLine(line);
    }

    private static void AppendBlank(StringBuilder sb)
    {
        sb.AppendLine();
    }
}