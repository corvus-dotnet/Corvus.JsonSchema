using Corvus.Text.Json;
using Corvus.Text.Json.OpenApi;

namespace Corvus.Text.Json.OpenApi.CodeGeneration;

/// <summary>
/// Emits C# source files from a <see cref="ClientModel"/>.
/// </summary>
/// <remarks>
/// <para>
/// The emitter produces:
/// <list type="bullet">
/// <item><description>One request struct per operation (e.g. <c>ListPetsRequest</c>).</description></item>
/// <item><description>One response struct per operation (e.g. <c>ListPetsResponse</c>).</description></item>
/// <item><description>One client interface per tag group (e.g. <c>IPetstoreClient</c>).</description></item>
/// <item><description>One client implementation per tag group (e.g. <c>PetstoreClient</c>).</description></item>
/// </list>
/// </para>
/// <para>
/// Generated client methods dispatch to the <see cref="IApiTransport"/>
/// via strongly-typed request/response structs. No-body operations use
/// <c>SendAsync&lt;TRequest, TResponse&gt;</c>,
/// while operations with a body use
/// <c>SendAsync&lt;TRequest, TBody, TResponse&gt;</c>,
/// which writes the body directly into the transport's output stream.
/// </para>
/// </remarks>
public sealed class ClientCodeEmitter
{
    private readonly string rootNamespace;
    private readonly string? clientNamePrefix;
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="ClientCodeEmitter"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator. Every schema pointer discovered by the
    /// walker must have a corresponding entry.
    /// </param>
    /// <param name="clientNamePrefix">
    /// Optional prefix for client type names. If <see langword="null"/>,
    /// the API title is used.
    /// </param>
    public ClientCodeEmitter(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap,
        string? clientNamePrefix = null)
    {
        this.rootNamespace = rootNamespace;
        this.schemaTypeMap = schemaTypeMap;
        this.clientNamePrefix = clientNamePrefix;
    }

    /// <summary>
    /// Classifies how a parameter value should be serialized to UTF-8 bytes.
    /// </summary>
    private enum ParameterSerializationKind
    {
        /// <summary>
        /// String value: use <c>GetUtf8String()</c> to get unescaped UTF-8 bytes directly.
        /// </summary>
        String,

        /// <summary>
        /// Boolean value: use <c>TryFormat</c> with a 5-byte buffer.
        /// </summary>
        Boolean,

        /// <summary>
        /// Bounded integer (int32): use <c>TryFormat</c> with an 11-byte buffer.
        /// </summary>
        Int32,

        /// <summary>
        /// Bounded integer (int64): use <c>TryFormat</c> with a 20-byte buffer.
        /// </summary>
        Int64,

        /// <summary>
        /// Bounded float (float): use <c>TryFormat</c> with a buffer.
        /// </summary>
        Float,

        /// <summary>
        /// Bounded float (double): use <c>TryFormat</c> with a buffer.
        /// </summary>
        Double,

        /// <summary>
        /// Unbounded scalar number (no format constraint): use
        /// <c>JsonMarshal.GetRawUtf8Value</c> to get the raw UTF-8 bytes.
        /// </summary>
        UnboundedNumber,

        /// <summary>
        /// Object value: serialize with style/explode rules, or write the JSON
        /// representation via <c>WriteTo(Utf8JsonWriter)</c> over the output buffer.
        /// </summary>
        Object,

        /// <summary>
        /// Array value: serialize with style/explode rules, or write the JSON
        /// representation via <c>WriteTo(Utf8JsonWriter)</c> over the output buffer.
        /// </summary>
        Array,
    }

    /// <summary>
    /// Emits all generated files from the given client model.
    /// </summary>
    /// <param name="model">The client model to emit.</param>
    /// <returns>The generated source files.</returns>
    public IReadOnlyList<GeneratedFile> Emit(ClientModel model)
    {
        List<GeneratedFile> files = [];
        Dictionary<string, List<ClientOperation>> groups = GroupOperationsByTag(model.Operations);
        string? defaultServerUrl = model.GetDefaultServerUrl();

        foreach (ClientOperation op in model.Operations)
        {
            string methodName = op.GetMethodName();
            files.Add(this.EmitRequestStruct(op, methodName));
            files.Add(this.EmitResponseStruct(op, methodName));
        }

        foreach ((string tag, List<ClientOperation> operations) in groups)
        {
            string clientName = GetClientName(tag);
            files.Add(this.EmitInterface(clientName, operations, defaultServerUrl));
            files.Add(this.EmitImplementation(clientName, operations));
        }

        return files;
    }

    private static Dictionary<string, List<ClientOperation>> GroupOperationsByTag(
        ClientOperation[] operations)
    {
        Dictionary<string, List<ClientOperation>> groups = new(StringComparer.Ordinal);

        foreach (ClientOperation op in operations)
        {
            string[] tags = op.GetTags();

            if (tags.Length == 0)
            {
                AddToGroup(groups, "default", op);
            }
            else
            {
                foreach (string tag in tags)
                {
                    AddToGroup(groups, tag, op);
                }
            }
        }

        return groups;

        static void AddToGroup(
            Dictionary<string, List<ClientOperation>> groups,
            string tag,
            ClientOperation op)
        {
            if (!groups.TryGetValue(tag, out List<ClientOperation>? list))
            {
                list = [];
                groups[tag] = list;
            }

            list.Add(op);
        }
    }

    private static string SanitizeIdentifier(string name)
    {
        char[] chars = new char[name.Length];
        bool capitalizeNext = true;
        int written = 0;

        foreach (char c in name)
        {
            if (c is ' ' or '_' or '-' or '.')
            {
                capitalizeNext = true;
                continue;
            }

            if (!char.IsLetterOrDigit(c))
            {
                continue;
            }

            chars[written++] = capitalizeNext ? char.ToUpperInvariant(c) : c;
            capitalizeNext = false;
        }

        return new string(chars, 0, written);
    }

    private static string SanitizeParameterName(string name)
    {
        // If the name is already a valid C# identifier, use it as-is.
        bool needsSanitizing = false;
        if (name.Length == 0 || (!char.IsLetter(name[0]) && name[0] != '_'))
        {
            needsSanitizing = true;
        }
        else
        {
            foreach (char c in name)
            {
                if (!char.IsLetterOrDigit(c) && c != '_')
                {
                    needsSanitizing = true;
                    break;
                }
            }
        }

        if (!needsSanitizing)
        {
            return name;
        }

        // Otherwise sanitize (PascalCase) and lower-case the first char for camelCase.
        string pascal = SanitizeIdentifier(name);
        if (pascal.Length == 0)
        {
            return "_param";
        }

        return string.Concat(char.ToLowerInvariant(pascal[0]).ToString(), pascal.AsSpan(1));
    }

    private static string EscapeCSharpKeyword(string name) =>
        name switch
        {
            "abstract" or "as" or "base" or "bool" or "break" or "byte" or "case" or
            "catch" or "char" or "checked" or "class" or "const" or "continue" or
            "decimal" or "default" or "delegate" or "do" or "double" or "else" or
            "enum" or "event" or "explicit" or "extern" or "false" or "finally" or
            "fixed" or "float" or "for" or "foreach" or "goto" or "if" or "implicit" or
            "in" or "int" or "interface" or "internal" or "is" or "lock" or "long" or
            "namespace" or "new" or "null" or "object" or "operator" or "out" or
            "override" or "params" or "private" or "protected" or "public" or "readonly" or
            "ref" or "return" or "sbyte" or "sealed" or "short" or "sizeof" or
            "stackalloc" or "static" or "string" or "struct" or "switch" or "this" or
            "throw" or "true" or "try" or "typeof" or "uint" or "ulong" or "unchecked" or
            "unsafe" or "ushort" or "using" or "virtual" or "void" or "volatile" or "while"
                => $"@{name}",
            _ => name,
        };

    private static string EscapeXml(string text) =>
        text.Replace("&", "&amp;", StringComparison.Ordinal)
            .Replace("<", "&lt;", StringComparison.Ordinal)
            .Replace(">", "&gt;", StringComparison.Ordinal);

    private static string EscapeStringLiteral(string text) =>
        text.Replace("\\", "\\\\", StringComparison.Ordinal)
            .Replace("\"", "\\\"", StringComparison.Ordinal);

    private static string OperationMethodExpression(OperationMethod method) =>
        method switch
        {
            OperationMethod.Get => "OperationMethod.Get",
            OperationMethod.Post => "OperationMethod.Post",
            OperationMethod.Put => "OperationMethod.Put",
            OperationMethod.Delete => "OperationMethod.Delete",
            OperationMethod.Patch => "OperationMethod.Patch",
            OperationMethod.Head => "OperationMethod.Head",
            OperationMethod.Options => "OperationMethod.Options",
            OperationMethod.Trace => "OperationMethod.Trace",
            _ => $"(OperationMethod){(int)method}",
        };

    private static void EmitHeader(IndentedWriter w)
    {
        w.WriteLine("// <auto-generated>");
        w.WriteLine("// This code was generated by the Corvus.Text.Json OpenAPI code generator.");
        w.WriteLine("// Do not edit this file directly.");
        w.WriteLine("// </auto-generated>");
        w.WriteLine();
        w.WriteLine("#nullable enable");
        w.WriteLine();
        w.WriteLine("using System.Buffers;");
        w.WriteLine("using Corvus.Runtime.InteropServices;");
        w.WriteLine("using Corvus.Text.Json;");
        w.WriteLine("using Corvus.Text.Json.Internal;");
        w.WriteLine("using Corvus.Text.Json.OpenApi;");
        w.WriteLine();
    }

    private static string StatusCodeToName(string statusCode) =>
        statusCode switch
        {
            "200" => "Ok",
            "201" => "Created",
            "202" => "Accepted",
            "204" => "NoContent",
            "301" => "MovedPermanently",
            "304" => "NotModified",
            "400" => "BadRequest",
            "401" => "Unauthorized",
            "403" => "Forbidden",
            "404" => "NotFound",
            "405" => "MethodNotAllowed",
            "409" => "Conflict",
            "422" => "UnprocessableEntity",
            "429" => "TooManyRequests",
            "500" => "InternalServerError",
            "502" => "BadGateway",
            "503" => "ServiceUnavailable",
            "default" => "Default",
            _ when statusCode.Length == 3 && statusCode[1] == 'X' && statusCode[2] == 'X' =>
                $"Status{statusCode[0]}xx",
            _ => $"Status{statusCode}",
        };

    private string GetClientName(string tag)
    {
        string prefix = this.clientNamePrefix ?? "Api";
        string sanitized = SanitizeIdentifier(tag);
        return $"{prefix}{sanitized}";
    }

    private string GetParameterTypeName(ClientParameter param)
    {
        string resolved = this.ResolveSchemaTypeName(param.SchemaPointer);
        return param.IsRequired ? resolved : $"{resolved}?";
    }

    private string GetParameterSourceTypeName(ClientParameter param)
    {
        string resolved = this.ResolveSchemaTypeName(param.SchemaPointer);
        return $"{resolved}.Source";
    }

    private string ResolveSchemaTypeName(string? schemaPointer)
    {
        if (schemaPointer is not null
            && this.schemaTypeMap.TryGetValue(schemaPointer, out string? typeName))
        {
            return typeName;
        }

        return "JsonElement";
    }

    private string ResolveRequestBodyTypeName(ClientRequestBody requestBody)
    {
        ReadOnlySpan<byte> applicationJson = "application/json"u8;

        foreach (ClientMediaTypeContent content in requestBody.Content)
        {
            if (content.MediaTypeEquals(applicationJson))
            {
                return this.ResolveSchemaTypeName(content.SchemaPointer);
            }
        }

        return "JsonElement";
    }

    private static ParameterSerializationKind ClassifyParameter(ClientParameter param)
    {
        if (!param.Element.TryGetProperty("schema"u8, out JsonElement schema))
        {
            return ParameterSerializationKind.String;
        }

        if (!schema.TryGetProperty("type"u8, out JsonElement typeElement)
            || typeElement.ValueKind != JsonValueKind.String)
        {
            return ParameterSerializationKind.String;
        }

        if (typeElement.ValueEquals("string"u8))
        {
            return ParameterSerializationKind.String;
        }

        if (typeElement.ValueEquals("boolean"u8))
        {
            return ParameterSerializationKind.Boolean;
        }

        if (typeElement.ValueEquals("integer"u8))
        {
            if (schema.TryGetProperty("format"u8, out JsonElement fmt)
                && fmt.ValueKind == JsonValueKind.String)
            {
                if (fmt.ValueEquals("int32"u8))
                {
                    return ParameterSerializationKind.Int32;
                }

                if (fmt.ValueEquals("int64"u8))
                {
                    return ParameterSerializationKind.Int64;
                }
            }

            return ParameterSerializationKind.UnboundedNumber;
        }

        if (typeElement.ValueEquals("number"u8))
        {
            if (schema.TryGetProperty("format"u8, out JsonElement fmt)
                && fmt.ValueKind == JsonValueKind.String)
            {
                if (fmt.ValueEquals("float"u8))
                {
                    return ParameterSerializationKind.Float;
                }

                if (fmt.ValueEquals("double"u8))
                {
                    return ParameterSerializationKind.Double;
                }
            }

            return ParameterSerializationKind.UnboundedNumber;
        }

        if (typeElement.ValueEquals("object"u8))
        {
            return ParameterSerializationKind.Object;
        }

        if (typeElement.ValueEquals("array"u8))
        {
            return ParameterSerializationKind.Array;
        }

        return ParameterSerializationKind.String;
    }

    private string? ResolveResponseTypeName(ClientResponse resp)
    {
        ReadOnlySpan<byte> applicationJson = "application/json"u8;

        foreach (ClientMediaTypeContent content in resp.Content)
        {
            if (content.MediaTypeEquals(applicationJson))
            {
                string resolved = this.ResolveSchemaTypeName(content.SchemaPointer);
                return resolved;
            }
        }

        return null;
    }

    private GeneratedFile EmitRequestStruct(ClientOperation op, string methodName)
    {
        string structName = $"{methodName}Request";
        IndentedWriter w = new();

        EmitHeader(w);
        w.WriteLine($"namespace {this.rootNamespace};");
        w.WriteLine();

        string? summary = op.GetSummary();
        w.WriteLine("/// <summary>");
        w.WriteLine($"/// Request type for the {methodName} operation.");
        w.WriteLine("/// </summary>");

        if (summary is not null)
        {
            w.WriteLine($"/// <remarks>{EscapeXml(summary)}</remarks>");
        }

        w.WriteLine($"public readonly struct {structName} : IApiRequest<{structName}>");
        w.OpenBrace();

        ClientParameter[] pathParams = op.Parameters
            .Where(p => p.Location == ParameterLocation.Path).ToArray();
        ClientParameter[] queryParams = op.Parameters
            .Where(p => p.Location == ParameterLocation.Query).ToArray();
        ClientParameter[] headerParams = op.Parameters
            .Where(p => p.Location == ParameterLocation.Header).ToArray();
        ClientParameter[] cookieParams = op.Parameters
            .Where(p => p.Location == ParameterLocation.Cookie).ToArray();

        this.EmitRequestFields(w, op.Parameters);
        this.EmitRequestConstructor(w, structName, op.Parameters);

        string pathTemplateUtf8 = op.GetPathTemplate();

        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static ReadOnlySpan<byte> PathTemplateUtf8 => \"{pathTemplateUtf8}\"u8;");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static OperationMethod Method => {OperationMethodExpression(op.Method)};");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static bool HasPathParameters => {(pathParams.Length > 0 ? "true" : "false")};");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static bool HasQueryParameters => {(queryParams.Length > 0 ? "true" : "false")};");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static bool HasHeaderParameters => {(headerParams.Length > 0 ? "true" : "false")};");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static bool HasCookieParameters => {(cookieParams.Length > 0 ? "true" : "false")};");

        w.WriteLine();
        this.EmitWriteResolvedPath(w, pathTemplateUtf8, pathParams);
        w.WriteLine();
        this.EmitWriteQueryString(w, queryParams);
        w.WriteLine();
        this.EmitWriteHeaders(w, headerParams);
        w.WriteLine();
        this.EmitWriteCookies(w, cookieParams);

        w.CloseBrace();

        return new GeneratedFile($"{structName}.cs", w.ToString());
    }

    private void EmitRequestFields(IndentedWriter w, ClientParameter[] allParams)
    {
        foreach (ClientParameter param in allParams)
        {
            string fieldName = SanitizeIdentifier(param.GetName());
            string typeName = this.GetParameterTypeName(param);

            w.WriteLine();
            w.WriteLine("/// <summary>");
            w.WriteLine($"/// Gets the {param.GetName()} parameter.");
            w.WriteLine("/// </summary>");
            w.WriteLine($"public {typeName} {fieldName} {{ get; init; }}");
        }
    }

    private void EmitRequestConstructor(
        IndentedWriter w,
        string structName,
        ClientParameter[] allParams)
    {
        ClientParameter[] requiredParams = allParams.Where(p => p.IsRequired).ToArray();

        if (requiredParams.Length == 0)
        {
            return;
        }

        w.WriteLine();
        w.WriteLine("/// <summary>");
        w.WriteLine($"/// Initializes a new instance of the <see cref=\"{structName}\"/> struct.");
        w.WriteLine("/// </summary>");

        List<string> paramParts = [];

        foreach (ClientParameter param in requiredParams)
        {
            string sanitizedParam = SanitizeParameterName(param.GetName());
            w.WriteLine($"/// <param name=\"{sanitizedParam}\">The {param.GetName()} parameter.</param>");
            string typeName = this.GetParameterTypeName(param);
            paramParts.Add($"{typeName} {EscapeCSharpKeyword(sanitizedParam)}");
        }

        w.WriteLine($"public {structName}({string.Join(", ", paramParts)})");
        w.OpenBrace();

        foreach (ClientParameter param in requiredParams)
        {
            string fieldName = SanitizeIdentifier(param.GetName());
            string paramIdentifier = EscapeCSharpKeyword(SanitizeParameterName(param.GetName()));
            w.WriteLine($"this.{fieldName} = {paramIdentifier};");
        }

        w.CloseBrace();
    }

    private void EmitWriteResolvedPath(
        IndentedWriter w,
        string pathTemplate,
        ClientParameter[] pathParams)
    {
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine("public void WriteResolvedPath(IBufferWriter<byte> writer)");
        w.OpenBrace();

        if (pathParams.Length == 0)
        {
            w.WriteLine($"writer.Write(\"{pathTemplate}\"u8);");
        }
        else
        {
            this.EmitPathSegmentWrites(w, pathTemplate, pathParams);
        }

        w.CloseBrace();
    }

    private void EmitPathSegmentWrites(
        IndentedWriter w,
        string pathTemplate,
        ClientParameter[] pathParams)
    {
        ReadOnlySpan<char> remaining = pathTemplate;

        while (remaining.Length > 0)
        {
            int openBrace = remaining.IndexOf('{');

            if (openBrace < 0)
            {
                w.WriteLine($"writer.Write(\"{remaining.ToString()}\"u8);");
                break;
            }

            if (openBrace > 0)
            {
                w.WriteLine($"writer.Write(\"{remaining[..openBrace].ToString()}\"u8);");
            }

            int closeBrace = remaining[(openBrace + 1)..].IndexOf('}');

            if (closeBrace < 0)
            {
                w.WriteLine($"writer.Write(\"{remaining[openBrace..].ToString()}\"u8);");
                break;
            }

            string paramName = remaining[(openBrace + 1)..(openBrace + 1 + closeBrace)].ToString();
            string fieldName = SanitizeIdentifier(paramName);

            ClientParameter? matchingParam = null;

            foreach (ClientParameter p in pathParams)
            {
                if (p.GetName() == paramName)
                {
                    matchingParam = p;
                    break;
                }
            }

            ParameterSerializationKind kind = matchingParam is not null
                ? ClassifyParameter(matchingParam.Value)
                : ParameterSerializationKind.String;

            EmitPathParamWrite(w, $"this.{fieldName}", fieldName, kind);
            remaining = remaining[(openBrace + 1 + closeBrace + 1)..];
        }
    }

    // ── Scalar UTF-8 value extraction helpers ───────────────────────────
    //
    // These emit code to write the raw UTF-8 value of a scalar into a
    // buffer. They do NOT apply URI escaping — the caller is responsible
    // for that based on the parameter location.

    /// <summary>
    /// Emits code that gets the UTF-8 bytes for a string value via
    /// <c>GetUtf8String()</c> and writes them to <c>writer</c>.
    /// </summary>
    private static void EmitStringWrite(
        IndentedWriter w,
        string valueExpr,
        string uid)
    {
        w.WriteLine(
            $"using UnescapedUtf8JsonString utf8{uid} = " +
            $"((JsonElement){valueExpr}).GetUtf8String();");
    }

    /// <summary>
    /// Emits a <c>TryFormat</c> into a correctly-sized stackalloc buffer.
    /// After this, <c>buf{uid}[..bw{uid}]</c> holds the UTF-8 bytes.
    /// </summary>
    private static void EmitTryFormatToLocal(
        IndentedWriter w,
        string valueExpr,
        string uid,
        int bufferSize)
    {
        w.WriteLine($"Span<byte> buf{uid} = stackalloc byte[{bufferSize}];");
        w.WriteLine(
            $"{valueExpr}.TryFormat(buf{uid}, " +
            $"out int bw{uid}, default, default);");
    }

    /// <summary>
    /// Emits code that gets the raw UTF-8 JSON bytes for an unbounded
    /// number via <c>JsonMarshal.GetRawUtf8Value</c>.
    /// </summary>
    private static void EmitUnboundedNumberToLocal(
        IndentedWriter w,
        string valueExpr,
        string uid)
    {
        w.WriteLine(
            $"using RawUtf8JsonString raw{uid} = " +
            $"JsonMarshal.GetRawUtf8Value({valueExpr});");
    }

    /// <summary>
    /// Returns the stackalloc buffer size for a bounded type, or -1 if
    /// the type doesn't use <c>TryFormat</c>.
    /// </summary>
    private static int TryFormatBufferSize(ParameterSerializationKind kind) =>
        kind switch
        {
            ParameterSerializationKind.Boolean => 5,
            ParameterSerializationKind.Int32 => 11,
            ParameterSerializationKind.Int64 => 20,
            ParameterSerializationKind.Float => 32,
            ParameterSerializationKind.Double => 32,
            _ => -1,
        };

    // ── Path parameter serialization (simple style, no explode) ─────────
    private static void EmitPathParamWrite(
        IndentedWriter w,
        string valueExpr,
        string uid,
        ParameterSerializationKind kind)
    {
        switch (kind)
        {
            case ParameterSerializationKind.String:
                EmitStringWrite(w, valueExpr, uid);
                EmitUriEscapeAndWrite(w, $"utf8{uid}.Span", uid);
                break;

            case ParameterSerializationKind.Boolean:
                EmitBooleanWrite(w, valueExpr);
                break;

            case ParameterSerializationKind.Int32:
            case ParameterSerializationKind.Int64:
            case ParameterSerializationKind.Float:
            case ParameterSerializationKind.Double:
                EmitTryFormatToLocal(w, valueExpr, uid, TryFormatBufferSize(kind));
                w.WriteLine($"writer.Write(buf{uid}[..bw{uid}]);");
                break;

            case ParameterSerializationKind.UnboundedNumber:
                EmitUnboundedNumberToLocal(w, valueExpr, uid);
                w.WriteLine($"writer.Write(raw{uid}.Span);");
                break;

            case ParameterSerializationKind.Object:
            case ParameterSerializationKind.Array:
                // TODO: style+explode dependent serialization.
                EmitJsonWriterFallback(w, valueExpr, uid);
                break;
        }
    }

    // ── Query parameter serialization (form style) ──────────────────────
    private static void EmitQueryScalarWrite(
        IndentedWriter w,
        string valueExpr,
        string uid,
        ParameterSerializationKind kind)
    {
        switch (kind)
        {
            case ParameterSerializationKind.Boolean:
                EmitBooleanWriteCounted(w, valueExpr);
                break;

            case ParameterSerializationKind.Int32:
            case ParameterSerializationKind.Int64:
            case ParameterSerializationKind.Float:
            case ParameterSerializationKind.Double:
                EmitTryFormatToLocal(w, valueExpr, uid, TryFormatBufferSize(kind));
                w.WriteLine($"writer.Write(buf{uid}[..bw{uid}]);");
                w.WriteLine($"totalWritten += bw{uid};");
                break;

            case ParameterSerializationKind.UnboundedNumber:
                EmitUnboundedNumberToLocal(w, valueExpr, uid);
                w.WriteLine($"writer.Write(raw{uid}.Span);");
                w.WriteLine($"totalWritten += raw{uid}.Span.Length;");
                break;

            case ParameterSerializationKind.String:
                EmitStringWrite(w, valueExpr, uid);
                EmitUriEscapeAndWriteCounted(w, $"utf8{uid}.Span", uid);
                break;

            case ParameterSerializationKind.Object:
            case ParameterSerializationKind.Array:
                // TODO: style+explode dependent serialization.
                EmitJsonWriterFallbackCounted(w, valueExpr, uid);
                break;
        }
    }

    // ── Header parameter serialization (simple style, no escaping) ──────
    private static void EmitHeaderScalarWrite(
        IndentedWriter w,
        string valueExpr,
        string uid,
        ParameterSerializationKind kind)
    {
        switch (kind)
        {
            case ParameterSerializationKind.String:
                EmitStringWrite(w, valueExpr, uid);
