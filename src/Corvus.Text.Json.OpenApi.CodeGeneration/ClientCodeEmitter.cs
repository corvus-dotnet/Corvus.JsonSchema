            w.OpenBrace();
            w.WriteLine(
                $"callback(\"{headerName}\"u8, hbuf{sanitized}[..hw{sanitized}], state);");
            w.CloseBrace();
        }
    }

    private GeneratedFile EmitResponseStruct(ClientOperation op, string methodName)
    {
        string structName = $"{methodName}Response";
        IndentedWriter w = new();

        EmitHeader(w);
        w.WriteLine($"namespace {this.rootNamespace};");
        w.WriteLine();

        w.WriteLine("/// <summary>");
        w.WriteLine($"/// Response type for the {methodName} operation.");
        w.WriteLine("/// </summary>");
        w.WriteLine($"public struct {structName} : IApiResponse<{structName}>");
        w.OpenBrace();

        w.WriteLine("private IAsyncDisposable? owner;");
        w.WriteLine("private IDisposable? parsedDocument;");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine("public int StatusCode { get; private set; }");
        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine("public bool IsSuccess => this.StatusCode >= 200 && this.StatusCode < 300;");

        foreach (ClientResponse resp in op.Responses)
        {
            string statusCode = resp.GetStatusCode();
            string accessorName = StatusCodeToName(statusCode);
            string? typeName = this.ResolveResponseTypeName(resp);

            if (typeName is not null)
            {
                w.WriteLine();
                w.WriteLine("/// <summary>");
                w.WriteLine($"/// Gets the {statusCode} response body.");
                w.WriteLine("/// </summary>");
                w.WriteLine($"public {typeName} {accessorName}Body {{ get; private set; }}");
            }
        }

        w.WriteLine();
        this.EmitCreateAsync(w, structName, op.Responses);

        foreach (ClientResponse resp in op.Responses)
        {
            string statusCode = resp.GetStatusCode();
            string accessorName = StatusCodeToName(statusCode);
            string? typeName = this.ResolveResponseTypeName(resp);

            if (typeName is null)
            {
                continue;
            }

            w.WriteLine();
            w.WriteLine("/// <summary>");
            w.WriteLine($"/// Tries to get the {statusCode} response body.");
            w.WriteLine("/// </summary>");
            w.WriteLine(
                "/// <param name=\"result\">The typed response body if the status matches.</param>");
            w.WriteLine(
                $"/// <returns><see langword=\"true\"/> if the status code is {statusCode}.</returns>");
            w.WriteLine($"public bool TryGet{accessorName}(out {typeName} result)");
            w.OpenBrace();

            if (statusCode == "default")
            {
                w.WriteLine($"result = this.{accessorName}Body;");
                w.WriteLine("return true;");
            }
            else
            {
                w.WriteLine($"if (this.StatusCode == {statusCode})");
                w.OpenBrace();
                w.WriteLine($"result = this.{accessorName}Body;");
                w.WriteLine("return true;");
                w.CloseBrace();
                w.WriteLine();
                w.WriteLine("result = default;");
                w.WriteLine("return false;");
            }

            w.CloseBrace();
        }

        w.WriteLine();
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine("public async ValueTask DisposeAsync()");
        w.OpenBrace();
        w.WriteLine("this.parsedDocument?.Dispose();");
        w.WriteLine("if (this.owner is not null)");
        w.OpenBrace();
        w.WriteLine("await this.owner.DisposeAsync().ConfigureAwait(false);");
        w.CloseBrace();
        w.CloseBrace();

        w.CloseBrace();

        return new GeneratedFile($"{structName}.cs", w.ToString());
    }

    private void EmitCreateAsync(
        IndentedWriter w,
        string structName,
        ClientResponse[] responses)
    {
        w.WriteLine("/// <inheritdoc/>");
        w.WriteLine($"public static async ValueTask<{structName}> CreateAsync(");
        w.PushIndent();
        w.WriteLine("int statusCode,");
        w.WriteLine("Stream contentStream,");
        w.WriteLine("IAsyncDisposable? owner = null,");
        w.WriteLine("CancellationToken cancellationToken = default)");
        w.PopIndent();
        w.OpenBrace();

        w.WriteLine($"{structName} response = default;");
        w.WriteLine("response.StatusCode = statusCode;");
        w.WriteLine("response.owner = owner;");
        w.WriteLine();

        foreach (ClientResponse resp in responses)
        {
            string statusCode = resp.GetStatusCode();
            string accessorName = StatusCodeToName(statusCode);
            string? typeName = this.ResolveResponseTypeName(resp);

            if (typeName is null || statusCode == "default")
            {
                continue;
            }

            w.WriteLine($"if (statusCode == {statusCode})");
            w.OpenBrace();
            EmitParseResponseBody(w, typeName, accessorName);
            w.WriteLine("return response;");
            w.CloseBrace();
            w.WriteLine();
        }

        ClientResponse? defaultResp = null;

        foreach (ClientResponse resp in responses)
        {
            if (resp.GetStatusCode() == "default")
            {
                defaultResp = resp;
                break;
            }
        }

        if (defaultResp is not null)
        {
            string? defaultTypeName = this.ResolveResponseTypeName(defaultResp.Value);

            if (defaultTypeName is not null)
            {
                string accessorName = StatusCodeToName("default");
                EmitParseResponseBody(w, defaultTypeName, accessorName);
            }
        }

