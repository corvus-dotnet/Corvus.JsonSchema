namespace Corvus.Text.Json.OpenApi.CodeGeneration;

/// <summary>
/// The default C# <see cref="ISchemaTypeResolver"/>: maps a schema pointer to the fully
/// qualified .NET type name produced by the JSON Schema model generator, falling back to
/// <c>JsonElement</c> when the pointer is absent or unknown.
/// </summary>
public sealed class DefaultSchemaTypeResolver : ISchemaTypeResolver
{
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="DefaultSchemaTypeResolver"/> class.
    /// </summary>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names generated by the
    /// V5 code generator.
    /// </param>
    public DefaultSchemaTypeResolver(IReadOnlyDictionary<string, string> schemaTypeMap)
    {
        this.schemaTypeMap = schemaTypeMap;
    }

    /// <inheritdoc/>
    public string ResolveTypeName(SchemaRef? schema)
    {
        if (schema is { } schemaRef
            && this.schemaTypeMap.TryGetValue(schemaRef.PositionalPointer, out string? typeName))
        {
            return typeName;
        }

        return "JsonElement";
    }

    /// <inheritdoc/>
    public string ResolveElementTypeName(SchemaRef? schema) => this.ResolveTypeName(schema);
}