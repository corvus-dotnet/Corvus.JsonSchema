using Corvus.Text.Json.OpenApi.CodeGeneration;

namespace Corvus.Text.Json.OpenApi.TypeScript.CodeGeneration;

/// <summary>
/// The TypeScript <see cref="ISchemaTypeResolver"/>: maps a schema pointer to the TypeScript final
/// name produced by the <c>TypeScriptLanguageProvider</c> model generator, falling back to
/// <c>unknown</c> when the pointer is absent or unknown.
/// </summary>
/// <remarks>
/// <para>
/// This is the seam the TypeScript emitter supplies to the shared OpenAPI generators in place of the
/// C# <c>DefaultSchemaTypeResolver</c>. The map is built by reading each generated type's
/// <c>Ts_FinalName</c> metadata, just as the C# path reads <c>FullyQualifiedDotnetTypeName()</c>.
/// </para>
/// </remarks>
public sealed class TypeScriptSchemaTypeResolver : ISchemaTypeResolver
{
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="TypeScriptSchemaTypeResolver"/> class.
    /// </summary>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to TypeScript final names generated by the TypeScript engine.
    /// </param>
    public TypeScriptSchemaTypeResolver(IReadOnlyDictionary<string, string> schemaTypeMap)
    {
        this.schemaTypeMap = schemaTypeMap;
    }

    /// <inheritdoc/>
    public string ResolveTypeName(SchemaRef? schema)
    {
        if (schema is { } schemaRef
            && this.schemaTypeMap.TryGetValue(schemaRef.PositionalPointer, out string? typeName))
        {
            return typeName;
        }

        return "unknown";
    }

    /// <inheritdoc/>
    public string ResolveElementTypeName(SchemaRef? schema) => this.ResolveTypeName(schema);

    /// <summary>
    /// Gets the model type names this resolver knows about (the distinct TypeScript final names), so
    /// the emitter can compute the set of named model imports it needs.
    /// </summary>
    /// <returns>The distinct model type names.</returns>
    public IReadOnlyCollection<string> KnownTypeNames()
        => [.. new HashSet<string>(this.schemaTypeMap.Values, StringComparer.Ordinal)];
}