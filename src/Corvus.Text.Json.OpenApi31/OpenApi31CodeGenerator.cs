using Corvus.Text.Json;
using Corvus.Text.Json.Internal;
using Corvus.Text.Json.OpenApi;
using Corvus.Text.Json.OpenApi.CodeGeneration;

namespace Corvus.Text.Json.OpenApi31;

/// <summary>
/// A self-contained code generator that walks the strongly-typed
/// <see cref="OpenApiDocument"/> model directly and emits C# source files.
/// </summary>
/// <remarks>
/// <para>
/// This replaces the multi-stage pipeline (Walker → Walked* → ClientModelBuilder →
/// Client* → ClientCodeEmitter) with a single class that:
/// <list type="number">
/// <item><description>Walks the typed model to collect operation data into lightweight internal records.</description></item>
/// <item><description>Emits C# source files from those records.</description></item>
/// </list>
/// </para>
/// <para>
/// All shared text utilities, scalar emission, and pointer construction are delegated
/// to <see cref="CodeEmitHelpers"/> and <see cref="SchemaPointerBuilder"/>.
/// </para>
/// </remarks>
public sealed class OpenApi31CodeGenerator
{
    private readonly string rootNamespace;
    private readonly string? clientNamePrefix;
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="OpenApi31CodeGenerator"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator. Every schema pointer discovered
    /// during the walk must have a corresponding entry.
    /// </param>
    /// <param name="clientNamePrefix">
    /// Optional prefix for client type names. If <see langword="null"/>,
    /// <c>"Api"</c> is used.
    /// </param>
    public OpenApi31CodeGenerator(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap,
        string? clientNamePrefix = null)
    {
        this.rootNamespace = rootNamespace;
        this.schemaTypeMap = schemaTypeMap;
        this.clientNamePrefix = clientNamePrefix;
    }

    // ── Walk-phase reference (typed model objects, no strings extracted) ──
    private readonly record struct OperationRef(
        JsonProperty<OpenApiDocument.PathItem> PathProp,
        OpenApiDocument.Operation Operation,
        OperationMethod Method,
        OpenApiDocument.PathItem PathItem);

    // ── Emit-boundary record types (strings for C# code generation) ─────
    private readonly record struct OperationInfo(
        string PathTemplate,
        OperationMethod Method,
        string MethodName,
        string? OperationId,
        string? Summary,
        string? Description,
        string[] Tags,
        ParameterInfo[] Parameters,
        RequestBodyInfo? RequestBody,
        ResponseInfo[] Responses);

    private readonly record struct ParameterInfo(
        string Name,
        ParameterLocation Location,
        bool IsRequired,
        ParameterStyle Style,
        bool Explode,
        ParameterSerializationKind SerializationKind,
        string? SchemaPointer,
        bool HasDeepNesting);

    private readonly record struct RequestBodyInfo(
        string? Description,
        bool IsRequired,
        ContentInfo[] Content);

    private readonly record struct ResponseInfo(
        string StatusCode,
        ContentInfo[] Content,
        HeaderInfo[] Headers);

    private readonly record struct ContentInfo(
        string MediaType,
        string? SchemaPointer);

    private readonly record struct HeaderInfo(
        string HeaderName,
        string? SchemaPointer,
        bool Explode,
        ParameterSerializationKind SerializationKind,
        bool HasDeepNesting);

    /// <summary>
    /// Walks the OpenAPI 3.1 specification and collects all schema
    /// JSON Pointer strings reachable from the selected operations.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">
    /// Optional reference resolver. If <see langword="null"/>,
    /// a <see cref="LocalReferenceResolver"/> is used.
    /// </param>
    /// <returns>An array of JSON Pointer strings.</returns>
    public static string[] CollectSchemaPointers(
        JsonElement specRoot,
        out Dictionary<string, string> parameterNames,
        OperationFilter? filter = null,
        IOpenApiReferenceResolver? referenceResolver = null)
    {
        referenceResolver ??= new LocalReferenceResolver(specRoot);
        OpenApiDocument doc = specRoot;

        if (doc.PathsValue.IsUndefined())
        {
            parameterNames = new Dictionary<string, string>();
            return [];
        }

        List<string> pointers = [];
        Dictionary<string, string> paramNames = new(StringComparer.Ordinal);

        foreach (JsonProperty<OpenApiDocument.PathItem> pathProp in doc.PathsValue.EnumerateObject())
        {
            if (filter is not null)
            {
                using UnescapedUtf16JsonString name = pathProp.Utf16NameSpan;
                if (!filter.Matches(name.Span))
                {
                    continue;
                }
            }

            CollectPathItemPointers(pathProp, pointers, paramNames, referenceResolver);
        }

        parameterNames = paramNames;
        return [.. pointers];
    }

    /// <summary>
    /// Walks the OpenAPI 3.1 specification and emits C# source files.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">
    /// Optional reference resolver. If <see langword="null"/>,
    /// a <see cref="LocalReferenceResolver"/> is used.
    /// </param>
    /// <returns>The generated source files.</returns>
    public IReadOnlyList<GeneratedFile> Generate(
        JsonElement specRoot,
        OperationFilter? filter = null,
        IOpenApiReferenceResolver? referenceResolver = null)
    {
        referenceResolver ??= new LocalReferenceResolver(specRoot);
        List<OperationInfo> operations = [];

        foreach (OperationRef opRef in WalkOperationRefs(specRoot, filter))
        {
            operations.Add(PrepareOperation(opRef, referenceResolver));
        }

        List<GeneratedFile> files = [];
        Dictionary<string, List<OperationInfo>> groups = GroupOperationsByTag(operations);
        string? defaultServerUrl = GetDefaultServerUrl(specRoot);

        foreach (OperationInfo op in operations)
        {
            files.Add(this.EmitRequestStruct(op));
            files.Add(this.EmitResponseStruct(op));
        }

        foreach ((string tag, List<OperationInfo> tagOps) in groups)
        {
            string clientName = this.GetClientName(tag);
            files.Add(this.EmitInterface(clientName, tagOps, defaultServerUrl));
            files.Add(this.EmitImplementation(clientName, tagOps));
        }

        return files;
    }

    // ═══════════════════════════════════════════════════════════════════
    // Schema pointer collection — walks typed model directly
    // ═══════════════════════════════════════════════════════════════════
    private static void CollectPathItemPointers(
        JsonProperty<OpenApiDocument.PathItem> pathProp,
        List<string> pointers,
        Dictionary<string, string> parameterNames,
        IOpenApiReferenceResolver referenceResolver)
    {
        OpenApiDocument.PathItem pathItem = pathProp.Value;

        if (pathItem.Get.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Get, OperationMethod.Get, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Put.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Put, OperationMethod.Put, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Post.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Post, OperationMethod.Post, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Delete.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Delete, OperationMethod.Delete, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Options.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Options, OperationMethod.Options, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Head.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Head, OperationMethod.Head, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Patch.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Patch, OperationMethod.Patch, pathItem, pointers, parameterNames, referenceResolver);
        }

        if (pathItem.Trace.IsNotUndefined())
        {
            CollectOperationPointers(pathProp, pathItem.Trace, OperationMethod.Trace, pathItem, pointers, parameterNames, referenceResolver);
        }
    }

    private static void CollectOperationPointers(
        JsonProperty<OpenApiDocument.PathItem> pathProp,
        OpenApiDocument.Operation operation,
        OperationMethod method,
        OpenApiDocument.PathItem pathItem,
        List<string> pointers,
        Dictionary<string, string> parameterNames,
        IOpenApiReferenceResolver referenceResolver)
    {
        using UnescapedUtf8JsonString pathName = pathProp.Utf8NameSpan;

        // Parameter schema pointers (with path/operation merge)
        foreach ((OpenApiDocument.Parameter param, int sourceIndex, bool isPathLevel) in
            MergeParameters(operation, pathItem, referenceResolver))
        {
            if (param.SchemaValue.IsNotUndefined())
            {
                string pointer = SchemaPointerBuilder.BuildParameterSchemaPointer(
                    pathName.Span, method, sourceIndex, isPathLevel);
                pointers.Add(pointer);

                // Record the parameter name keyed by fragment (pointer without '#')
                // so the heuristic can look it up from reference.Fragment without allocating.
                if (param.Name.IsNotUndefined())
                {
                    string? name = param.Name.GetString();
                    if (name is not null)
                    {
                        parameterNames[pointer.AsSpan(1).ToString()] = name;
                    }
                }
            }
        }

        // Request body content schema pointers
        if (operation.RequestBody.IsNotUndefined()
            && TryResolveRequestBody(operation.RequestBody, referenceResolver, out OpenApiDocument.RequestBody requestBody)
            && requestBody.ContentValue.IsNotUndefined())
        {
            foreach (var mediaTypeProp in requestBody.ContentValue.EnumerateObject())
            {
                if (mediaTypeProp.Value.SchemaValue.IsNotUndefined())
                {
                    using UnescapedUtf8JsonString mediaTypeName = mediaTypeProp.Utf8NameSpan;

                    // Skip raw stream content types — no JSON Schema type needed.
                    if (CodeEmitHelpers.IsRawStreamMediaType(mediaTypeProp.Name))
                    {
                        continue;
                    }

                    pointers.Add(SchemaPointerBuilder.BuildContentSchemaPointer(
                        pathName.Span, method, "/requestBody"u8, mediaTypeName.Span));
                }
            }
        }

        // Response content and header schema pointers
        if (operation.ResponsesValue.IsNotUndefined())
        {
            foreach (var responseProp in operation.ResponsesValue.EnumerateObject())
            {
                if (responseProp.Value.IsUndefined())
                {
                    continue;
                }

                if (!TryResolveResponse(responseProp.Value, referenceResolver, out OpenApiDocument.Response response))
                {
                    continue;
                }

                using UnescapedUtf8JsonString statusCode = responseProp.Utf8NameSpan;

                if (response.ContentValue.IsNotUndefined())
                {
                    foreach (var mediaTypeProp in response.ContentValue.EnumerateObject())
                    {
                        if (mediaTypeProp.Value.SchemaValue.IsNotUndefined())
                        {
                            // Skip raw stream content types — no JSON Schema type needed.
                            if (CodeEmitHelpers.IsRawStreamMediaType(mediaTypeProp.Name))
                            {
                                continue;
                            }

                            using UnescapedUtf8JsonString mediaTypeName = mediaTypeProp.Utf8NameSpan;
                            pointers.Add(SchemaPointerBuilder.BuildResponseContentSchemaPointer(
                                pathName.Span, method, statusCode.Span, mediaTypeName.Span));
                        }
                    }
                }

                if (response.Headers.IsNotUndefined())
                {
                    foreach (var headerProp in response.Headers.EnumerateObject())
                    {
                        if (headerProp.Value.IsUndefined())
                        {
                            continue;
                        }

                        if (!TryResolveHeader(headerProp.Value, referenceResolver, out OpenApiDocument.Header header))
                        {
                            continue;
                        }

                        if (header.SchemaValue.IsNotUndefined())
                        {
                            using UnescapedUtf8JsonString headerName = headerProp.Utf8NameSpan;
                            pointers.Add(SchemaPointerBuilder.BuildResponseHeaderSchemaPointer(
                                pathName.Span, method, statusCode.Span, headerName.Span));
                        }
                    }
                }
            }
        }
    }

    // ═══════════════════════════════════════════════════════════════════
    // Walk logic — yields typed model references
    // ═══════════════════════════════════════════════════════════════════
    private static IEnumerable<OperationRef> WalkOperationRefs(
        JsonElement specRoot,
        OperationFilter? filter)
    {
        OpenApiDocument doc = specRoot;

        if (doc.PathsValue.IsUndefined())
        {
            yield break;
        }

        foreach (JsonProperty<OpenApiDocument.PathItem> pathProp in doc.PathsValue.EnumerateObject())
        {
            if (filter is not null)
            {
                using UnescapedUtf16JsonString name = pathProp.Utf16NameSpan;
                if (!filter.Matches(name.Span))
                {
                    continue;
                }
            }

            foreach (OperationRef entry in WalkPathItemRefs(pathProp))
            {
                yield return entry;
            }
        }
    }

    private static IEnumerable<OperationRef> WalkPathItemRefs(
        JsonProperty<OpenApiDocument.PathItem> pathProp)
    {
        OpenApiDocument.PathItem pathItem = pathProp.Value;

        if (pathItem.Get.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Get, OperationMethod.Get, pathItem);
        }

        if (pathItem.Put.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Put, OperationMethod.Put, pathItem);
        }

        if (pathItem.Post.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Post, OperationMethod.Post, pathItem);
        }

        if (pathItem.Delete.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Delete, OperationMethod.Delete, pathItem);
        }

        if (pathItem.Options.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Options, OperationMethod.Options, pathItem);
        }

        if (pathItem.Head.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Head, OperationMethod.Head, pathItem);
        }

        if (pathItem.Patch.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Patch, OperationMethod.Patch, pathItem);
        }

        if (pathItem.Trace.IsNotUndefined())
        {
            yield return new OperationRef(pathProp, pathItem.Trace, OperationMethod.Trace, pathItem);
        }
    }

    // ═══════════════════════════════════════════════════════════════════
    // Parameter merging — typed dedup via JsonString equality
    // ═══════════════════════════════════════════════════════════════════
    private static List<(OpenApiDocument.Parameter Parameter, int SourceIndex, bool IsPathLevel)> MergeParameters(
        OpenApiDocument.Operation operation,
        OpenApiDocument.PathItem pathItem,
        IOpenApiReferenceResolver referenceResolver)
    {
        bool hasOperationParams = operation.Parameters.IsNotUndefined();
        bool hasPathParams = pathItem.Parameters.IsNotUndefined();

        if (!hasOperationParams && !hasPathParams)
        {
            return [];
        }

        List<(OpenApiDocument.Parameter Parameter, int SourceIndex, bool IsPathLevel)> result = [];

        if (hasPathParams)
        {
            int sourceIndex = 0;
            foreach (OpenApiDocument.ParameterOrReference paramOrRef in pathItem.Parameters.EnumerateArray())
            {
                if (paramOrRef.IsUndefined())
                {
                    sourceIndex++;
                    continue;
                }

                if (TryResolveParameter(paramOrRef, referenceResolver, out OpenApiDocument.Parameter typed))
                {
                    result.Add((typed, sourceIndex, true));
                }

                sourceIndex++;
            }
        }

        if (hasOperationParams)
        {
            int sourceIndex = 0;
            foreach (OpenApiDocument.ParameterOrReference paramOrRef in operation.Parameters.EnumerateArray())
            {
                if (paramOrRef.IsUndefined())
                {
                    sourceIndex++;
                    continue;
                }

                if (TryResolveParameter(paramOrRef, referenceResolver, out OpenApiDocument.Parameter typed))
                {
                    int existingIndex = FindParameterIndex(result, typed);

                    if (existingIndex >= 0)
                    {
                        result[existingIndex] = (typed, sourceIndex, false);
                    }
                    else
                    {
                        result.Add((typed, sourceIndex, false));
                    }
                }

                sourceIndex++;
            }
        }

        return result;
    }

    private static int FindParameterIndex(
        List<(OpenApiDocument.Parameter Parameter, int SourceIndex, bool IsPathLevel)> parameters,
        OpenApiDocument.Parameter newParam)
    {
        for (int i = 0; i < parameters.Count; i++)
        {
            if (newParam.In.Equals(parameters[i].Parameter.In)
                && newParam.Name.Equals(parameters[i].Parameter.Name))
            {
                return i;
            }
        }

        return -1;
    }

    // ── $ref resolution ─────────────────────────────────────────────────
    private static T? ResolveRef<T>(
        OpenApiDocument.Reference reference,
        IOpenApiReferenceResolver solver)
        where T : struct, IJsonElement<T>
    {
        using UnescapedUtf8JsonString refUtf8 = reference.Ref.GetUtf8String();
        return solver.TryResolve<T>(refUtf8.Span, out T r) ? r : null;
    }

    private static bool TryResolveParameter(
        OpenApiDocument.ParameterOrReference paramOrRef,
        IOpenApiReferenceResolver referenceResolver,
        out OpenApiDocument.Parameter resolved)
    {
        OpenApiDocument.Parameter? result = paramOrRef.Match<IOpenApiReferenceResolver, OpenApiDocument.Parameter?>(
            in referenceResolver,
            static (in OpenApiDocument.Reference reference, in IOpenApiReferenceResolver solver) =>
                ResolveRef<OpenApiDocument.Parameter>(reference, solver),
            static (in OpenApiDocument.Parameter parameter, in IOpenApiReferenceResolver _) =>
                parameter);

        if (result is { } r2)
        {
            resolved = r2;
            return true;
        }

        resolved = default;
        return false;
    }

    private static bool TryResolveRequestBody(
        OpenApiDocument.RequestBodyOrReference requestBodyOrRef,
        IOpenApiReferenceResolver referenceResolver,
