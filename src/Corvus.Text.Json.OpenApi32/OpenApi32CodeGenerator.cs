/// <summary>
/// A self-contained code generator that walks the strongly-typed
/// <see cref="OpenApiDocument"/> model directly and emits C# source files.
/// </summary>
/// <remarks>
/// <para>
/// This replaces the multi-stage pipeline (Walker → Walked* → ClientModelBuilder →
/// Client* → ClientCodeEmitter) with a single class that:
/// <list type="number">
/// <item><description>Walks the typed model to collect operation data into lightweight internal records.</description></item>
/// <item><description>Emits C# source files from those records.</description></item>
/// </list>
/// </para>
/// <para>
/// All shared text utilities, scalar emission, and pointer construction are delegated
/// to <see cref="CodeEmitHelpers"/> and <see cref="SchemaPointerBuilder"/>.
/// </para>
/// </remarks>
public sealed class OpenApi32CodeGenerator
{
    private readonly string rootNamespace;
    private readonly string? clientNamePrefix;
    private readonly bool ignoreEmptyFormUrlEncodedBody;
    private readonly IReadOnlyDictionary<string, string> schemaTypeMap;

    /// <summary>
    /// Initializes a new instance of the <see cref="OpenApi32CodeGenerator"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator. Every schema pointer discovered
    /// during the walk must have a corresponding entry.
    /// </param>
    /// <param name="clientNamePrefix">
    /// Optional prefix for client type names. If <see langword="null"/>,
    /// <c>"Api"</c> is used.
    /// </param>
    /// <param name="ignoreEmptyFormUrlEncodedBody">
    /// When <see langword="true"/>, form-urlencoded request bodies whose schema defines
    /// no properties are treated as if the body were absent. Useful for real-world APIs
    /// (e.g. Stripe) that emit empty body definitions.
    /// </param>
    public OpenApi32CodeGenerator(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap,
        string? clientNamePrefix = null,
        bool ignoreEmptyFormUrlEncodedBody = false)
    {
        this.rootNamespace = rootNamespace;
        this.schemaTypeMap = schemaTypeMap;
        this.clientNamePrefix = clientNamePrefix;
        this.ignoreEmptyFormUrlEncodedBody = ignoreEmptyFormUrlEncodedBody;
    }

