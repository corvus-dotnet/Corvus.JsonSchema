using Corvus.Text.Json;
using Corvus.Text.Json.Internal;
using Corvus.Text.Json.OpenApi;
using Corvus.Text.Json.OpenApi.CodeGeneration;

namespace Corvus.Text.Json.OpenApi32;

/// <summary>
/// A self-contained code generator that walks the strongly-typed
/// <see cref="OpenApiDocument"/> model directly and emits C# source files.
/// </summary>
/// <remarks>
/// <para>
/// The generator separates the version-specific <em>walk</em> (over the typed model, producing the
/// shared intermediate representation) from the language-specific <em>emit</em>. The walk lives in
/// <see cref="OpenApi32Walker"/>; the C# emit lives in <see cref="OpenApi32CSharpEmitter"/> and is
/// sequenced by the version-neutral <see cref="ClientEmitDriver"/>. Each public entry point walks
/// the typed model into the intermediate representation and then drives the emitter over it.
/// </para>
/// <para>
/// All shared text utilities, scalar emission, and pointer construction are delegated
/// to <see cref="CodeEmitHelpers"/> and <see cref="SchemaPointerBuilder"/>.
/// </para>
/// </remarks>
public sealed class OpenApi32CodeGenerator
{
    private readonly OpenApi32Walker walker;
    private readonly OpenApi32CSharpEmitter emitter;
    private readonly ClientEmitDriver driver;

    /// <summary>
    /// Initializes a new instance of the <see cref="OpenApi32CodeGenerator"/> class.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeMap">
    /// A map from JSON pointer strings to fully qualified .NET type names
    /// generated by the V5 code generator. Every schema pointer discovered
    /// during the walk must have a corresponding entry.
    /// </param>
    /// <param name="clientNamePrefix">
    /// Optional prefix for client type names. If <see langword="null"/>,
    /// <c>"Api"</c> is used.
    /// </param>
    /// <param name="ignoreEmptyFormUrlEncodedBody">
    /// When <see langword="true"/>, form-urlencoded request bodies whose schema defines
    /// no properties are treated as if the body were absent.
    /// </param>
    public OpenApi32CodeGenerator(
        string rootNamespace,
        IReadOnlyDictionary<string, string> schemaTypeMap,
        string? clientNamePrefix = null,
        bool ignoreEmptyFormUrlEncodedBody = false)
        : this(rootNamespace, new DefaultSchemaTypeResolver(schemaTypeMap), clientNamePrefix, ignoreEmptyFormUrlEncodedBody)
    {
    }

    /// <summary>
    /// Initializes a new instance of the <see cref="OpenApi32CodeGenerator"/> class with an explicit
    /// schema-type resolver.
    /// </summary>
    /// <param name="rootNamespace">The root namespace for generated code.</param>
    /// <param name="schemaTypeResolver">
    /// Resolves a discovered schema pointer (a <see cref="SchemaRef"/>) to a target-language type name.
    /// This is the seam a non-C# emitter (e.g. TypeScript) supplies to resolve pointers to its own type
    /// names; the dictionary-based constructor wraps a pointer map in a <see cref="DefaultSchemaTypeResolver"/>.
    /// </param>
    /// <param name="clientNamePrefix">
    /// Optional prefix for client type names. If <see langword="null"/>, <c>"Api"</c> is used.
    /// </param>
    /// <param name="ignoreEmptyFormUrlEncodedBody">
    /// When <see langword="true"/>, form-urlencoded request bodies whose schema defines no properties are
    /// treated as if the body were absent.
    /// </param>
    public OpenApi32CodeGenerator(
        string rootNamespace,
        ISchemaTypeResolver schemaTypeResolver,
        string? clientNamePrefix = null,
        bool ignoreEmptyFormUrlEncodedBody = false)
    {
        this.walker = new OpenApi32Walker(clientNamePrefix, ignoreEmptyFormUrlEncodedBody);
        this.emitter = new OpenApi32CSharpEmitter(
            rootNamespace,
            clientNamePrefix,
            ignoreEmptyFormUrlEncodedBody,
            schemaTypeResolver,
            this.walker);
        this.driver = new ClientEmitDriver();
    }

    /// <summary>
    /// Walks the OpenAPI 3.2 specification and collects all schema
    /// JSON Pointer strings reachable from the selected operations.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">
    /// Optional reference resolver. If <see langword="null"/>,
    /// a <see cref="LocalReferenceResolver"/> is used.
    /// </param>
    /// <returns>An array of JSON Pointer strings.</returns>
    public static SchemaReference[] CollectSchemaPointers(
        JsonElement specRoot,
        out Dictionary<string, string> parameterNames,
        OperationFilter? filter = null,
        IOpenApiReferenceResolver? referenceResolver = null)
    {
        referenceResolver ??= new LocalReferenceResolver(specRoot);
        OpenApiDocument doc = specRoot;

        if (doc.PathsValue.IsUndefined())
        {
            parameterNames = new Dictionary<string, string>();
            return [];
        }

        List<SchemaReference> pointers = [];
        Dictionary<string, string> paramNames = new(StringComparer.Ordinal);

        foreach (JsonProperty<OpenApiDocument.PathItem> pathProp in doc.PathsValue.EnumerateObject())
        {
            if (filter is not null)
            {
                using UnescapedUtf16JsonString name = pathProp.Utf16NameSpan;
                if (!filter.Matches(name.Span))
                {
                    continue;
                }
            }

            OpenApi32Walker.CollectPathItemPointers(pathProp, pointers, paramNames, referenceResolver, "paths"u8);
        }

        parameterNames = paramNames;
        return [.. pointers];
    }

    /// <summary>
    /// Lists all operations in the OpenAPI 3.2 specification, optionally filtered.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <returns>An array of <see cref="OperationSummary"/> records.</returns>
    public static OperationSummary[] ListOperations(
        JsonElement specRoot,
        OperationFilter? filter = null)
    {
        List<OperationSummary> result = [];

        foreach (OpenApi32Walker.OperationRef opRef in OpenApi32Walker.WalkOperationRefs(specRoot, filter))
        {
            using UnescapedUtf16JsonString pathName = opRef.PathProp.Utf16NameSpan;
            string path = pathName.Span.ToString();

            string? operationId = opRef.Operation.OperationId.IsNotUndefined()
                ? opRef.Operation.OperationId.GetString()
                : null;

            string? summary = opRef.Operation.Summary.IsNotUndefined()
                ? opRef.Operation.Summary.GetString()
                : null;

            bool isDeprecated = opRef.Operation.Deprecated.ValueKind == JsonValueKind.True;

            List<string> tags = [];
            if (opRef.Operation.Tags.IsNotUndefined())
            {
                foreach (var tag in opRef.Operation.Tags.EnumerateArray())
                {
                    if (tag.ValueKind == JsonValueKind.String)
                    {
                        tags.Add(tag.GetString()!);
                    }
                }
            }

            int paramCount = 0;
            if (opRef.Operation.ParametersValue.IsNotUndefined())
            {
                foreach (var param in opRef.Operation.ParametersValue.EnumerateArray())
                {
                    _ = param;
                    paramCount++;
                }
            }

            // Also count path-level parameters
            if (opRef.PathItem.ParametersValue.IsNotUndefined())
            {
                foreach (var param in opRef.PathItem.ParametersValue.EnumerateArray())
                {
                    _ = param;
                    paramCount++;
                }
            }

            bool hasBody = opRef.Operation.RequestBody.IsNotUndefined();

            result.Add(new OperationSummary(
                path,
                opRef.Method,
                operationId,
                [.. tags],
                isDeprecated,
                paramCount,
                hasBody,
                summary));
        }

        return [.. result];
    }

    /// <summary>
    /// Lists all tags defined in the top-level <c>tags</c> array of an OpenAPI 3.2 specification,
    /// including the <c>parent</c>, <c>kind</c>, and <c>summary</c> fields introduced in 3.2.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <returns>An array of <see cref="TagInfo"/> records.</returns>
    public static TagInfo[] ListTags(JsonElement specRoot)
        => OpenApiWalkerBase.ListTags(specRoot);

    /// <summary>
    /// Walks the OpenAPI 3.2 specification and emits C# source files.
    /// </summary>
    /// <param name="specRoot">The root element of the parsed spec document.</param>
    /// <param name="filter">Optional operation filter.</param>
    /// <param name="referenceResolver">
    /// Optional reference resolver. If <see langword="null"/>,
    /// a <see cref="LocalReferenceResolver"/> is used.
    /// </param>
    /// <returns>The generated source files.</returns>
    public IReadOnlyList<GeneratedFile> Generate(
        JsonElement specRoot,
        OperationFilter? filter = null,
        IOpenApiReferenceResolver? referenceResolver = null)
    {
        referenceResolver ??= new LocalReferenceResolver(specRoot);
        ServerInfo? rootServer = OpenApi32Walker.GetDefaultServerInfo(specRoot);
        List<OperationInfo> operations = [.. this.walker.Walk(specRoot, filter, referenceResolver)];

        return this.driver.EmitClient(this.emitter, operations, specRoot, referenceResolver, rootServer);
    }

