using System.Reflection;

#if NET8_0_OR_GREATER
using System.Runtime.Loader;
#endif

using System.Text;
using Corvus.Json.CodeGeneration;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.Emit;
using Microsoft.Extensions.DependencyModel;

namespace Corvus.Text.Json.Validator;

/// <summary>
/// Compiles dynamically-generated C# code into an assembly and resolves the root type.
/// </summary>
public static class DynamicCompiler
{
    private const string GlobalUsingStatements =
        """
        // <auto-generated/>

        global using global::System;
        global using global::System.Collections.Generic;
        global using global::System.IO;
        global using global::System.Linq;
        """;

#if NET8_0_OR_GREATER
    private static readonly DynamicAssemblyLoadContext PluginAssemblyLoadContext = new();
#endif

    // Cache metadata references per host assembly to avoid re-reading PE metadata
    // from hundreds of DLLs on every compilation. The references depend only on the
    // host assembly's compilation context, which doesn't change across compilations.
    private static readonly object s_referenceCacheLock = new();
    private static Assembly? s_cachedHostAssembly;
    private static IReadOnlyList<MetadataReference>? s_cachedReferences;
    private static IReadOnlyList<string?>? s_cachedDefines;

    /// <summary>
    /// Compile the generated code files and return the exported type with the given fully-qualified name.
    /// </summary>
    /// <param name="rootTypeName">The fully-qualified .NET type name of the root type.</param>
    /// <param name="generatedCode">The generated code files.</param>
    /// <param name="hostAssembly">The host assembly whose compilation context provides metadata references.</param>
    /// <returns>The compiled <see cref="Type"/>.</returns>
    /// <exception cref="InvalidOperationException">Compilation failed or the root type was not found.</exception>
    public static Type CompileGeneratedType(
        string rootTypeName,
        IReadOnlyCollection<GeneratedCodeFile> generatedCode,
        Assembly hostAssembly)
    {
        using MemoryStream outputStream = EmitOrThrow(generatedCode, hostAssembly);
        Assembly generatedAssembly = LoadAssembly(outputStream);
        return generatedAssembly.ExportedTypes.Single(t => t.FullName == rootTypeName);
    }

    /// <summary>
    /// Compile the generated code files into a single assembly and return its raw PE (DLL) bytes, without
    /// loading it. Used to produce a storable assembly artifact (e.g. baked into a workflow package).
    /// </summary>
    /// <param name="generatedCode">The generated code files.</param>
    /// <param name="hostAssembly">The host assembly whose compilation context provides metadata references.</param>
    /// <returns>The emitted assembly bytes.</returns>
    /// <exception cref="InvalidOperationException">Compilation failed.</exception>
    public static byte[] CompileToAssemblyBytes(
        IReadOnlyCollection<GeneratedCodeFile> generatedCode,
        Assembly hostAssembly)
    {
        using MemoryStream outputStream = EmitOrThrow(generatedCode, hostAssembly);
        return outputStream.ToArray();
    }

    private static MemoryStream EmitOrThrow(IReadOnlyCollection<GeneratedCodeFile> generatedCode, Assembly hostAssembly)
    {
        (IReadOnlyList<MetadataReference> references, IReadOnlyList<string?> defines) =
            GetOrBuildMetadataReferences(hostAssembly);

        IEnumerable<SyntaxTree> syntaxTrees = ParseSyntaxTrees(generatedCode, defines);

        CSharpCompilationOptions options = new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary)
            .WithAssemblyIdentityComparer(DesktopAssemblyIdentityComparer.Default);

        var compilation = CSharpCompilation.Create(
            $"Corvus.Dynamic.GeneratedTypes_{Guid.NewGuid()}",
            syntaxTrees,
            references,
            options);

        MemoryStream outputStream = new();
        EmitResult result = compilation.Emit(outputStream);

        if (!result.Success)
        {
            outputStream.Dispose();
            string errors = BuildCompilationErrors(result);
            throw new InvalidOperationException(
                "Unable to compile generated code\r\n" + errors);
        }

        outputStream.Flush();
        outputStream.Position = 0;
        return outputStream;
    }

    private static (IReadOnlyList<MetadataReference> MetadataReferences, IReadOnlyList<string?> Defines)
        GetOrBuildMetadataReferences(Assembly hostAssembly)
    {
        lock (s_referenceCacheLock)
        {
            if (s_cachedReferences is not null && ReferenceEquals(s_cachedHostAssembly, hostAssembly))
            {
                return (s_cachedReferences, s_cachedDefines!);
            }

            (IReadOnlyList<MetadataReference> references, IReadOnlyList<string?> defines) =
                BuildMetadataReferencesAndDefines(hostAssembly);

            s_cachedHostAssembly = hostAssembly;
            s_cachedReferences = references;
            s_cachedDefines = defines;

            return (references, defines);
        }
    }

    private static (IReadOnlyList<MetadataReference> MetadataReferences, IReadOnlyList<string?> Defines)
        BuildMetadataReferencesAndDefines(Assembly hostAssembly)
    {
        DependencyContext? ctx = DependencyContext.Load(hostAssembly) ?? DependencyContext.Default;

        List<MetadataReference> refs;
        List<string?> defines = ["DYNAMIC_BUILD"];

        if (ctx is not null)
        {
            refs = (
                from l in ctx.CompileLibraries
                from r in TryResolveReferencePaths(l)
                select (MetadataReference)MetadataReference.CreateFromFile(r)).ToList();

            defines.AddRange(ctx.CompilationOptions.Defines);
        }
        else
        {
            refs = [];
        }

        // Always supplement — even when DependencyContext resolves framework assemblies,
        // they may be facades (e.g. mscorlib 2.0.0.0 from NuGet reference assembly packages)
        // that cause CS1705 version mismatch. AppDomain has the real GAC versions.
        SupplementWithDirectoryAndAppDomain(refs, hostAssembly);

        return (refs, defines);
    }

    private static IEnumerable<string> TryResolveReferencePaths(CompilationLibrary library)
    {
        try
        {
            return library.ResolveReferencePaths();
        }
        catch (InvalidOperationException)
        {
            // Some reference assembly entries (e.g., System.ValueTuple.Reference) may not be
            // resolvable when the framework reference assemblies are not available. These types
            // are typically in-box in the target framework and reachable through other references.
            return [];
        }
    }

    private static void SupplementWithDirectoryAndAppDomain(List<MetadataReference> references, Assembly hostAssembly)
    {
        // On .NET Framework, assemblies may be shadow-copied to a temp directory,
        // so hostAssembly.Location may not be in the original output directory.
        // Use AppDomain.CurrentDomain.BaseDirectory which points to the original output directory.
        string? directory = AppDomain.CurrentDomain.BaseDirectory;

        // Fall back to the host assembly's directory if BaseDirectory is not available
        if (string.IsNullOrEmpty(directory) || !Directory.Exists(directory))
        {
            string? assemblyLocation = hostAssembly.Location;
            directory = !string.IsNullOrEmpty(assemblyLocation) ? Path.GetDirectoryName(assemblyLocation) : null;
        }

        // Seed seenNames from references already resolved by DependencyContext
        // so we don't add duplicates.
        HashSet<string> seenNames = new(StringComparer.OrdinalIgnoreCase);
        foreach (MetadataReference r in references)
        {
            if (r is PortableExecutableReference peRef && peRef.FilePath is string path)
            {
                seenNames.Add(Path.GetFileNameWithoutExtension(path));
            }
        }

        // AppDomain assemblies are the authoritative runtime assemblies (e.g. mscorlib
        // 4.0.0.0 from the GAC). When DependencyContext resolved a facade with the same
        // name (e.g. mscorlib 2.0.0.0 from NuGet reference assemblies), replace it.
        foreach (Assembly a in AppDomain.CurrentDomain.GetAssemblies())
        {
            if (!a.IsDynamic && !string.IsNullOrEmpty(a.Location))
            {
                string name = a.GetName().Name ?? Path.GetFileNameWithoutExtension(a.Location);
                if (!seenNames.Add(name))
                {
                    references.RemoveAll(r =>
                        r is PortableExecutableReference peRef &&
                        peRef.FilePath is string p &&
                        Path.GetFileNameWithoutExtension(p).Equals(name, StringComparison.OrdinalIgnoreCase));
                }

                references.Add(MetadataReference.CreateFromFile(a.Location));
            }
        }

        // Then scan DLLs in the output directory for project dependencies not yet
        // loaded into the AppDomain.
        if (directory is not null && Directory.Exists(directory))
        {
            foreach (string dll in Directory.EnumerateFiles(directory, "*.dll"))
            {
                string simpleName = Path.GetFileNameWithoutExtension(dll);
                if (seenNames.Add(simpleName))
                {
                    try
                    {
                        // Validate the DLL is a managed assembly before adding it.
                        // Native DLLs (e.g. coverage instrumentation, msdia140.dll)
                        // pass MetadataReference.CreateFromFile but cause CS0009
                        // "PE image doesn't contain managed metadata" at compile time.
                        AssemblyName.GetAssemblyName(dll);
                        references.Add(MetadataReference.CreateFromFile(dll));
                    }
                    catch
                    {
                        // Skip DLLs that aren't managed assemblies or can't be loaded
                        seenNames.Remove(simpleName);
                    }
                }
            }
        }

        // On .NET Framework, resolve referenced assemblies that may be in the GAC or
        // reference assembly directories but not in the output directory or loaded AppDomain.
        // This catches assemblies like System.Numerics and netstandard that are transitively referenced.
        ResolveTransitiveReferences(references, seenNames);
    }

    private static void ResolveTransitiveReferences(List<MetadataReference> references, HashSet<string> seenNames)
    {
        Queue<AssemblyName> toResolve = new();

        // Gather referenced assemblies from all assemblies in the output directory
        foreach (Assembly a in AppDomain.CurrentDomain.GetAssemblies())
        {
            if (!a.IsDynamic)
            {
                foreach (AssemblyName refName in a.GetReferencedAssemblies())
                {
                    toResolve.Enqueue(refName);
                }
            }
        }

        while (toResolve.Count > 0)
        {
            AssemblyName name = toResolve.Dequeue();

            try
            {
                var resolved = Assembly.Load(name);
                if (!resolved.IsDynamic && !string.IsNullOrEmpty(resolved.Location) && seenNames.Add(resolved.GetName().Name ?? Path.GetFileNameWithoutExtension(resolved.Location)))
                {
                    references.Add(MetadataReference.CreateFromFile(resolved.Location));
                }
            }
            catch
            {
                // Assembly could not be loaded — skip it
            }
        }
    }

    private static IEnumerable<SyntaxTree> ParseSyntaxTrees(
        IReadOnlyCollection<GeneratedCodeFile> generatedTypes,
        IEnumerable<string?> defines)
    {
        CSharpParseOptions parseOptions = CSharpParseOptions.Default
            .WithLanguageVersion(LanguageVersion.Preview)
            .WithPreprocessorSymbols(defines.Where(s => s is not null).Cast<string>());

        yield return CSharpSyntaxTree.ParseText(
            GlobalUsingStatements, options: parseOptions, path: "GlobalUsingStatements.cs");

        foreach (GeneratedCodeFile type in generatedTypes)
        {
            yield return CSharpSyntaxTree.ParseText(
                type.FileContent, options: parseOptions, path: type.FileName);
        }
    }

    private static string BuildCompilationErrors(EmitResult result)
    {
        StringBuilder builder = new();
        foreach (Diagnostic diagnostic in result.Diagnostics)
        {
            builder.AppendLine(diagnostic.ToString());
        }

        return builder.ToString();
    }

#if NET8_0_OR_GREATER
    private static Assembly LoadAssembly(MemoryStream outputStream)
    {
        return PluginAssemblyLoadContext.LoadFromStream(outputStream);
    }

    private sealed class DynamicAssemblyLoadContext : AssemblyLoadContext
    {
        public DynamicAssemblyLoadContext()
            : base($"DynamicAssemblyLoadContext_{Guid.NewGuid():N}", isCollectible: true)
        {
        }
    }
#else
    private static Assembly LoadAssembly(MemoryStream outputStream)
    {
        return Assembly.Load(outputStream.ToArray());
    }
#endif
}